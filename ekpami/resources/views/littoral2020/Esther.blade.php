            <p>Diverses bourses et stages professionnels en Belgique ont permis à Esther Bigo de perfectionner sa pratique de photographe autodidacte. Son travail et son intérêt pour le développement de la création photographique, l’importance de la transmission l’a amenée à mobiliser des associations de photographes et les réunir. Elle est aujourd’hui la prési-dente de la Fédération des artistes photographes du Bénin qui  assure  une  meilleure  visibilité  et  la  coopérativité  de  leurs  actions.  Elle  participe  de  la  formation  en  art  photo-graphique, en montage-vidéo, fait intervenir des profession-nels pour parler de la propriété intellectuelle et du droit à l’image et œuvre pour la création d’une banque de l’image. L’association qu’elle dirige à Abomey, porte un regard parti-culier sur l’accession des femmes à ce métier. Elle présente à  diverses  occasions  la  musique  traditionnelle  Akonhoun,  rythme  d’Abomey  à  l’extérieur,  initie  un  festival  pour  des  jeunes vidéastes.... Esther Bigo qui a commencé par faire des reportages  «  a  aiguisé  son  regard  à  l’école  de  la  rue.  C’est  là qu’elle apprend à chercher passionnément les regards de ses modèles, c’est là qu’elle explore la lumière du Bénin ».  dira  d’elle  un  journaliste.  De  ses  photos  argentiques  elle  souhaite  faire  des  peintures,  elle  les  transforme,  joue  avec  le flou, se concentre sur un détail et les imprime aujourd’hui sur des plaques de polystyrène. Des modèles, humains, pay-sages  réels  ou  imaginaires,  animaux,  l’environnement  et  le  temps sont ses préoccupations majeures. Les hommes dans leur  quotidien,  des  villes  imaginaires,  des  lézards  comme  exemples de vie à préserver et le temps qu’elle veut conte-nir dans le maintenant, non pas destructeurs mais allié. De l’instinct à l’instant, les cartes mémoire d’Esther Bigo. F.B
