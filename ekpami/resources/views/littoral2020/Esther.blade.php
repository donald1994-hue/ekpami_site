            <p>Various scholarships and professional internships in Belgium have allowed Esther Bigo to  perfect  her practice as a self-taught photographer. Her work and her interest in the development of photogra-phic  creation,  the  importance  of  transmission  has  led  her  to  mobilize  associations  of  photographers  and  bring  them  together.  Today  she  is  the  president  of  the  Photographic  Artists of Benin Federation, which ensures a better visibility and cooperation of their actions. She takes part to training in  photographic  art  and  video  editing,  brings  in  professio-nals  to  talk  about  intellectual  property  and  image  rights  and  works  to  create  an  image  bank.  The  association  that  she directs in Abomey, has a particular interest in women’s access  to  this  profession.  It  presents  on  various  occasions  the traditional music Akonhoun, Abomey’s rhythm outdoors, initiates  a  festival  for  young  video  makers  ...  Esther  Bigo,  who  began  by  making  reports,  ‘‘sharpened  her  gaze  at  the  street school. It is there that she learns to passionately seek the glances of her models, it is there that she explores the light  of  Benin’’.  a  journalist  would  say  about  her.  From  her  silver  photos  she  wants  to  make  paintings,  she  transforms  them,  plays  with  the  blur,  focuses  on  a  detail  and  prints  them  today  on  polystyrene  plates.  Models,  humans,  real  or  imaginary  landscapes,  animals,  the  environment  and  time  are her major preoccupations. Humans in their everyday life, imaginary cities, lizards as examples of life to be preserved and the time she wants to contain in the now, not destruc-tive  but  allied.  From  instinct  to  the  moment,  Esther  Bigo’s  memory cards. F.B.
            </p>
