            <p>More  than  two  decades  of  hard  work  and  assiduous  research  have  allowed  him  to  specialize,  through  his  works,  in  the  profound,  divine,  spiritual, mystical Vodoun. His works, which are unobtrusive, uncompromising  and  penetrating,  have  all  been  conceived  and  created  within  the  very  walls  of  the  Royal  Palace  of  Toffo.  They  are  thus  imbued  with  the  sacred  imprint,  and  evoke the essence of vodun and of a tradition full of secrets and  mysteries,  eminently  symbolic  and  strong  of  ancestral  presences.  For  both  his  paintings  and  sculptures,  his  work  is  based  on  his  perfect  knowledge  of  both  ancestral  and  modern  spiritual  and  cultural  rites,  which  moreover  easily  translate  his  deep  degree  of  initiation,  his  involvement  in  the vodun culture and his passion for traditional cults. Each of  his  creations  draws  its  essence  from  Nature  and  eve-rything can be explained by and from it. Warm, lively, exhila-rating, they are a source of discovery, teaching and richness. Impossible to describe all the reflections behind the power of the gaze and the precision of the gestures of His Majesty AGBODANDE III. His simplicity stems from his humility and his deep respect for Nature. The slightest action taken takes into account its impact on the things of life. G.L (adapted)
