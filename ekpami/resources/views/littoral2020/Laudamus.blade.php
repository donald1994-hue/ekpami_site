            <p>A  dozen  exhibitions  and  three  sub-jects:  Fa,  women  and  Vodun.  Lauda-mus  Segbo  tries  to  circumscribe  the  Fâ  with Fâ, langage des dieux (Fâ, language of the gods), Rêve flou (Blurry dream), Hommage à la femme (Tribute to  wo-man)  le Vodoun. of living sculptures, naked women progres-sively dressed by body painting. Laudamus comes from the depths of the bowels of the Savalou royalty, a buttress that remains to a thorough knowledge of Vodun, its functioning, the  stages  and  scale  of  its  divinities,  its  relationship  with  the  Fâ  and  the  irreplaceable  role  that  woman  has  played  since the dawn of time. He, who remains keenly aware that this  endogenous  religious  system  is  feared  because  of  the  harmful exploitation of men, makes us feel that the religion of Vodun is first and foremost the first edge of the knife that is used to cut tomatoes, onions or simply to cut a flower to make it look more beautiful. The warmth of the colours, the finesse of the priestesses’ lines, the beauty and elegance of their  dances,  the  force  of  communication  displayed  by  the  faithful, the reliefs of the fabric lines to display the instru-ments  of  Vodun  or  Fâ.  If  The  gods  ride  men,  it  is  because  they tame them, ‘‘possess’’ them, to make themselves known, to  materialize  their  personality,  their  strength,  their  power,  their  voice,  their  usefulness,  their  duality,  their  complexity.  To decipher the unity of Vodun, its divinity, its love of huma-nity, its intrinsic and authentic religious character, the value of Vodun for the Beninese, for the African, for mankind: this is what Laudamus challenges himself to. M.K. (adapted)
