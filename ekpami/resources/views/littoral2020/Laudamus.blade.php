            <p>Une douzaine d’expositions et trois sujets : le Fâ, la femme et  le  Vodoun.  Laudamus  Segbo  tente  de  circonscrire  le  Fâ  avec Fâ, langage des dieux, Rêve flou, Hommage à la femme, le  Vodoun,  par  des  performances  de  sculptures  vivantes,  femmes  nues  habillées  progressivement  par  le  body  pain-ting. Laudamus vient du tréfonds des entrailles de la royauté savaloise,  arc-bouté  qu’il  reste  à  une  connaissance  appro-fondie du Vodoun, de son fonctionnement, des étapes et de l’échelle de ses divinités, de sa relation avec le Fâ et du rôle irremplaçable que la femme y joue depuis la nuit des temps. Lui, qui reste âprement conscient que ce système religieux endogène  est  redouté  du  fait  d’une  exploitation  nuisible  des  hommes,  il  fait  sentir  que  la  religion  du  Vodoun  est  d’abord  le  premier  tranchant  du  couteau  qui  sert  à  couper  de la tomate, des oignons ou, simplement, à tailler une fleur pour  la  faire  percevoir  plus  belle.  La  chaleur  des  couleurs,  la finesse du trait des prêtresses, la beauté, l’élégance de leurs  danses,  la  force  de  communication  que  déploient  les  fidèles, les reliefs de lignes de tissus pour mettre en vue les instruments du Vodoun ou du Fâ. Si Les dieux chevauchent les hommes, c’est qu’ils les domptent, les « possèdent », pour se faire connaître, pour matérialiser leur personnalité, leur force, leur puissance, leur voix, leur utilité, leur dualité, leur complexité.  Faire  décrypter  l’unité  du  Vodoun,  sa  divinité,  son  amour  de  l’humanité,  son  caractère  religieux  intrin-sèque et authentique, la valeur du Vodoun pour le Béninois, pour l’Africain, pour l’homme : le défi que se lance Laudamus. 
