            <p>Edwige Aplogan est maître de cérémonies. Si dans son pays on sort les masques pour les faire danser, elle, elle sort les bâtiments, les monuments, les espaces publics de l’indiffé-rence, les habille, et les pare, non pas pour les masquer mais les dévoile et leur fait porter le sens de ses questionnements sur  les  indépendances,  de  façade,  sur  les  combats  pour  la  liberté, leurs illusions et désillusions, sur la mondialisation, la  globalisation...  Politique,  disait  Hannah  Arendt,  signifie agir ensemble. On ne devient citoyen que par son irruption dans l’espace public, qui est aussi la sortie de l’espace privé.« Espace d’apparence », « où la liberté peut devenir une réa-lité tangible ». Les hommes y font l’expérience de la liberté.Edwige  Aplogan  est  politique.  Son  travail  s’enquière  à  tra-vers les réflexions qu’il soulève sur l’avenir et le devenir des sociétés,  des  êtres,  hommes  et  femmes.  Sur  ses  toiles,  les  individus  différents  ne  s’opposent  pas,  ils  fusionnent  et  se  confondent, les corps avec leurs attributs du genre portent la tête du sexe opposé, les différences d’origine, d’opinion, de culture, les fécondent et les complètent. Son legba, divinité du  panthéon  vodun,  est  électrique,  tout  en  néons,  c’est  par  la  lumière  des  valeurs  ancestrales  qu’il  porte,  non  pas  par  l’obscurité  des  interdits  qu’il  protège,  irrationalité  filtrée, intronisé  ainsi  dans  le  monde  moderne,  il  dit  que  dans  un  monde cosmopolite, les origines servent de repères à l’iden-tité. « Si tu ne peux exprimer courageusement tes pensées, donne la parole aux griots ». Edwige Aplogan est griot. Ses espaces  drapés  par  la  mémoire,  sont  surtout  drapés  par  le  courage de dire et de penser. Ancien avocat au Barreau de Paris, membre de l’Aica Bénin, elle vit et travaille à Cotonou et à Paris. F.B.
