            <p>Edwige Aplogan is master of ceremonies. If  in  her  country  one  takes  out  masks  to  make  them  dance,  she  takes  the  buildings,  monuments,  public  spaces  out  of  indifference,  dresses  and  adorns them, not to mask them but to reveal and make them carry the meaning of her questionings on independence, on facade, on fights for freedom, their illusions and disillusions, on  globalization...  As  Hannah  Arendt  said,  politics  means  acting  together.  One  only  becomes  a  citizen  by  entering  the public space, which is also the exit of the private space.«Space of appearance», «where freedom can become a tan-gible  reality».  «This  is  where  people  experience  freedom.»  Edwige Aplogan is political. Her work inquires through the reflections  she  raises  on  the  future  and  the  becoming  of societies, of beings, men and women. On her canvases, dif-ferent  individuals  do  not  oppose  each  other,  they  mix  and  merge,  bodies  with  their  gender  attributes  carry  the  head  of  the  opposite  sex,  differences  of  origin,  opinion,  culture,  fertilize  and  complement  them.  Her  legba,  divinity  of  the  voodoo  pantheon,  is  electric,  all  neon,  it  is  by  the  light  of  the ancestral values it carries, not by the darkness of the for-bidden that it protects, filtered irrationality, thus enthroned in the modern world, she says that in a cosmopolitan world, origins serve as landmarks of identity. «If you can’t express your thoughts courageously, give the griots a voice. «Edwige Aplogan is a griot. Her spaces, draped by memory, are mostly draped  by  the  courage  to  say  and  think.  Former  lawyer  at  the Paris Bar, member of Aica Benin, she lives and works in Cotonou and Paris. F.B
            </p>
