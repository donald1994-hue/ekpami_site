    <div class="col-md-6 artiste-content-traduction">
            <p>Lucien Houessou appears late in the Beninese artistic land-scape, when he participates to Waba, first Open Studios in 2010. His evolution took place in a closed, sheltered space. Reader,  interested  in  philosophy,  photographer  by  trade  having spent more then twenty years in Camerun, for Kaman Esso the individual is a messenger and to communicate, he is  inspired  by  what  he  sees  and  hears.  For  him,  these  are  poems, proverbs and tales. La force aveugle fait basculer la terre, Blind  force  tips  the  earth  over,  Dans  cette  existence  où  on  se  découvre  chacun  se  fait  un  masque,  In  this  exis-tence where one discovers oneself, each one makes a mask... These adages sometimes appear on his canvases which thus take prophetic accents. His series, ‘‘Les mains de la vie’’, The Hands of Life, is an extension of his thinking. Hands plowing a field or holding a book, hands in a workshop, hands writing or cooking, skilled and precious hands, tool hands. A series like  a  blueprint  that  reveals  with  deep  simplicity  through  the  hands,  trades.  Paintings  like  adages.  Attentive  to  the  human  being,  to  what  feeds  him,  corrupts,  makes  him  live:  religion,  creation,  work,  desires,  sins,  constraints,  he  paints  the reflections of his life, he also collects them in writings. If his painting may seem illustrative, it is an illustration not of what we see, but of what the thought projects in the back-ground to guide us. F.B.
            </p>
