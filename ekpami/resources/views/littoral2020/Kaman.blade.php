            <p>Lucien Houessou apparaît tard dans le paysage artistique, avec  Waba,  les  Portes  ouvertes  des  ateliers  d’artistes  en  2010. Son évolution s’est faite en vase clos, à l’abri. Lecteur, intéressé  par  la  philosophie,  photographe  de  métier  ayant  passé  une  vingtaine  d’années  au  Cameroun,  pour  Kaman  Esso  l’individu  est  un  messager  et  pour  communiquer,  il  s’inspire  de  ce  qu’il  voit  et  entend.  Pour  lui,  ce  sont  les  poèmes,  les  proverbes  et  les  contes.  La  force  aveugle  fait  basculer  la  terre,  Dans  cette  existence  où  on  se  découvre,  chacun se fait un masque... Ces adages aparaissent parfois sur  ses  toiles  qui  prennent  des  accents  prophétiques.  Sa  série, «Les mains de la vie», prolonge sa pensée. Mains qui labourent  un  champ  ou  qui  tiennent  un  livre,  mains  dans  un  atelier,  mains  qui  écrivent  ou  cuisinent,  mains  habiles  et  précieuses,  mains  outils.  Une  série  comme  une  épure  qui révèle avec une simplicité profonde à travers des mains des  métiers.  Des  toiles  comme  des  adages.  Attentif  à  l’être  humain, à ce qui le nourrit, corrompe, fait vivre : la religion, la création, le travail, les désirs, les pêchés, les contraintes, il peint les réflexions de sa vie, il les recueille aussi dans des écrits. Si sa peinture peut paraître illustrative, c’est une illus-tration non pas de ce qu’on voit, mais de ce que la pensée projette en arrière-plan pour nous guider.  F.B.
            </p>


