    <div class="col-md-6 artiste-content-traduction">
            <p>Claude  Koffi  Alodji  uses  the  computer  to  define  the  fra-mework  of  his  painting.  Computer  scientist  by  trade,  self-taught painter, he participates in a group exhibition where his painting representing the country’s coat of arms will be acquired  to  appear  in  the  boardroom  of  the  state’s  former  presidency.  As  he  likes  to  say,  this  ‘‘trial  run,  masterstroke’’  and a meeting with the artist Pascal Avokpo, who will refine his  technique  and  guide  him  in  his  research,  will  be  deci-sive in his work. Professional training, life in other cities, in other countries, Côte d’Ivoire, Lagos in Nigeria, also formed his  spirit.  Attracted  by  the  representations  of  wild  animals’  life,  he  will  mainly  make  portraits  and  masks  in  balanced  compositions of which Marcel Kpogodé, a cultural journalist will say in Le Mutateur ‘‘Warm, bright, shiny, the colours fill the lines, spaces end up becoming faces sometimes unique, other times multiple which interpenetrate, are decomposed, burst, Kandoté brings out a mirror of human daily life dyed with feelings’’.F.B.
            </p>
