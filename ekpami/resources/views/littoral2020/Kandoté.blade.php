            <p>Claude Koffi Alodji se sert de l’ordinateur pour définir la trame de sa peinture. Informaticien de métier, peintre autodidacte, il participe en grand débutant à une exposition collective où son  tableau  représentant  les  armoiries  du  pays  sera  acquis  pour figurer dans la salle de conseil de l’ancienne présidence de  l’état.  Comme  il  se  plaît  à  dire,  ce  «  coup  d’essai,  coup de  maître  »  et  une  rencontre  avec  l’artiste  Pascal  Avokpo qui affinera sa technique et l’orientera dans ses recherches, seront déterminants dans son travail. Les formations profes-sionnelles, la vie dans d’autres villes, dans d’autres pays, Côte d’Ivoire,  Lagos  au  Nigéria,  ouvertures  qui  forment  aussi  un  esprit. Attiré par la représentations de la vie des animaux sau-vages, il fera surtout des portraits et des masques dans des compositions  équilibrées  dont  Marcel  Kpogodé,  journaliste  culturel,  dira  dans  Le  Mutateur  «  Chaudes,  vives,  brillantes,  les couleurs emplissent des tracés, les espaces finissent par devenir des visages tantôt uniques, tantôt multiples qui s’in-terpénètrent, sont décomposés, éclatés, Kandoté fait ressortir un miroir du quotidien humain teinté des sentiments. » F.B.
            </p>


    </div>
