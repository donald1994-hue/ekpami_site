            <p>For  Oswald  Adandè  the  artist  is  one  who  creates  beauty.  Even if he denounces demagogy, injustice, poverty. Oswald’s boxes  -  showcases  where  we  discover  miniaturized  sculp-tures in carefully carved and painted calabashes, represen-ting animals, characters on foot, by bicycle, cars, rickshaws, attached to buildings made of painted cardboard, recreating a  whole  world  that  flowes  and  swarms  in  the  cityscape - are  jewels  of  his  Scenes  of  street  life.  Cutouts  of  everyday  life,  they  reconstruct  it  while  imposing  a  new  reading.  On  the  buildings  he  inscribes  false  slogans,  false  advertising  panels,  diverts  the  names  of  the  institutions,  and,  fruit  of  his  society  observation,  he  caricatures  the  surrounding  demagogy,  takes  off  the  masks.  Meticulous  work,  patience,  sassy  imagination  and  above  all,  sense  of  observation.  The  playful-decorative appearance is the hiding spot of a subtle art,  which  by  miniaturizing  the  tares,  compels  our  gaze  to  linger, to better understand, shields us from the temptation to minimize them. FB
