            <p>A  degree  in  cultural  administration,  two  masters  degrees,  cinema  and  art  consulting,  training  in  fashion  styling  and  design, economic operator, a great experience as a costume designer in the theatre. The only field where Patrice Tomedé dared to be self-taught is painting. On a grey canvas where black  spots  occupy  the  upper  part,  several  rows  of  small  squares  of  fabric  of  slightly  different  sizes  at  regular  dis-tances from each other that stand out from the background by their relief occupy the other half. Two of these pieces are coloured,  on  the  sober  canvas,  it  is  towards  them  that  the  eye is focused. A horizontal line separates the two parts of the painting, some dark spots go down on one side. Shadows threatening humanity, which is running to its doom? A self-destructive humanity heading towards the shadows? Links, uniformity,  but  also  unicity  and  differences...  The  readings  are multiple, a theme, patterns, symbols, imaginary life in the colors that flow, veins that irrigate the canvases. Individuals stand out but advance with the others on their way to the unknown that is the world they are trying to tame. Pat’ace is concerned about the protection of the environment and the future  of  society.  As  a  cultural  consultant,  Pat’ace  puts  his  knowledge at the service of the community. F.B.

