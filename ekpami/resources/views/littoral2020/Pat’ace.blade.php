            <p>Une  licence  en  administration  culturelle,  deux  masters,  cinéma et conseil d’art, une formation en stylisme et design de mode, d’opérateur économique, une belle expérience de costumier au théâtre. Le seul domaine où Patrice Tomede a osé être autodidacte est la peinture. Sur une toile grise où des  taches  noires  occupent  la  partie  supérieure,  plusieurs  rangées de petits carrés de tissu de tailles légèrement dif-férentes à distance régulière l’un de l’autre qui se détachent du fond par leur relief occupent l’autre moitié. Deux de ces pièces  sont  colorées,  sur  la  toile  sobre,  c’est  vers  eux  que  le regard se focalise. Une ligne horizontale sépare les deux parties du tableau, quelques taches sombres descendent sur un coté. Seuil ? Des ombres menaçant l’humanité qui coure à sa perte ? Une humanité autodestructrice se dirigeant vers les ombres ? Liens, uniformité, mais aussi unicité et diffé-rences. Les lectures sont multiples, un thème, des motifs, des symboles,  la  vie  imaginaire  dans  les  couleurs  qui  coulent,  veines qui irriguent les toiles. Des individus se démarquent mais avancent avec les autres en route vers l’inconnu qu’est le monde qu’ils essaient d’apprivoiser. Pat’ace est préoccupé par  la  sauvegarde  de  l’environnement  et  par  le  devenir  de  la  société.  Formateur  en  art  plastique  en  milieu  carceral,  consultant culturel, Pat’ace met ses connaissances au service de la communauté. F.B.
