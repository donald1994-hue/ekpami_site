            <p>Lors  du  vernissage  d’une  exposition,  sous  l’emprise  d’une  grande  émotion  il  touche  furtivement  sur  une  toile  la  sil-houette délicate d’une jeune fille rêveuse. Il emportera chez lui sur les bouts de ses doigts des tâches colorées de la pein-ture encore fraîche... John Daniel Atiogbé peint le paysage où il est né, le paysage africain, la brousse, les bergers,  les cours d’eau, les villes, les femmes, le quotidien d’où il écarte tout ce qui pourrait obstruer la vision du ciel, du soleil, de l’air embaumé et surtout les signes du progrès. Qu’il invite notre regard à se poser sur le paysan en vélo sur une route pous-siéreuse, ou le laisser s’égarer sur un paysage champêtre, se reposer sur la bleuté d’un lac, ou sur un carrefour nettoyé par la pluie, il vole l’instant de l’oubli où la mémoire pourrait le confiner. Un figuratif idyllique. Certes, mais on s’y plaît, dans ses gouaches, aquarelles, peintures à l’huile, aspirés par la lu-mière tendre, protectrice qui enveloppe ses personnages on se surprend curieux : on voudrait savoir où ils vont, comment ils vivent, on voudrait leur poser des questions certains qu’ils vont répondre.  Aspirés dans un dialogue, emportés par son regard nous acceptons la vie telle qu’il voudrait qu’elle soit, la  vie  comme  utopie  heureuse.  Aujourd’hui  Koblanc  s’ouvre  également à une réprésentation figurative qui ne tient plus à  distance  ses  personnages,  son  regard  tendre  n’est  plus  lointain, il se les rapproche pour « écouter » leur expression, tente de les faire répondre à nos questions. F.B.
            </p>


