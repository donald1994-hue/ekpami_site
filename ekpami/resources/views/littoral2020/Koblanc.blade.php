    <div class="col-md-6 artiste-content-traduction">
            <p>During the opening of an exhibition, under the influence of a great emotion, he furtively touches on a canvas the delicate silhouette  of  a  dreamy  young  girl.  He  will  take  home  with  him on the tips of his fingers coloured stains of the paint, still fresh... John Daniel Atiogbé paints the landscape where he was born, the African landscape, the bush, the shepherds, the  rivers,  the  cities,  the  women,  the  daily  life  from  which  he  removes  everything  that  could  obstruct  the  vision  of  the sky, the sun, the embalmed air and especially the signs of  progress.  Whether  he  invites  our  gaze  to  settle  on  the  peasant  on  a  bicycle  riding  on  a  dusty  road,  or  let  him  get  lost on a rural landscape, resting on the blueness of a lake, or on crossroads cleaned by the rain, he steals the moment of oblivion where memory could confine him. A figurative idyllic. Certainly, but we like it, in his gouaches, watercolours, oil paintings, sucked up by the tender, protective light that envelops his characters, we find ourselves curious: we would like to know where they go, how they live, we would like to ask them questions that they are sure to answer. Aspired in a  dialogue,  carried  away  by  his  gaze,  we  accept  life  as  he  would like it to be, life as a happy utopia. Today Koblanc also opens up to a figurative representation that no longer keeps his  characters  at  a  distance,  his  tender  gaze  is  no  longer  faraway, he gets them closer to ‘‘listen’’ to their expression, attempts to make them answer our questions. F.B.
            </p>
