            <p>Samson  Gahowi  a  choisi  d’être  libre,  il  vit  en  bord  de  mer  dans un abri improvisé et corollaire de cette liberté, il récu-père ce que la mer rejette pour faire des sculptures pleines de  poésie  et  d’ingéniosité.  Masques  ou  installations,  Le  sage,  Hommage  aux  chaussures,  L’évolution  de  la  lumière,  et  les  quelques  scènes  bibliques  sont  composées  avec  des  « tapettes » (chaussures de plage), des ampoules, des vieux vêtements,  des  boîtes  de  conserves.  Sensible  au  regard qu’on  pose  sur  le  monde,  au  peu  de  tendresse  qu’on  dis-pense autour de soi, il rend hommage aux petites choses de la vie. La récupération, on le sait, rien de nouveau, pourtant elle prend ici une dimension nécessaire. A travers ses actes engagés, Gaoui « photographie » la pollution engendrée par la consommation et tente de secouer l’immobilisme des diri-geants et de la population. Il rend par là une certaine beauté à ces objets devenus inutiles puisque supposés inutilisables. F.B.
