    <div class="col-md-6 artiste-content-traduction">
            <p>Samson  Gahowi  has  chosen  to  be  free,  he  lives  by  the  sea  in an improvised shelter and as a corollary of this freedom, he recovers what the sea rejects to make sculptures filled with  poetry  and  ingenuity.  Masks  or  installations,  The  wise  man, Homage to shoes, The evolution of light, and the few biblical scenes are composed with ‘‘fags’’ (beach shoes), light bulbs, old clothes, cans. Sensitive to the way we look at the world, to the little tenderness we give out around us, he pays homage to the little things in life. Recuperation, as we know, is nothing new, yet here it takes on a necessary dimension. Through  his  committed  acts,  Gaoui  ‘‘photographs’’  the  pol-lution generated by consumption and tries to shake up the immobility  of  leaders  and  the  population.  By  doing  so,  he  restores  certain  beauty  to  these  objects  that  have  become  useless since they are supposedly unusable. F.B.
            </p>
