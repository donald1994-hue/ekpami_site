            <p>Statisticien de formation, passionné par le dessin et la mode, Marcel  Kpoho  réalisait  des  publicités  pour  des  promoteurs  de prêt-à-porter et des stylistes ainsi que des dessins pour de nouvelles créations de couturiers. Venu à l’art plastique plus tard, freiné par la crainte de pénétrer dans un domaine en autodidacte il a osé encouragé par un regard confiant. Une fois lancé sur ce chemin, il poursuivra par une formation en dessin et peinture au Centre culturel chinois de Cotonou. Après  la  peinture,  il  cherchera  à  s’ouvrir  à  d’autres  disci-plines et sa préoccupation pour l’environnement lui donnera la clé. Les pneus usés qui prolifèrent un peu partout dans la ville, il les utilisera pour confectionner des masques. Visages doubles, ambivalences, celles de l’humain qui construit d’une part  et  détruit  de  l’autre,  qui  aime  la  nature  et  la  dégrade,  qui  vit  mal  dans  un  environnement  pollué  et  continue  de  polluer. Pour Marcel Kpoho l’ambivalence et là, implacable, mais il y a aussi la sagesse de la reconnaître, visage d’une prise de conscience, premier pas. F.B.
            </p>


