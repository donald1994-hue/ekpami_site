            <p>Trained as a statistician and passionate about drawing and fashion,  Marcel  Kpoho  produced  advertisements  for  ready-to-wear  promoters  and  designers  as  well  as  drawings  for  new creations by couturiers. Coming to plastic art later, held back by the fear of entering a field as a self-taught artist, he dared to, encouraged by a confident look. Once launched on this path, he continued with a training in drawing and pain-ting at the Chinese Cultural Centre from Cotonou. After the painting, he will seek to open up to other disciplines and his concern for the environment will give him the key. He will use the used tires that proliferate all over the city to make masks. Double faces, ambivalence, those of the human being who builds on the one hand and destroys on the other, who loves nature and degrades it, who lives badly in a polluted environment  and  continues  to  pollute.  For  Marxk  ambiva-lence  is  implacable,  but  there  is  also  the  wisdom  to  reco-gnize it, the face of awareness, the first step. F.B.
            </p>
