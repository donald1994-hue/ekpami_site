            <p>Traditions, rhythms and masks. Youri Zomahoun brings out on canvas  a  cultural  heritage  of  the  performing  arts  which  is  more than a performance is a ritual. He brings it to life in two dimensions  on  canvases  that  reveal  the  Egungun  dancers,  the  ghosts,  reconstruct  their  steps  during  ceremonies  that  give coherence to the community, appease it and pay homage to  the  ancestors.  Protective  masks  from  the  dangers  of  the  night,  guardian  like  the  Zangbeto,  mysterious  deities  with  three  faces.  Guélédé,  Egungun,  Zangbéto,  vodun  pantheon  where  this  native  of  the  Oyo  region  in  Nigeria  has  bathed  since  childhood.  His  canvases  are  rich  in  materials,  fabrics,  pearls, paper, lace, paint, cassava flour, just like the costumes of the ghosts, the fruit of meticulous, patient work, creativity and skilful hands. Connected to its culture, Youri@. F.B
