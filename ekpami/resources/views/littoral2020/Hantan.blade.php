            <p>Tenturiers  émérites,  Hantan  et  Zinflou  originaires  de  la région  d’Avrankou,  dans  l’Ouémé,  créateurs  de  vêtements  de style novateur pour les notables de la région, sont enle-vés  sur  ordre  du  roi  Agadja  de  Danxomé  lors  d’une  razzia  militaire  et  amenés  à  la  cour  royale  d’Abomey.  Désormais  designers  des  tenues  royales,  ils  seront  considérés  comme  faisant partie de la famille du roi et logés dans le palais.  En 2015,  Franck  Hantan,  technicien  de  maintenance  informa-tique, pour ne pas rompre le fil de la tradition qui a toujours eu,  jusqu’à  la  disparition  de  sa  mère,  Suzanne  Hantan,  un  représentant  de  la  famille,  par  devoir  filial,  reprend  avec les  ciseaux  et  la  symbolique  du  royaume  l’héritage  ances-tral  familial.  Mission  patrimoniale  à  travers  la  voix  de  l’art  «  enfouie  en  lui  depuis  toujours  ».  La  toile  appliquée  est  une  technique  qui  consiste  en  à  coudre  diverses  formes  (objets,  personnages,  animaux,  signes  et  symboles)  décou-pées  dans  des  tissus  colorés,  sur  une  toile  au  fond  uni  pour  créer  une  narration,  crypter  un  message,  révéler  une  symbolique.   Les   toiles   appliquées   de   Hantan,   reprises   contemporaines,  fines  réconnexions  avec  l’histoire,  réin-ventent  l’imagerie  de  Danxomé.  Sur  ses  tableaux,  où  il  verse de l’eau avant de peindre, libation, ancienne tradition d’accueil d’un nouveau venu, d’un esprit ou hôte de marque, la présence de Gbòzùnmè, forêt sacrée où vivaient jadis ses aieux, où il insère des liens, les fils tissés par la transmission, marque un territoire où les traces rencontrent la magie du conte. Des pièces de Hantan et Zinflou, teintes vers 1800, peuvent toujours être vues au Musée du Quai Branly à Paris. Franck  Hantan  travaille  entre  Cotonou  au  Bénin  et  Neuilly  sur Marne en France. F.B
