            <p>Hantan and Zinflou, two skilled tenturers from the region of  Avrankou  in  the  Ouémé,  creators  of  innovative  clothing  styles for the region’s notables, are abducted on the orders of King Agadja of Danxomé during a military raid and taken to  the  royal  court  of  Abomey.  Now  designers  of  the  royal  outfits, they will be considered part of the king’s family and housed  in  the  palace.  In  2015,  Franck  Hantan,  a  computer  maintenance technician, in order not to break the thread of tradition  which  has  always  had,  until  the  death  of  his  mo-ther, Suzanne Hantan, a representative in the family, by filial duty, takes over with the scissors and the symbolism of the kingdom the family ancestral heritage. Patrimonial mission through the voice of art, which has ‘‘always been buried in him‘‘. Applied canvas is a technique that consists of sewing or gluing various shapes (objects, characters, animals, signs and symbols) cut out of coloured fabrics, onto a canvas with a plain background to create a narrative, encrypt a message, reveal  a  symbolism.  Hantan’s  appliqué  cloth,  contemporary  revivals, fine reconnections with history, reinvent Danxomé’s imagery. On his canvases, where he pours water before pain-ting, libation, an ancient tradition of welcoming a newcomer, a spirit or a distinguished guest, the presence of Gbòzùnmè, the sacred forest where his ancestors once lived, where he inserts  links,  the  threads  woven  by  transmission,  marks  a  territory where traces meet the magic of storytelling. Pieces by Hantan and Zinflou dyed around 1800, can be seen at the Musée du Quai Branly in Paris. Franck Hantan works between Cotonou in Benin and Neuilly sur Marne in France. F.B
