            <p>« Pour Sika Da Silveira, créer est une parfaite collaboration entre l’Esprit et la Matière. La matière n’est que le conduc-teur  à  travers  lequel  l’esprit  se  matérialise.  Une  dualité  évidente  se  dessine  alors  au  regard  de  l’observateur  entre  la  forme  qui  veut  s’imposer  et  l’essence  de  l’œuvre  qui  la  transcende pour exister dans une parfaite unité. Affronter la matière, la mettre à des épreuves, la défier afin de l’amener vers quelque chose de plus essentiel, qui est au delà de sa manifestation,  c’est  refuser  d’être  limitée  par  elle.  L’artiste  se  sert  de  médiums  différents,  peinture,  performance,  ins-tallation  et  ne  circonscrit  son  travail  ni  dans  un  contexte  culturel  ni  dans  un  cadre  géographique.  L’être  humain  doit  être  vu  et  considéré  au-delà  de  son  enveloppe  charnelle,  c’est  l’élévation  de  celui-ci  qui  demeure  au  cœur  d’un  tra-vail  qui  exprime  sa  vision  de  la  Vie  et  de  sa  sacralité.  Sika  da  Silveira  pense  que  de  contribuer  à  une  reconnexion  de  chaque personne avec son être, à l’harmonie avec la Vie, ren-dra ses œuvres intemporelles car aussi longtemps que la Vie va  s’émouvoir,  qu’il  y  aura  d’hommes  sur  terre,  ses  œuvres  auront une raison d’exister. » (adapté)
            </p>


