            <p>«For  Sika  Da  Silveira,  creating  is  a  perfect  collaboration  between  Spirit  and  Matter.  Matter  is  only  the  conductor  through  which  spirit  materializes.  An  obvious  duality  then  emerges in the observer’s eyes between the form that wants to impose itself and the essence of the work that transcends it to exist in perfect unity. To confront matter, to put it to trial, to challenge it in order to lead it towards something more essential, which is beyond its manifestation, is to refuse to be  limited  by  it.  The  artist  uses  different  mediums,  pain-ting,  performance,  installation,  and  does  not  circumscribe  his  work  in  a  cultural  or  geographical  context.  The  human  being must be seen and considered beyond his carnal enve-lope, it is the elevation of the human being that remains at the heart of a work that expresses his vision of Life and its sacredness.  Sika  da  Silveira  believes  that  contributing  to  a  reconnection of each person with his being, to harmony with Life,  will  make  her  works  timeless  because  as  long  as  Life  will be moving, as long as there will be people on earth, her works will have a reason to exist. (adapted)
