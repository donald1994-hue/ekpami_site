    <div class="col-md-6 artiste-content-traduction">
            <p>His  working  technique  came  from  obser-ving  the  spider  weaving  its  web.  The  use  of  plastic  thread,  his  environment,  his  daily  life.  He  diverted  an ordinary element intended to manufacture domestic use objects,  and  by  a  technique  borrowed  from  nature,  in  an  approach through the known of life’s mysteries and conver-ted it into an art object. Marius Tchiakpè sketches his sub-ject, cuts out the coloured plastic threads, affixes them and glues them to the support with meticulous, patient touches, and  brings  out  works  that  look  like  textile  art  in  living  compositions  where  imagined  beings  and  shapes  along  with  repetitive  line  patterns  make  up  what  could  be  read  as  another  pattern.  A  stylized  drawing,  rhytmic  sequences  that   reveal   emotions   in   contrasting   colours,   geometric   pattern  that  seems  to  drawn  from  a  universel  decorative  background.  Another  series,  revealed  by  unreal  forms,  the  presence of spirits, explores another one of his themes, the vodun.  Tchiakpè’s  subjects,  materials,  technique  and  plastic  approach are part of a spiral that is that of life, day by day. His work is special not only by using this material but above all  in  his  approach,  which  by  the  touch  reminds  us  of  an  Oscar Wilde sentence : ‘‘He was trying to gather up the scar-let threads of life, and to weave them into a pattern; to find his way through the sanguine labyrinth of passion through which he wandering’’. F.B.

            </p>
