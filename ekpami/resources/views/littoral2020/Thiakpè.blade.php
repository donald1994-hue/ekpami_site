    <div class="col-md-6 ">
            <p>Sa  technique  de  travail  est  venue  de  l’observation  de  l’araignée tissant sa toile. L’emploi du fil plastique, de son environnement, de son quotidien. Il a détourné un élément ordinaire  destiné  à  la  confection  d’objets  d’usage  domes-tique et par une technique empruntée à la nature, dans une démarche d’approche par le connu des mystères de la créa-tion et de la vie, l’a converti en objet d’art. Marius Tchiakpè esquisse son sujet, découpe les fils de plastique colorés, les appose  et  les  colle  sur  le  support  par  des  touches  minu-tieuses, patientes et fait ressortir des ouvrages aux allures d’art  textile  dans  des  compositions  vivantes  où  des  êtres  et des formes imaginées et des motifs des lignes répétitifs composent ce qui pourrait être lu comme un autre motif. Un dessin stylisé, séquences rythmiques qui révèlent des émo-tions en couleurs contrastées, des motifs géométriques qui semblent puiser dans un fond décoratif universel. Une autre série, révèle par des formes irréelles, la présence des esprits, explore le vodun. Les sujets, les matériaux, la technique et la démarche plastique de Tchiakpè s’inscrivent dans une spi-rale qui est celle de la vie au jour le jour. Travail singulier par l’emploi  de  cette  matière  et  surtout  par  une  démarche  qui  par effleurement fait penser à une phrase de Oscar Wilde : « Il essayait de rassembler les fils écarlates de sa vie et d’en tisser  un  motif,  de  trouver  son  chemin  dans  le  labyrinthe  excitant de la passion où il errait ». F.B.
            </p>


