            <p>Simon Kossou has a diploma in graphic design from ESMA. His  committed  work  is  a  denunciation  of  acts  that  are  disrespectful  to  the  environment,  uncivil  habits  and  their  consequences  that  pollute  the  atmosphere  and  endanger  health.  Pliers,  welding  set,  hammers,  circular  wax  for  wire  and  galvanized  iron  sculptures  to  ‘‘illustrate  the  behaviour  of  man  in  a  changing  world’’.  Ingenious,  realistic,  resulting  from her observations on the daily life of the most destitute, her sculptures, Nanga, Dossi, Moussa, street vendors and saleswomen,  young  unemployed,  women  in  their  domestic  life,  always  carry  an  accessory,  A  recuperated  object  that  attaches  itself  to  their  bodies,  a  colourful  touch  that  sof-tens the subject and brings a tender note to its intransigent approach. F.B.
            </p>
