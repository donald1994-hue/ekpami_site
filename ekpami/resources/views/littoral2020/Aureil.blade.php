            <p>A  degree  in  art  history,  a  residency  at  the  Fondation  Zinsou  with  the  cartoo-nist  and  comics  creator  Hector  Sonon,  led  him towards his practice. Picasso and Basquiat set him free in  the  quest  for  his  approach  and  if  he  admires  the  work  of  Aboudia  and  Evans  Mbugua  it  isn’t  a  lucky  coincidence,  his interest also focuses on man in his urban environment.  Aureil Bessan has given up the brush, he prefers the direct contact of his hand, his fingers with the material, oil paint, pens, coloured pencils, markers, twigs of brooms and a tube of paint that he transformed into a ‘‘pen’’. The first gesture is to coat the canvas with black before integrating his charac-ters, there is no complacency in his drawings, there is com-passion,  the  desire  to  know  man,  to  decipher  the  mystery  of life. The war in Syria is one of the subjects he will tackle in  a  series.  He  will  describe  a  brutal  world  or  complicit  in  atrocities,  the  dark  colours  are  barely  enhanced  by  white  lines,  obsessive,  curved  lines,  vertical,  horizontal,  diagonal,  ‘‘doodles’’, the cities are indifferent or in ruins, executioners and victims look alike and confused, it is the weapons that differentiate  them.  Women,  children,  families,  fragility,  vio-lence, writings, several languages, in capitals or tiny, visible, or  barely  detectable,  repetitive,  hypnotic,  which  take  place  through  curtains.  Patricorel  distorts  reality,  overexposes  it  and  surprises  humanity  in  obvious  offence  of  vulnerability.  F.B.
