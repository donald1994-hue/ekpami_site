    <div class="col-md-6 artiste-content-traduction">
            <p>Painter, Hippolyte Djizo folds the canvas, pleats it and gives it the relief that he will follow with a brush for his compo-sitions.  The  whole  cracked,  scratched,  aged,  reminds  us  of  plants, tree bark and roots. When he stops painting feminine silhouettes  without  faces,  he  lets  us  guess  sketches  of  the  men, characters scattered in the decor. Fossilized footprints of the passage. Having learned that plastic does not degrade over time, D’Hyzo has made it his own, he likes to make his work  last.  Complex  assemblages  of  old  motorcycle  engine  casings emerge, which he cuts, shears, notches, paints, sews and  dresses,  where  he  inserts  recycled  materials,  screws,  petrol cans, caps, in fact sculptures and masks. On these sup-ports  he  draws,  decorates  them  with  fabrics  that  he  hangs  with coloured threads and makes them dance. F.B.
            </p>
