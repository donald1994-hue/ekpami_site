            <p>Peintre, Hippolyte D’Hyzo plie la toile, la plisse et lui donne le relief qu’il suivra au pinceau pour ses compositions. L’en-semble  craquelé,  gratté,  vieilli,  fait  penser  aux  végétaux,  à  l’écorce  des  arbres,  aux  racines.  Lorsqu’il  cesse  de  peindre  des silhouettes féminines sans visage, il nous laisse deviner des  esquisses  des  bonhommes,  personnages  épars  dans  le  décor. Empreintes fossilisées du passage. D’avoir appris que le plastique ne se dégrade pas dans le temps, D’Hyzo se l’est approprié,  il  aime  inscrire  son  travail  ainsi  dans  la  durée.  Surgissent  des  assemblages  complexes  des  carcasses  des  moteurs  de  vieilles  motos  qu’il  découpe,  cisaille,  entaille,  peint, coud et habille, où il insère des matériaux de récupé-ration, vis, bidons à essence, capsules, en fait des sculptures et des masques. Sur ces supports il dessine, les pare des tis-sus qu’il accroche avec des fils colorés et les fait danser. F.B.
            </p>


