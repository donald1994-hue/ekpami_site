    <div class="col-md-6 artiste-content-traduction">
            <p>‘‘I  came  to  color  by  chance,  at  the  bend  of  a  shape  workshop  on  an  island  in  the  Indian  Ocean,  Reunion  Island.  Hide  the  defects  of  an  uncon-trolled  stratification,  by  bubbles  of  colors. A  compressor,  a  pistol  and  an  airbrush,  universe  controls  to  customize surfboards, colors like spices, to give them a perso-nal taste. 500 boards later, I gave up the compressor for the wand and the brush, aboriginal art took my guts, then African art,  then  Papua  New  Guinea  art,  first  nations  art.  Multicul-tural  by  essence,  by  birth,  each  people  touches  and  inspires  me, awakens my memories and my spirits. A time of patience and suspension. My interpretations take the black line of the drawings  and  the  dots  of  colors  inspired  by  aboriginal  and  melanesian art, a bit of graphic design illustration and colori-zation in air brush.It all starts in black and white, graphic, on a journey. In pencil or pen, I write or draw shapes, what I see outside or inside me. I don’t choose a subject, there is no claim or even any project that would inspire my line. Painting is my breathing. Encounters, places and the time. The time to wait for a train in Sri Lanka, the time to wait for a bus in Mali, the time of a breakdown, someone I am waiting for, or the time to be there. Always a notebook and a pencil. Listening to a noise, a rhythm, a shape. The first line. Then I interpret’’. Franco-Beninese, Afi Kodjobi Thoukpa works in France, Benin and elsewhere.
            </p>
