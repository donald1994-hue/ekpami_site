            <p>«  J’ai  débuté  la  couleur  par  hasard,  au  détour  d’un  atelier  de  shape dans une île de l’Océan Indien, la Réunion. Cacher les défauts  d’une  stratification  non  maîtrisée,  par  des  bulles  de couleurs.  Un  compresseur,  un  pistolet  et  un  aérographe,  des  commandes  d’univers  pour  personnaliser  des  surfs,  des  cou-leurs  comme  des  épices,  leur  donner  un  goût  personnel.  500  planches plus tard, j’ai lâché le compresseur pour la baguette et le pinceau, l’art aborigène m’a prise aux tripes, puis l’art afri-cain, puis l’art papouasien, les arts des first nation. Multicul-turelle  par  essence,  par  naissance,  chaque  peuple  me  touche  et m’inspire, réveille mes mémoires et mes esprits. Un temps de patience et suspendu. Mes interprétations prennent le trait noir  des  dessins  et  les  points  de  couleurs  inspirées  par  l’art  aborigène et mélanésien, un chouia de graphisme illustration et  de  la  colorisation  en  air  brush.  Tout  commence  en  noir  et  blanc, graphique, en voyage. Au crayon ou à la plume, j’écris ou je dessine des formes, ce que je vois dehors ou en moi. Je ne choisis pas de sujet, il n’y a pas de revendication ni même au-cun projet qui inspirerait mon trait. Peindre est ma respiration. Des rencontres, des endroits et le temps. Le temps d’attendre un  train  au  Sri  Lanka,  le  temps  d’attendre  un  bus  au  Mali,  le  temps d’une panne, quelqu’un que j’attends, ou le temps d’être là.  Toujours  un  carnet  et  un  crayon.  A  l’écoute  d’un  bruit,  d’un  rythme, d’une forme. Le premier trait. Puis, j’interprète. » Franco-béninoise, Afi Kodjobi Thoukpa travaille en France, au Bénin et ailleurs.
            </p>


