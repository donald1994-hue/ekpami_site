            <p>Traditional  musical  instruments,  Dougba,  Ogbon,  the  big  drum,  the  djembe,  the  callebasse  «  ika»,  traditional  trum-pets, castanets, Eric Ahouansou’s passion. Fascinated by their drawing, by their contours, by the rhythm, this former drafts-man of an architecture firm and graphic designer, reproduces them  with  care.  His  compositions  always  tend  towards  balance,  the  various  instruments  are  associated  according  to  their  virtues  and  their  titles  are  the  ripe  fruits  of  this  association: La  voie  intime,  The  Intimate  Way,  Le  Chemin,  The  Way,  Passivité  et  fermeté,  Passivity  and  Firmness...  His  drawings  seems  notebooks  from  an  ethnographic  journey  through villages in the footsteps of the rhythms that made the  heart  of  the  community  beat.  The  paintings  in  Indian  ink, pencil, charcoal, crystal ink show a sensitive, fine, hypno-tic  work.  Scores  rhythmed  by  constant  movements,  colours  arranged  by  small  dots  with  scientific  rigour  for  a  result, musical,  translucent  that  exalts  civilizations,  cultures,  that  describe  an  emotion,  a  feeling  or  states  of  mind.  «A  tech-nique as old as the old Alchemy, or esotericism: the hidden teaching of pure and Universal Spirituality. «Musician, Erick   Ahouansou  works  for  the  promotion  of  an  educational  art  and for the promotion of African culture. He is at the origin of the Festival Ilé ya Africa, in Yoruba, Africa, it’s time to go home. F.B
