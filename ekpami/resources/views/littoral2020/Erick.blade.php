            <p>Les  instruments  de  musique  traditionnels,  Dougba,  Ogbon,  le grand tambour, le djembé, la callebasse « ika», les trom-pettes   traditionnnelles,   les   castagnettes,   passion   d’Eric   Ahouansou. Fasciné par leur dessin, par leur contours, par le rythme, cet ancien dessinateur d’un cabinet d’architecture et graphiste, les reproduit avec soin. Ses compositions tendent toujours vers l’équilibre, les divers instruments sont associés selon leurs vertus et leurs titres sont les fruits mûrs de cette association : La voie intime, Le chemin, Passivité et fermeté... Ses  dessins  semblent  carnets  de  notes  d’un  voyage  ethno-graphique dans des villages sur les traces des rythmes qui ont  fait  battre  le  cœur  de  la  communauté.  Les  peintures  à  l’encre de chine, crayon, charbon, encre de cristal montrent un travail sensible, fin, hypnotique. Partitions rythmées par des  mouvements  constants,  couleurs  agencées  par  petits  points avec une rigueur scientifique pour un résultat musi-cal, translucide qui exalte des civilisations, des cultures, qui décrivent une émotion, une sensation ou des états d’esprit. « Une technique aussi vielle que la vielle alchimie, ou l’éso-térisme  :  l’enseignement  caché  de  la  pure  et  universelle  spiritualité ». Musicien, Erick Ahouansou œuvre pour ’un art éducatif,  pédagogique  et  pour  la  promotion  de  la  culture  Africaine. Il est à l’origine du Festival Ilé ya Africa, en yoruba, Afrique, il est temps de rentrer à la maison. F.B.
