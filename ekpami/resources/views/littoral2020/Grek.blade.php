            <p>Peintre  du  «  réel  et  de  l’irréel,  du  tangible  et  de  l’intangible  », Gratien Anagossi Eclou peint et sculpte le monde physique et le monde spirituel. Ses œuvres s’inspirent de la complexité de la création et « invitent à faire un retour sur soi-même afin d’explorer  les  profondeurs  de  son  être.  »  Cette  approche  ne  s’inspire pas seulement de sa propre connaissance du monde, mais  surtout  et  avant  tout  de  la  tradition,  de  la  culture  afri-caine dans laquelle il a été moulé. Il a choisi de travailler sur un des éléments fondamentaux de la création : la terre. « La terre est le début de tout, elle nourrit tout et toute vie vient d’elle ». Grek érige des sculptures au nom des rois, il rend hom-mage  aux  femmes  et  à  leur  fruit,  modèle  une  main  du  bon-heur, des botchios (sculptures Fon)... Alliant divers matériaux de  récupération,  auxquels  il  rajoute  la  latérite,  ses  œuvres  situées à la lisière du monde moderne et ancestral dégagent une  belle  énergie  et  sensibilité.  Ses  tableaux,  sur  papier  ou  sur toile, ses sculptures, sont empreints de l’imaginaire vodun indissociable de sa terre-mère. F.B
