            <p>A  painter  of  the  ‘‘real  and  the  unreal,  the  tangible  and  the  intangible’’,  Gratien  Anagossi  Eclou  paints  and  sculpts  the  physical and the spiritual world. His works are inspired by the complexity  of  creation  and  ‘‘invite  us  to  take  a  look  back  at  ourselves in order to explore the depths of our being’’. This ap-proach is inspired not only by his own knowledge of the world, but first and foremost by the tradition, the African culture in which he was moulded. He chose to work on one of the fun-damental  elements  of  creation:  the  earth.  ‘‘The  earth  is  the  beginning  of  everything,  it  nourishes  everything  and  all  life  comes from it’’. Grek erects sculptures in the name of kings, he pays homage to women and their fruit, models a hand of hap-piness, botchios (Fon sculptures)... Combining various recycled materials, to which he adds laterite, his works located on the edge  of  the  modern  and  ancestral  world  exude  a  beautiful  energy  and  sensitivity.  His  paintings,  on  paper  or  canvas,  his  sculptures, are imbued with the vodun imaginary inseparable from it’s motherland. F.B
