            <p>‘‘What  drives  my  production  is  the  contrast   between   human   beauty   and   the   monstrous   weight   they   embody   on   Earth... At the age of 15, Stéphane Vlavonou starts drawing and  already  unconsciously  distorts  in  a  caricatured  way  the  subjects  he  treats.  Having  passed  the  entrance  exam  to  the  Ecole  des  Gobelins  in  Paris,  he  studied  for  3  years  the  design  and  production  of  animated  films. Victim  of  a road accident that reduced his visual field by half, he now naturally  distorts  all  the  subjects  he  translates  plastically.  As he no longer manages to work in the world of cartoons where the morphology of the characters must be respected to the letter, his distorted field of vision turns him towards painting which allows him to express his creativity in com-plete freedom. PSYCOFFI is born. From accident to painting as a consequence of the visual after-effects that plastically opened his eyes from drawing to the opposite, the contrary, the  enemy  or  soulmate  of  pictorial  technical  realism:  the  graphic rendering that approaches Abstraction. His expres-sion?  Human  corpulence  as  an  adventure.  Body,  anatomy, funny caricatural expression. The formal and coloristic tones assert  themselves  between  monstrosity  and  cartoon  spirit.  The  deformations  and  mergers  of  objects  embody  a  tote-mic spirit. The backgrounds, like souls or weights that hold, shape and sculpt the subjects’’. Psycoffi works between Cotonou in Benin and Romainville in the 93 in France. (adapted)

