            <p>The  rather  rich,  even  tortuous  path  that  leads  Innocent  Bienvenu  Monkoun,  Bimo,  from  drawing  and  serigraphy  to  painting  remained  his  favourite  art,  undoubtedly  explains  his negation of all conventional forms, with a strong hint of drawing. Close to naive art, his work borrows from the great and recognized sources of modern art: Picasso and Wifredo Lam are not without having influenced him. However, Bimo is  not  a  vulgar  copyist,  but  a  young  artist,  convinced  that  naive forms are undoubtedly the most appropriate ones to truly reflect the mental universe he projects on his canvases. Like many young contemporary artists, Bimo is also a resear-cher in esotericism and occultism. He plunges his real roots in the past bequeathed by his ancestors and perpetuated by the various local cults of which he does not want to be an ambassador : there are risks in these places, and beware for those who get involved without the necessary precautions. Bimo chooses a rich, rather luminous palette that cheerfully paradoxes, somewhat obscures his message and forces the viewer to take a better look. J.A.
