            <p>Le  parcours  plutôt  riche,  voir  tortueux  qui  mène  Innocent  Bienvenu Monkoun, Bimo, du dessin et de la sérigraphie à la peinture qui demeure son art favori, explique sans doute sa négation pour toute forme conventionnelle, à fort relent de dessin. Proche de l’art naïf, son œuvre emprunte aux grandes sources reconnues de l’art moderne : Picasso et Wifredo Lam ne sont pas sans l’avoir influencé. Pourtant, Bimo n’est pas un vulgaire copiste, mais un jeune artiste, convaincu que les formes naïves sont sans doute celles qui sont le plus propre à  rendre  compte  vraiment  de  l’univers  mental  qu’il  pro-jette  sur  ses  toiles.  Comme  bon  nombre  de  jeunes  artistes  contemporains  Bimo  est  aussi  un  chercheur  en  ésotérisme  et en occultisme. Il plonge ses racines réelles dans le passé légué par les ancêtres et perpétué par les divers cultes lo-caux dont il ne veut pourtant pas se faire l’ambassadeur : il y a des risques en ces lieux, et gare à qui s’en mêle sans les précautions nécessaires. Bimo choisi une palette riche, plu-tôt  lumineuse  qui  joyeux  paradoxe,  obscurcit  quelque  peu  son message et oblige le spectateur à mieux regarder. J.A.
