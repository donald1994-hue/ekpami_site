            <p>Une  lignée  royale,  un  passé,  une  formation  de  styliste.  De  ces milieux, il a gagné la présence, le goût de la parure, le sens de la parade, des cérémonies. Prince Toffa orchestre ses apparitions avec un sens inné du spectacle qui dévoile avec élégance sa sensibilité aux rituels, son attachement aux va-leurs culturelles, à la transmission de la mémoire collective. A  l’instar  d’un  griot,  il  performe  des  panégyriques,  incon-tournables éléments vivants de sa culture, sauvegardes fer-ventes de la mémoire des événements, des faits et actions mémorables,  éloges  des  personnes  vivantes  ou  disparues.  Ses recherches se portent sur l’identité, c’est elle qui est le fil conducteur de son travail. Dès la peinture où elle est révélée dans les scarifications qui la fixaient antan sur les visages jusqu’aux déambulations où elle transcende l’individu pour mettre en scène les valeurs d’une communauté, elle est pour lui source de questionnements et de quelques réponses. Les canettes,  leurs  languettes,  les  capsules  de  bouteilles,  ves-tiges polluants du monde moderne, qu’il récupère, applatit et transforme, transposées de son habileté de couturier dans des créations vestimentaires somptueuses, traînent de loin et  portent  avec  panache  leur  lourd  héritage.  Portées,  dan-sées  comme  les  masques,  elles  sont  habits  de  rois,  sculp-tures, déguisements, masques tombées, elles identifient et pointent  la  consommation  moderne  clinquante,  qui  brille  de tous ses feux sous nos yeux éblouis. La scarifient. Prince Toffa a mille métiers, un seul visage. Sans scarifications. Son identité il la trouve dans les mille visages de son art. F.B
            </p>


