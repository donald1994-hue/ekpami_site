    <div class="col-md-6 artiste-content-traduction">
            <p>A  royal  lineage,  a  past,  a  fashion  desi-gner training. From these backgrounds, he  has  gained  a  presence,  a  taste  for  finery,  a  sense  of  parade  and  ceremony. Prince   Toffa   orchestrates   his   appearances   with an innate sense of showmanship that elegantly reveals his  sensitivity  to  rituals,  his  attachment  to  cultural  values,  to the transmission of collective memory. Griot-like, he per-forms  panegyrics,  the  inescapable  living  elements  of  his  culture,  fervent  memory  of  events  guardians,  memorable  facts  and  actions,  praising  living  or  missing  persons.  His  research focuses on identity, which is the guiding thread of his  work.  From  the  painting  where  she  is  revealed  in  the  scarifications that used to fix her on faces to the wanderings where  she  transcends  the  individual  to  stage  the  values  of  a  community,  she  is  for  him  a  source  of  questions  and  some answers. The cans, their tabs and bottle caps, polluting vestiges of the modern world, which he recovers, flattens and transforms, transposed from his skill as a couturier into sumptuous  clothing  creations,  ‘‘trail’’  from  afar  and  carry  their huge heritage with panache, worn, danced like masks, they are kings’ garments, sculptures, disguises, fallen masks, they identify and point out the flashy modern consumption, which  shines  with  all  its  glitter  before  our  dazzled  eyes.  Scarify it. Prince Toffa has a thousand trades, only one face. Without scarification. His identity he finds it in the thousand faces of his art. F.B.

            </p>
