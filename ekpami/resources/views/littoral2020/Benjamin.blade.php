    <div class="col-md-6 artiste-content-traduction">
            <p>They  have  mouthpieces,  claws,  talons,  tails,  extravagant  creatures straight out of the flute of an urban Pan, chime-ras  from  a  personal  mythology  that  he  takes  off  from  the  street,  the  city,  from  life.  Spreading  without  disenchant-ment between before and now, he draws as one spreads his wings, from above, diving, sweeping the landscape from the heights to get an overall view. Sheet metal worker, assem-bler of heterogeneous compositions, fruit of a fierce struggle with  the  material,  Benjamin  Deguenon  recovers.  Recovers  from everyday life to sew his investigations with wire, from his childhood to invent an urban mythology. He recovers not only  the  materials,  the  techniques,  the  idea  of  communion  between man and nature, he recovers from the intimate his anxieties  to  deliver  those  of  others,  in  fact  a  pantheon  of  unrealities, made of wood, decorated with iron, adorned with fabrics, installations-exorcisms that expose him. His beauti-ful energy, a projection of the fury for existing in a mutant society where the sacred etiolates and individuals get lost, is channeled in the drawings which prolong his approach on a more fragile support, with more sensitive tools. Tenderness, more humour in his urban utopias, his struggle with matter has branched out on multiple paths. Serene on paper, facing iron and wood, she remains a primary force, a wise determi-nation  where  subtle  insinuation  gives  way  to  the  obvious,  nestling  in  the  iron,  spearhead,  point  with  the  pencil  that  erases the answers and hems the questions. F.B
            </p>
