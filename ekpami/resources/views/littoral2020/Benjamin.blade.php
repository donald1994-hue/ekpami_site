            <p>Elles ont des becs, des griffes, des serres, des queues, créa-tures extravagantes sorties tout droit de la flûte d’un Pan urbain,  chimères  issues  d’une  mythologie  personnelle  qu’il  enlève de la rue, de la ville, de la vie. Ecartelé sans désen-chantement entre avant et maintenant, il dessine comme on déploie ses ailes, d’en haut, en plongée, à balayer le paysage des  hauteurs  pour  avoir  une  vue  d’ensemble.  Tailleur  de  tôles, assembleur des compositions hétéroclites, fruits d’une lutte  acharnée  avec  la  matière,  Benjamin  Deguenon  récu-père. Récupère du quotidien pour coudre ses enquêtes au fil de fer, de son enfance pour inventer une mythologie urbaine. Récupère non seulement les matières, les techniques, l’idée de la communion entre l’homme et la nature, il récupère de l’intime ses angoisses pour délivrer celles des autres, en fait un  panthéon  d’irréalités,  en  bois,  ornées  de  fer,  parées  des  tissus, installations-exorcismes qui le mettent à nu. Sa belle énergie,  projection  d’une  fureur  d’exister  dans  une  société  mutante  où  le  sacré  s’étiole  où  les  individus  se  perdent,  se  canalise  dans  les  dessins  qui  prolongent  sa  démarche  sur  un  support  plus  fragile,  avec  des  outils  plus  sensibles.  Tendresse,  plus  d’humour  dans  ses  utopies  urbaines,  sa  lutte avec la matière s’est ramifiée sur des multiples voies. Sereine sur le papier, face au fer, au bois, elle reste force pri-maire,  une  détermination  sage  où  l’insinuation  subtile  fait  place  à  l’évidence  se  niche  dans  le  fer  de  sa  lance,  pointe  du crayon qui efface les réponses et ourle les questions. F.B
            </p>


