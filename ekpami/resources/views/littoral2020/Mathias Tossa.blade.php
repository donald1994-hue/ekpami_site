    <div class="col-md-6 artiste-content-traduction">
            <p>Social  inequalities  hit  him  just  as  hard  as  the  blindness  that  drives  to give interest only to «important» men and to despise the «little ones», to  pay  attention  only  to  conventionally  called valuable objects to the detriment of the useful ones. For Mathias Sossa everything is simpler, men have value and deserve consideration, objects have their usefulness. Value, consideration, usefulness, thoughts that will be conveyed by the  tires  that  he  will  cut  and  carve  in  this  material  with  a  hard and soft texture at the same time, the rubber. He will put  primary  colours  on  the  natural  black  of  the  material  and cover it with an almost pop-art imagery to bring out a childlike universe, children’s silhouettes, a car, a toy perhaps, children  playing  outside,  a  dog...  A  wonderful  world  where  everything is on the same scale, where there is no hierarchy, no  constraints,  the  sky  is  red,  the  asphalt    blue,  there  is  a  play, a ball, life as a ball that we share in a children’s game. The format is variable, tables or volume, the reliefs of rubber, recovered  material,  considered  poor,  highlighted  with  skill  and care. An installation shows another world, monochrome, the  uniform  black  mass  of  immigrants  moving  slowly  but  with dignity towards an unknown place where their dreams can come true. Mathias Sossa has acquired the techniques of working with materials and perfected his know-how in the workshops of Zinkpé, but the critical and tender look he has on the world is his own. F.B

            </p>
