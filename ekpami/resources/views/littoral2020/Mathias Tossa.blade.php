    <div class="col-md-6 ">
            <p>Les  inégalités  dans  la  société  le  heurtent  de  plein  fouet  comme  le  heurte  l’aveuglement  qui  pousse  à  accorder  de  l’intérêt  aux  seuls  hommes  «  importants  »  et  mépriser  les  « petits », de prêter attention aux seuls objets convention-nellement  appelés  de  valeur  au  détriment  des  utiles.  Pour  Mathias  Sossa  tout  est  plus  simple,  les  hommes  ont  de  la  valeur et méritent considération, les objets ont leur utilité. Valeur,  considération,  utilité,  pensées  qui  vont  être  véhicu-lées  par  les  pneus  qu’il  va  découper  et  tailler  dans  cette  matière  à  la  texture  dure  et  molle  et  souple  à  la  fois,  le  caoutchouc.  Il  posera  sur  le  noir  naturel  de  la  matière  des  couleurs primaires et couvrira d’une imagerie presque pop-art  pour  faire  surgir  un  univers  enfantin,  des  silhouettes  d’enfants, une voiture, jouet peut être, des enfants qui jouent dehors,  un  chien...  Un  monde  merveilleux  où  tout  est  à  la  même échelle, où il n’y a pas d’hiérarchie, pas de contraintes, le ciel est rouge, l’asphalte est bleu, il y a le jeu, un ballon, la vie comme un ballon qu’on partage dans un jeu d’enfants. Le  format  est  variable,  tableaux  ou  volume,  les  reliefs  du  caoutchouc,  matière  récupérée,  considérée  pauvre,  mis  en  valeur  avec  habileté  et  soin.  Une  installation  montre  un  autre  monde,  monochrome,  la  masse  noire,  uniforme  des  immigrants se dirigeant lentement mais dignement vers un lieu inconnu où réaliser leurs rêves. Mathias Sossa a acquis les techniques de travail des matières et parfait son savoir-faire dans les ateliers de Zinkpé, mais le regard critique et tendre qu’il pose sur le monde est bien le sien. F.B.
            </p>


    </div>
