    <div class="col-md-6 artiste-content-traduction">
            <p>From  his  old  paintings,  an  impression  of  purity  emanated  from the economy of graphic elements, the translucent abs-traction of ideas, intellectual intuitions in ethereal colours. While retaining the central theme of his universe, Man in the world, Achille Zohoun constantly evolves his approach to it. The lines are sharper, the colours are more vibrant, and one notices above all the introduction of the figurative element, which often plays on the trompe l’oeil. ‘‘My mission is to be a  citizen  artist,  one  who  informs  and  trains  on  the  quality  of the relationships and interactions between Man and Na-ture, while taking hold of the pictorial material as a field of research, a way of expression and communication. I have an immaterial vision of painting. Painting is a state of mind, a state of thinking and doing that in melody, leaves the mind in  the  fantasy  of  an  aesthetic  enjoyment.  And  this  Melody  that I play, this pictorial dance, is the spirit. Deep down I am this  infinite  spirit’’. Whether  he  explores  our  hyper-secure universe - from fingerprints to QR codes to barcodes - our fears,  or  questions  our  identities,  between  psychoanalysis  and  metaphysical  essays  his  paintings  speak  of  humility,  love, the drive towards the divine as paths to the best of all possible worlds, towards the light. F.B.
            </p>
