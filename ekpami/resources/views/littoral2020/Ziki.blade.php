            <p>De ses anciennes toiles, une impression de pureté émanait de l’économie d’éléments graphiques, de l’abstraction trans-lucide des idées, des intuitions intellectuelles aux couleurs éthérées. Tout en conservant la thématique centrale de son univers, l’Homme dans le monde, Achille Zohoun fait évoluer constamment  sa  manière  de  l’aborder.  Les  traits  sont  plus  nets, les couleurs plus franches gagnent en vibration, et on remarque  surtout  l’introduction  de  l’élément  figuratif  qui joue souvent sur le trompe l’œil. « M’interrogeant sans cesse sur les capacités, voire les limites de l’artiste à transformer les  ‘’élémentaux’’  (air,  eau,  feu,  terre)  de  l’environnement,  ma  mission  est  d’être  un  artiste  citoyen,  celui  qui  informe  et forme sur la qualité des relations-interactions Homme & Nature tout en se saisissant de la matière picturale comme champ de recherche, mode d’expression, de communication. J’ai  de  la  peinture  une  vision  immatérielle.  La  peinture  est  un état d’âme, de pensée et de faire qui en mélodie, laisse l’esprit  dans  le  fantasme  d’une  jouissance  esthétique.  Et  cette Mélodie que je joue, cette danse picturale, c’est l’esprit. Au fond je suis cet esprit infini ». Qu’il explore notre univers hyper sécurisé - des empreintes digitales au QR code en pas-sant  par  les  codes  barres  –  nos  peurs,  ou  qu’il  questionne  nos  identités,  entre  psychanalyse  et  essais  métaphysiques  ses  tableaux  parlent  d’humilité,  d’amour,  de  l’élan  vers  le  divin  comme  chemins  d’accès  vers  le  meilleur  des  mondes  possibles, vers la lumière. F.B
            </p>


