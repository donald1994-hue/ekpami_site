            <p>« Je me nourris du doute. C’est du doute que je tire la subs-tance  de  mes  créations  ».  Ludovic  Fadaïro  se  définit  ainsi. Pour lui, peindre ne suffit pas parce qu’avant le geste il y a les questions et les réponses. Il est plus à l’aise dans la peau d’un chercheur  avec  ce  que  cela  comporte  comme  fragilités  et  découvertes. «Je questionne la matière. De mon questionne-ment, je découvre des éléments extérieurs qui me permettent d’affirmer une démarche technique aboutie.» Ludovic Fadaïro compte parmi les tout premiers peintres du continent à avoir tourné le dos aux techniques conventionnelles apprises dans les  écoles  d’art.  Ayant  très  tôt  fait  le  constat  de  son  appar-tenance  à  une  terre,  l’Afrique,  cette  gardienne  des  richesses  culturelles  du  monde,  il  a  opéré  un  admirable  retour  aux  sources. Sa peinture est comme une respiration, un souffle puissamment  renouvelé  par  une  source  intérieure.  Chaque  motif, chaque visage, chaque coulée de peinture naît d’une interrogation de la matière qui fait la texture de la toile. Rien n’est figé, dessiné à l’avance, rien n’est joué à l’avance. L’œuvre se  construit,  se  créée  au  détour  des  signes  ou  idées-forces  auxquels l’artiste donne vie. Chez Ludovic Fadaïro, matière et symbole se conjuguent pour dire ce qui n’est pas manifesté et  qui  néanmoins  existe  dans  la  permanence  au-delà  des  formes, au-delà du regard. Chaque œuvre est un acte impor-tant et primordial qui ne cède à aucune facilité. Elle explore les grandes questions que l’homme se pose sur la vie et sur lui-même. Elle est, pour celui qui la contemple, une invitation à un voyage qui le mènera à terme à la rencontre avec son être universel... A.M.Z.
            </p>


