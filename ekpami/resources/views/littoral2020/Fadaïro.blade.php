            <p>‘‘I  feed  on  doubt.  It  is  from  doubt  that  I  derive  the  substance  of  my  creations’’.  Ludovic  Fadaïro  defines  himself  like  this.  For  him,  pain-ting  is  not  enough  because  before  the  gesture  there  are  questions and answers. He is more at ease in the skin of a researcher  with  all  the  fragilities  and  discoveries  that  this  entails. ‘‘I question the material. From my questioning, I dis-cover external elements that allow me to affirm a successful technical approach’’. Ludovic Fadaïro is one of the very first painters  on  the  continent  to  have  turned  his  back  on  the  conventional techniques learned in art schools. Having very early  realized  that  he  belonged  to  a  land,  Africa,  the  guar-dian  of  the  world’s  cultural  wealth,  he  made  an  admirable  return  to  his  roots.  His  painting  is  like  a  breath,  a  breath  powerfully  renewed  by  an  inner  source.  Each  motif,  each  face, each flow of paint is born from a questioning of the material  that  makes  the  texture  of  the  canvas.  Nothing  is  fixed, drawn in advance, nothing is played in advance. The work is constructed, created through the signs or key ideas to which the artist gives life. In Ludovic Fadaïro’s work, mat-ter and symbol combine to say what is not manifested and which  nevertheless  exists  in  permanence  beyond  forms,  beyond the gaze. Each work is an important and primordial act that does not yield to any facility. It explores the great questions that man asks himself about life and about him-self. It is, for the one who contemplates it, an invitation to a journey that will eventually lead him to the encounter with his universal being... A.M.Z.
            </p>
