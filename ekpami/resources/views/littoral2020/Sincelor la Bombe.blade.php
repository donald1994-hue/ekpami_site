            <p>In his work the effervescence is almost palpable, one feels his overflowing energy, the joy, the hand is sure, the features precise,  the  mind  is  in  turmoil.  Apoté  Zekpa  draws,  does  performances,  styling,  scenography  for  concerts,  creates  the  scenery  for  shows,  creating  is  a  vital  need  for  him.  His  readings,  his  travels  and  dreams,  the  women,  the  streets  feed his eyes. The colours flash on the portraits of a realism that  borders  on  caricature,  cartoon  drawing  and  he  passes  indecisive (or nonchalant) from the figurative to an abstract that does not give up figurative elements, passes from still lives  to  idyllic  scenes.  His  urban  culture,  the  thirst  for  new  challenges, inevitably led him towards graffiti. For Sincelor La Bombe (ex Sincelor la Brosse), young man engaged in a peaceful struggle, armed with spray cans, brushes and hea-dphones on his shoulders, after Fijrossé, this neighbourhood by the sea in Cotonou (Fidjros’Angeles), the urban space of the whole world remains to be conquered. F.B.

