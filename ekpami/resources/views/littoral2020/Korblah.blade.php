            <p>«  Après  plusieurs  formations  en  calligraphie,  sérigraphie,  lithographie  et  en  art  publicitaire,  Richard  Korblah  com-mence  à  peindre,  puis  en  vient  très  vite  à  la  sculpture.  Le  dialogue  avec  la  matière  lui  dicte  le  chemin  à  prendre  pour  donner  du  sens  à  celle-ci.  Donner  du  relief,  travailler  le  volume,  et  le  contact  avec  la  matière  lui  sont  devenus  essentiels.  De  la  matière  première  «  brute  »  naissent  des  sculptures ocres, aux formes dures mais pures, symboliques et puissantes. Richard Korblah tire son inspiration des cou-tumes, rites et traditions découverts au cours de ses voyages. Son ouverture à l’Autre, sa sensibilité aux cultures et événe-ments qui l’entourent, influencent son travail. Ses œuvres présentent également une dimension politique, ou du moins sociale. Via une démarche narrative, l’artiste aborde des pro-blématiques contemporaines, telles que les migrations et la condition ou la représentation de la femme dans nos socié-tés, leur rendant ainsi hommage». Ethnographe, il recueille les  paroles  du  monde,  matérialisées  dans  les  gestes,  les  rites, les valeurs qui le constituent et lui confèrent authenti-cité. Artiste, il les sublime dans les dessins, peintures, sculp-tures,  ses  miroirs  du  monde.  Esthète,  il  exalte  leur  beauté,  beauté qu’il aime révéler à travers la souffrance, fardeau de l’être dans le monde qu’il tourmente et exhibe pour l’exor-ciser. Fabiola BadoiKorblah  RAK  voyage,  il  vit  en  France  et  travaille  aussi  à  Cotonou au Bénin
