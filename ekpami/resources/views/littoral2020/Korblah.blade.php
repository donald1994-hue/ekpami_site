    <div class="col-md-6 artiste-content-traduction">
            <p>‘‘After several training courses in cal-ligraphy, silkscreen printing, lithogra-phy and advertising art, Richard Korblah began  painting,  then  quickly  moved  on  to  sculpture. The dialogue with the material dictates the path he  must  take  to  give  meaning  to  it.  Giving  relief,  working  with  volume,  and  contact  with  the  material  have  become  essential to him. From the primary, ‘‘raw’’ material are born ochre  sculptures,  with  hard  but  pure  forms,  symbolic  and  powerful.  Richard  Korblah  draws  his  inspiration  from  the  customs,  rites  and  traditions  discovered  during  his  travels.  His openness to the Other, his sensitivity to the cultures and events that surround him, influence his work. His works also have a political, or at least social, dimension. Through a nar-rative approach, the artist tackles contemporary issues such as migration and the condition or representation of women in our societies, thus paying tribute to them’’. As an ethno-grapher, he collects the words of the world, materialized in the  gestures,  rites  and  values  that  constitute  it  and  give  it  authenticity.  As  an  artist,  he  sublimates  them  in  drawings,  paintings, sculptures, his mirrors of the world. Aesthete, he exalts  their  beauty,  beauty  that  he  likes  to  reveal  through  suffering, burden of the being in the world that he torments and exhibits to exorcise it.. Fabiola BadoiKorblah  RAK  travels,  he  lives  in  France  and  also  works  in  Cotonou, Benin.
            </p>
