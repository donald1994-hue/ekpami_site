            <p>Pour  conjurer  le  vide  de  la  toile,  Philippe  Hacheme  «  part  de  l’absence  ».  Reconstitution  sortilège  du  geste  créateur  premier,  jailli  du  néant,  il  pose  du  noir  sur  sa  toile  avant  de progressivement l’éclairer. Il se revendique d’un « expres-sionnisme figuratif », expressionnisme, car c’est à l’aune de ses émotions qu’il interroge sa subjectivité, l’émotion encore qu’il souhaite susciter, figuratif car il éprouve le besoin de l’ancrer  en  dessin.  Il  se  refuse  de  poser  la  tristesse,  la  dé-tresse,  le  malheur,  états  d’âme  qui  suspendent  son  élan  «  on  nous  montre  trop  souvent  la  laideur  dans  le  monde  »,  dans ces créations « parts de soi » qu’il offre, il préfère lui, restituer sa beauté. Pour y parvenir, le trait fin et en même temps tranchant du dessin, sa palette de nuances, se nour-rissent  à  la  sensualité  de  son  regard,  à  ses  bonheurs,  à  sa  joie. Formé à l’INMAAC, le département d’art de l’Université d’Abomey-Calavi, cet aspirant mangaka aux projets de bande dessinée en cours, oriente actuellement son travail vers les installations en recherche permanente d’outils. Avec le body painting, son expression prendra corps – corps, ce point de rencontre  de  l’esprit  avec  la  matière  -,  le  corps  deviendra  médium  et  deviendra  surtout  manifeste.  Manifeste  de  la  beauté  qu’il  voit,  manifeste  car  on  le  verra.  Le  nu,  féminin,  masculin, d’être si rare ici, est d’autant plus visible. Cette pré-sence fragile d’un art qui s’offre presque, à lui de la rendre en «amour, sensualité, élévation spirituelle, contemplation», figure expressive une anatomie dénudée du bonheur. F.B.
            </p>


