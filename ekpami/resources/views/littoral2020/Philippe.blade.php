    <div class="col-md-6 artiste-content-traduction">
            <p>To  conjure  up  the  emptiness  of  the  canvas,  Philippe  Ha-cheme  ‘‘starts  from  absence’’.  A  spellbinding  restitution  of  the  first  creative  gesture  born  from  nothingness,  he  puts black on his canvas before gradually lighting it up. He claims to be a ‘‘figurative expressionist’’, expressionism, because it is by the yardstick of his emotions that he questions his sub-jectivity, the emotion he wishes to arouse, figurative because he  feels  the  need  to  anchor  it  in  his  drawing.  He  refuses  to  pose  sadness,  distress,  unhappiness,  states  of  mind  that  suspend  his  impulse  ‘‘we  are  too  often  shown  ugliness  in  the world’’ in these creations gifts of self where he ‘‘deposits and fixes a part of his soul’’, he prefers it, restoring its beauty. To achieve this, the fine and at the same time sharp line of the drawing, his palette of shades, feeds on the sensuality of his  gaze,  his  happiness,  his  joy.  Trained  at  the  INMAAC,  the  art department of the University of Abomey-Calavi, this aspi-ring mangaka with current comic book projects, is currently directing his work towards installations in permanent search of  tools,  the  new  serves  as  a  catalyst.  With  body  painting,  his expression will take shape - body, this meeting point of the spirit with matter -, the body will become medium and above all will become manifest. Manifest of the beauty that it sees, manifest because we will see it. The nude, feminine, masculine,  to  be  so  rare  here,  is  all  the  more  visible.  This  fragile presence of an art that almost offers itself to him to render it in ‘‘love, sensuality, spiritual elevation, contempla-tion’’ - sketches an anatomy stripped of happiness. F.B.
            </p>
