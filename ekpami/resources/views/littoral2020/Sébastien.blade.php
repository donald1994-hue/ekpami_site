    <div class="col-md-6 artiste-content-traduction">
            <p>Sebastien   Boko,   a   worthy   heir   to  his  father’s  work,  chose  to  make  sculptures.  A  part  of  this  work  is  on  the  theme of the veil. The forms seem to dance in the wind and sometimes contort themselves into an elusive movement of flight towards another place that they alone master. They can also leave the impression of wanting to show several faces in the  same  volume  as  the  Egyptians  taught  us  millennia  ago  and,  closer  to  us,  the  Cubists.  The  successively  concave  and  convex  sides  give  movement  and  life  to  these  characters,  sometimes  out  of  time  or  totally  inserted  in  the  life  of  our  contemporary  societies,  slender  ladies  or  dancers  of  Gèlèdè  whose bells resonate, in the imagination, to our ears as regu-lars.  Her  sculptures  are  welcome.  They  belong  to  our  time  and  contribute  to  renewing  the  iconography  and  concepts  traditionally  attached  to  wood  sculpture,  a  precious  mate-rial whose preservation and transformation into works of art ensures  our  heirs  that  we  have  made  the  best  of  the  riches  of our environment in an exploration that can be infinite. J.A. (excerpt)
