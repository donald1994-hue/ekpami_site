            <p>Sébastien Boko, digne héritier du travail de son père a choisi de faire des sculptures. Un pan de ce travail porte sur le thème du voile. Les formes semblent danser dans le vent et se contor-sionner parfois en un insaisissable mouvement de fuite vers un autre lieu qu’elles seules maîtrisent. Elles peuvent aussi laisser l’impression de vouloir montrer plusieurs faces dans le même volume comme nous l’ont appris les égyptiens il y a des millé-naires et, plus proches de nous, les cubistes. Les faces succes-sivement  concaves  et  convexes  achèvent  de  donner  du  mou-vement et de la vie à ces personnages parfois hors du temps ou  totalement  insérés  dans  la  vie  de  nos  sociétés  contempo-raines,  sveltes  demoiselles  ou  danseuses  de  Gèlèdè  dont  les  grelots résonnent, dans l’imaginaire, à nos oreilles d’habitués. Ses  sculptures  sont  les  bienvenues.  Elles  appartiennent  bien  à  notre  époque  et  contribuent  à  renouveler  l’iconographie  et  les concepts traditionnellement attachés à la sculpture du bois, matière précieuse dont la préservation et la transformation en œuvres d’art assure à nos héritiers que nous avons fait le meil-leur possible des richesses de notre environnement dans une exploration qui peut être infinie.  J.A. (extrait)
