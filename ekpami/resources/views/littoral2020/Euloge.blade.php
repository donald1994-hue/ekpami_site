    <div class="col-md-6 artiste-content-traduction">
            <p>Sculpture  imposed  itself  on  Euloge  Glèlè Ahanhanzo with the need to give volume  to  his  drawings.  Ceramics,  as  a  way  of being in contact with the earth, Sakpata, his god. The rich traditions, the daily life, the Vodun, land of divinities, beliefs, pillar  of  his  culture  are  sources  of  inspiration  for  this  des-cendant of King Glèlè, originally from Abomey, historical ca-pital of the Danxomean royalty. His terracotta integrates the incursion of metal, bronze, aluminium and glass in creations that  are  mostly  anthropomorphic,  a  figurative  that  fades into the abstract in some works and testifies to this strong modernity of African first conceptual art. Characters adorned with finery, ornaments, signs of their social condition, can be painted or remain raw, like this woman’s head O  Tatché  which  retains  the  purity  of  the  drawing’s  lines.  Hwe Li, columns, straight, surmounted by an iron crest in the shape of a hoe blade, protectors of the house egun-guns, in-carnations of ancestors, queens and their court, little people in their daily errands... If Euloge Glèlè Hwe Li are no longer half  buried  in  front  of  houses  and  are  no  longer  meant  to  be  stepped  over  by  women  when  they  cross  the  threshold  to detect adultery, if the Abomey royalty has lost its lustre, their power has shifted into the art’s strength of expression which  in  a  set  of  mirrors  ressurects  it.  Euloge  Glèlè  says:  ‘‘The look on society gave me birth’’. Society gives him the glance back. F.B.
            </p>
