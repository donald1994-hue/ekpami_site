            <p>La  sculpture  s’est  imposée  à  Euloge  Glèlè  Ahanhanzo  avec  le besoin de donner du volume à ses dessins. La céramique, comme manière d’être en contact avec la terre, Sakpata, son dieu. Les riches traditions, le quotidien, le vodun, terroir de divinités,  de  croyances,  pilier  de  sa  culture  sont  sources  d’inspiration  pour  ce  descendant  du  roi  Glèlè,  originaire  d’Abomey,  capitale  historique  de  la  royauté  danxoméenne.  Ses  terres  cuites  intègrent  l’incursion  du  métal,  du  bronze,  de l’aluminium, du verre, dans des créations pour la plupart anthropomorphes,  un  figuratif  qui  s’efface  dans  l’abstrait dans certains ouvrages et témoigne de cette forte moderni-té de l’art africain premier art conceptuel. Des personnages parés  des  atours,  d’ornements,  signes  de  leur  condition  sociale,  peintes  ou  brutes,  comme  cette  tête  de  femme  O Tatché qui  conserve  la  pureté  des  lignes  d’un  dessin.  Des  Hwe Li, colonnes, rectilignes, surmontées d’un cimier de fer en  forme  de  lame  de  houe,  protecteurs  de  la  maison,  des  egun-guns, revenants, incarnations d’ancêtres, des reines et leur cour, des petits gens dans leur course quotidienne... Si les  Hwe  Li  d’Euloge  Glèlè  ne  sont  plus  enterrés  à  moitié  devant les maisons et n’ont plus vocation d’être enjambées par  les  femmes  lorsqu’elles  passent  le  seuil  pour  détecter  l’infidélité, si la royauté aboméenne a perdu de son éclat, leur pouvoir s’est déplacé dans la force d’expression de l’art qui dans un jeu de miroirs les ressuscite. Euloge Glèlè dit : « Le regard sur la société m’a fait naître. » La société lui rend son regard. F.B
            </p>


