    <div class="col-md-6 artiste-content-traduction">
            <p>Subtle observer of life and interpreter of its culture, Thierry Gansa  combines  natural  materials,  both  base  and  stock,  with  his  creative  spark.  He  paints  the  planks  in  fir  wood, this light, clear wood has become the support that inspires, catalyses  and  channels  his  creative  impulse.  In  the  sinuo-sities,  breaches  and  accidents  of  life  that  have  nested  in  the  memory  of  the  tree,  he  picks  out  silhouettes,  shadows,  contours  that  he  delicately  coats  with  natural  pigments  to  paint  his  emotions,  to  reveal  stories  and  understand  the  movement  of  the  world.  Sprinkled  with  symbols,  identity  marks, cultural legacies, his paintings transcribe the values of the past into the modern world they seek to tame.  ‘‘The wood makes the painting truer, more alive, it places the work in a wider world, a larger universe that goes beyond painting itself. Wood has a history, it has lived, it is not the product of  a  mass  production.  Each  wooden  plank  is  different.  This  diversity is a source of inspiration and represents for me the uniqueness  of  the  world’’.  Whether  he  strives  to  decipher  the evils of Africa or those that prevent man from being in harmony with the gods, Gansa calligraphies his world with exquisite sensitivity. F.B.
            </p>
