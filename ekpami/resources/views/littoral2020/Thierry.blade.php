            <p>Subtil  observateur  de  la  vie  et  interprète  de  sa  culture,  Thierry Gansa s’allie les matières naturelles, à la fois socle et souche, de son élan créateur. Il peint les planches en bois de sapin, ce bois clair, léger est devenu le support qui inspire, catalyse  et  canalise  son  élan  créateur.  Dans  les  sinuosités,  les brèches et les accidents de la vie qui se sont nichés dans la mémoire de l’arbre, il relève des silhouettes, des ombres, des contours qu’il enduit délicatement des pigments natu-rels  pour  peindre  ses  émotions,  pour  révéler  des  histoires  et comprendre le mouvement du monde. Parsemés de sym-boles, marques identitaires, héritages culturels, ses tableaux transcrivent  les  valeurs  du  passé  dans  le  monde  moderne  qu’ils  cherchent  à  dompter.  «  Le  bois  rend  le  tableau  plus  vrai, plus vivant, il inscrit l’œuvre dans un monde plus large, un  univers  plus  grand  qui  dépasse  la  peinture  elle-même.  Le bois a une histoire, a du vécu, il n’est pas le produit d’une fabrication à la chaîne. Les planches de bois sont chacune différentes. Cette diversité est source d’inspiration et repré-sente pour moi l’unicité du monde. » Qu’il s’attache à décryp-ter  les  maux  de  l’Afrique  ou  ceux  qui  empêchent  l’homme  d’être en harmonie avec les dieux, Thierry Gansa calligraphie son monde avec une exquise sensibilité. F.B.
            </p>


