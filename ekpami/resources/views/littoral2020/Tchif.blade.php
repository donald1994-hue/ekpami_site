    <div class="col-md-6 ">
            <p>Francis  Nicaise  Tchiakpe  poursuit  inlassablement  dans  son  œuvre la quête qu’il s’est imposée à lui même : comprendre l’homme.  Son  travail,  en  perpétuelle  évolution,  suit  donc  pas à pas les chemins de sa réflexion. Pour lui l’homme est malade, il faut le guérir. Ses tableaux sont donc en quelque sorte des talismans dont la vocation est de pénétrer le cœur des hommes. Pour Tchif nous vivons dans un monde énigma-tique qui est régi par des lois complexes. Ainsi, il a inventé tout  un  langage  composé  de  signes  et  de  symboles  qui  jouent le rôle de médiateur entre l’homme et le monde. La combinaison de ces « signes de vie », souvent géométriques, possède une signification qui participe à la cohérence de l’œuvre.  Le  symbole  le  plus  présent  est  le  margouillat.  Il  représente l’homme, toujours au centre de son travail et de ses  interrogations.  Tchif  entretient  un  rapport  tactile  avec  la matière, quasi sensuel. Ces toiles sont composées de tis-sus cousus ensemble, de fibres intégrées à la peinture, de corde, d’argile... L’artiste joue avec les aspérités de son sup-port qu’il aime travailler à la main, caresser pour sentir son œuvre  évoluer.  Les  ocres,  rouges  et  bruns  qui  constituent  l’essentiel de la palette de Tchif nous renvoient à la terre à la nature, à la vie. Connu pour son travail de peintre plasticien, il expérimente la vidéo, la photo et les installations.
            </p>


    </div>
