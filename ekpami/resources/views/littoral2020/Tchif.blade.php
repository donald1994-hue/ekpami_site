    <div class="col-md-6 artiste-content-traduction">
            <p>Francis  Nicaise  Tchiakpe  tirelessly  pursues  in  his  work  the  quest he has imposed on himself : to understand man. His work,  in  perpetual  evolution,  thus  follows  step  by  step  the  paths  of  his  reflection.  For  him,  man  is  sick,  needs  to  be cured. His paintings are therefore in a way talismans whose vocation is to penetrate the hearts of men. For Tchif we live in  an  enigmatic  world  that  is  governed  by  complex  laws.  Thus, he has invented a whole language composed of signs and  symbols  that  play  the  role  of  mediator  between  man  and the world. The combination of these ‘‘signs of life’’, often geometric,  has  a  meaning  that  contributes  to  the  work’s  coherence.  The  most  present  symbol  is  the  margouillat.  It  represents  man,  always  at  the  centre  of  his  work  and  his  questions.  Tchif  maintains  a  tactile,  almost  sensual  rela-tionship with the material. These canvases are composed of fabrics sewn together, fibres integrated into the paint, rope, clay...  The  artist  plays  with  the  roughness  of  his  support,  which he likes to work by hand, caressing it to feel his work evolve.  The  ochres,  reds  and  browns  that  make  up  most  of  Tchif’s  palette  send  us  back  to  the  earth,  to  nature,  to  life.  Known for his work as a plastic painter, he experiments with video, photography and installations.

            </p>
