            <p>Born  in  Lomé,  Togo,  Typamm  Pierre  Ayi,  lives  and  works  in  Cotonou.  A  graduate of the School of Arts and Crafts, ESMA, he is interested in the singular relationship between men  and  their  conscience.  Multidisciplinary  visual  artist,  street art is his first field of exploration. The transgression of pre-established norms and the subversion of plastic rules lead him to tag the walls of cities to make his contempora-ries  aware  of  their  disagreeable  habits  towards  their  habi-tats, and to underline his existence as a full-fledged citizen of his community: ‘‘I tag, therefore I am’’. Today, as a graphic designer  and  illustrator,  Typamm  extends  his  commitment  as  a  citizen  within  society  by  putting  his  creativity  at  the  service of UNICEF, taking part in workshops for the design of  awareness-raising  materials  through  illustrations,  songs  and  slam,  and  taking  part  in  campaigns  such  as  Zero  Tole-rance for the protection of children against forced marriage. Graffiti, painting, citizen engagement, his constant concern is man in front of his conscience. F.B.

