            <p>Hervé Vianney Cossi Attigla’s work is crossed by the different influences he acquired in his job as a calligrapher-designer. His  canvases  are  pure,  the  light  tones  carry  the  colours  of  everyday  life.  Everyday  life,  whose  topicality  is  the  artist’s  main  source  of  inspiration.  On  paintings,  often  of  large  di-mensions,  Avhec  depicts  the  Beninese  society.  His  interest  is in the common people, those whose ‘‘voice’’ has died out in the difficulties of the daily life and who resignedly live their life without real flavor. When he takes an interest in the institutions that found the same Beninese society or in the policies that govern it, he does so in a very critical way. Avhec stigmatizes a democracy in which the foundations are undermined  by  the  very  institutions  whose  mission  it  is  to  consolidate it. It mocks an oppressive elite, full of itself and ignorant  of  the  values  of  solidarity.  Its  questions  are  those  of the moment, those which, beyond borders, we share with other members of the human brotherhood. D.H.
            </p>
