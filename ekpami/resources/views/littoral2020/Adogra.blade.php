            <p>‘‘His days  pass  without  any  rest,  whe-ther it rains, it’s windy or the sun is at its zenith, he sits on the ground like a stainless metal, brush or other work material in hand, his head bent over his canvas. His head works all the time, as well as the arms’’.  Sculpted  paintings,  sculptures  on  canvas,  natural  materials,  salvaged  materials,  jeans  shreds,  cans,  bones...  Gratien Adotanou allows the environment to penetrate his work  and  give  the  key  to  reading  the  spiritual  life  of  his  community, discouraging anyone who would like to see the ‘‘sorcery’’ associated with vodun as an evil power. His mask-paintings of Ogou, Egun-guns, Gélédé, present these deities as pillars who, through the rituals and ceremonies dedicated to them, participate in the cohesion of the community that recognized  them,  or  else  invented  them.  Strongly  imbued  with her culture and values, Adogra picks with lucid fervour the elements that reveal her and without making them into new  fetishes,  designates  her  vital  forces  and  draws  their  lines with no concessions. F. B
