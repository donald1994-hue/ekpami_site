            <p>«Ses journées se passent de tout repos,qu’il pleuve,qu’il vente ou que le soleil pointe son nez au zénith, tel un métal inoxydable il se trouve assis à même le sol, pinceau ou autre matériel de travail en main, la tête penchée sur sa toile. Chez lui la tête travaille tout le temps, aussi bien que les bras ». Tableaux-sculptés,  sculptures  sur  toile,  matières  naturelles,  matériaux de récupération, lambeaux de jeans, bidons, osse-ments...  Gratien  Adotanou  laisse  l’environnement  pénétrer  dans  son  travail  et  donner  les  clés  de  lecture  des  pans  de  vie spirituelle de sa communauté et s’astreint à décourager qui  voudrait  voir  dans  la  «  sorcellerie  »  associée  au  vodun  un  pouvoir  maléfique.  Ses  tableaux-masques  d’Ogou,  des Egun-guns,  Gélédé,  présentent  ces  divinités  comme  des  piliers  qui,  par  les  rituels  et  les  cérémonies  qui  leurs  sont  dédiées,  participent  de  la  cohésion  de  la  communauté  qui  les  a  reconnus,  ou  alors  inventés.  Fortement imprégné  de sa culture, de ses valeurs,Adogra  cueille  avec  une  ferveur lucide les éléments qui la révèlent et sans en faire des nouveaux fétiches, désigne ses forces vitales et trace leurs lignes sans concessions. F. B
            </p>


