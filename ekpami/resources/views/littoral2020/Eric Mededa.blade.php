            <p>Le travail d’Eric Mededa est une tentative constante d’ana-lyser  la  complexité  humaine.  Pour  la  cerner,  il  part  de  son  histoire, les matériaux seront saisis dans son environnement immédiat, nattes, paniers, qu’il va découper et peindre, des pigments et de la latérite serviront des liaisons avec la terre, autre  source  d’histoire.  Le  tamis  est  particulier,  il  est  pour  lui  emblématique,  objet  indispensable  pour  préparer  les  repas, associé donc à la femme, il l’assigne à ce rôle second et il recueille en même temps ses états d’âme comme dans la série « Dieu bleu ». Chaque morceau de tamis, filtre une histoire et raconte un bout, toutes les histoires des femmes mises bout à bout raconteront la vraie histoire. Sa peinture épurée,  tons  rarement  clairs,  jamais  vraiment  sombres,  «  sonde  les  profondeurs  de  l’homme  imparfait  ».  Sur  ses tableaux, des silhouettes comme des ombres, êtres indéfinis de tailles diverses qui semblent en marche mais ne sont pas tout à fait là, les couleurs ont la finesse et la sensibilité du sujet qu’il aborde, sa profondeur. Comédien, comme il aime les  histoires  et  se  confronter  directement  avec  le  public,  pour « faire passer en force certains messages », il choisit la performance qui l’implique avec son corps entier « pour défendre,  pour  prendre  possession  des  événements  qu’il  dénonce, pour revendiquer un acte, une action déterminée. » Eric Mededa colorie les dieux et tamise la lumière qu’il pro-jette sur les femmes et sur sa culture, à son histoire. F.B.
            </p>


