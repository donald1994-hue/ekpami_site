    <div class="col-md-6 artiste-content-traduction">
            <p>Eric Mededa’s work is a constant attempt to analyze human complexity. To understand it, he starts from his story, Mate-rials  will  be  captured  in  his  immediate  environment,  mats,  baskets, which he will cut and paint, pigments and laterite will serve as links with the earth, another source of history. The sieve is particular, it is for him emblematic, an indispen-sable object for preparing meals, therefore associated with the  woman,  he  assigns  it  to  this  secondary  role  and  at  the  same time it collects his moods as in the series ‘‘Blue God’’. Each piece of sieve, filters a story and tells a part, all the women’s tales put together will tell the true story. His pure painting,  rarely  light  tones,  never  really  dark,  ‘‘probes  the  depths of the imperfect man’’. On his paintings, silhouettes like shadows, undefined beings of various sizes that seem to be on the move but are not quite there, the colours have the finesse and sensitivity of the subject he tackles, its depth. As an actor, as he loves stories and confronting himself directly with  the  audience,  to  ‘‘forcefully  convey  certain  messages’’,  he  chooses  the  performance  that  involves  him  with  his  whole body ‘‘to defend, to take possession of the events he denounces, to claim an act, a determined action’’. Eric Mede-da colours the gods and dims the light he casts on women and on his culture and history. F.B
            </p>
