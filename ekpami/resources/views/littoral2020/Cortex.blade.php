    <div class="col-md-6 artiste-content-traduction">
            <p>In an elegant and subtle approach «to sublimate what we considered not beautiful or elements of everyday life to which we are indif-ferent» Asquith Sognigbé questions ‘‘the incomprehensible obviousness,  complexity  in  simplicity’’.  His  questioning  of  life, death, water, materializes in an elaborate plastic work, summoning  different  mediums  to  make  complementary.  In  photography,  he  favours  black  and  white  and  archives  his  own  visual  memory,  nourished  by  his  view  of  the  voodoo,  the  culture  and  traditions  of  the  country.  Theatricality  in  the  staging,  double  manipulation  of  reality  -  through  the  staging and through the manipulation of photos -, distur-bing  characters,  disguised,  painted  faces,  eyes  hidden  by  hands  or,  like  their  bodies,  partly  concealed  by  props,  Cor-tex Sognigbé brings out his masks. In a society that has not sought  to  break  the  continuity  between  the  world  of  men  and  that  of  the  gods,  mediums  between  the  physical  and  spiritual  worlds  that  are  brought  out  at  precise  moments  to  maintain  balance  and  ensure  social  cohesion,  they  still  retain all their meaning. If the sculptures, adorned with raw or  transformed  elements  of  recuperation,  hold  him  in  the  known,  if  painting  brings  him  on  the  otherwise  sensitive  threshold  of  intuitions,  photography  is  the  hunting  ground  of his desires and fears. Cortex Sognigbé hides the obvious in  the  complexity  of  myths  in  order  to  understand  it  and  invents his other self. F.B
            </p>
