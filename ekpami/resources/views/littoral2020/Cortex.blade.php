            <p>Dans  une  démarche  élégante et subtile  «sublimer  ce  que  nous jugions  pas  beau  ou  des  éléments  du  quotidien  face  auxquels  nous  sommes  indifférents  »  Asquith  Sognigbé  interroge « l’incompréhensible évidence, la complexité dans la  simplicité.  »  Ses  questionnements  portent  sur  la  vie,  la  mort,  l’eau,  sur  «  l’être  mystérieux  »  qu’est  une  femme,  se  matérialisent dans un travail plastique élaboré, convoquent des médiums différents qu’il n’hésite pas à rendre complé-mentaires. Dans la photographie, il privilégie le noir et blanc et  archive  une  mémoire  visuelle  propre,  nourrie  au  regard  qu’il  pose  sur  le  vodoun,  sur  la  culture  et  les  traditions  du  pays.  Théâtralité  dans  la  mise  en  scène,  double  manipula-tion  de  la  réalité  -  par  la  mise  en  scène  et  par  la  manipu-lation  des  photos  -,  des  personnages  troublants,  déguisés,  visages  peints,  yeux  cachés  par  les  mains  ou,  comme  leurs  corps,  dissimulés  en  partie  par  des  accessoires,  Cortex  So-gnigbé fait sortir ses masques. Dans une société qui n’a pas cherché à rompre la continuité entre le monde des hommes et celui des dieux, intermédiaires entre le monde physique et  spirituel  qu’on  sort  à  des  moments  précis  pour  mainte-nir  l’équilibre  et  assurer  la  cohésion  sociale,  ils  conservent  encore  tout  leur  sens.  Si  les  sculptures,  parées  d’éléments  de récupération bruts ou transformés, le retiennent dans le connu, que la peinture l’amène sur le seuil autrement sen-sible des intuitions, la photo est le terrain de chasse de ses désirs et craintes. Cortex Sognigbé masque l’évidence dans la complexité des mythes pour la comprendre et invente son autre moi. F.B.
            </p>


