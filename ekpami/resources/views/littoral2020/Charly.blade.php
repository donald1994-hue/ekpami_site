            <p>Charly  d’Almeida  is  part  of  this  ge-neration of artists who paved the way for  contemporary  art  in  Benin.  His  work  reflects  this  humanistic  approach.  It  is  first  of  all  deeply marked by African culture to which he is very attached and whose signs he still tries to translate, which are of course go-verned by Vodun : ‘‘In Benin, Vodun is more than a religion, it is a culture, a tradition. Benin is divided into two categories, Catholics and Muslims, but all of them are animists, Vodun is omnipresent in people’s lives’’. But his work is also open to the world and its current problems, whether social, political or environmental. Charly d’Almeida has been painting since 1988  and  began  sculpting  in  the  2000s.  Today,  recycled  metal is his preferred means of expression and the revela-tion of an art work that is full of contrasts. From perishable and  outdated  objects,  scraps  of  our  consumer  society,  the  artist creates ageless sculptures that defy time. In his hands, utilitarian metal becomes a support for contemplation and reflection;  heavy  and  cumbersome,  it  nevertheless  gives birth  to  works  tending  towards  abstraction,  symbolism  and the conceptual, whose ethereal titles are charged with mysticism: Croisement, Crossing, Silhouette, Silhouette, Per-fection, Perfection, Double sens, Double sense, Monde sacré, Sacred  Word...  From  the  cold  and  hard  material,  the  artist  creates emotion. For Charly d’Almeida, art is ‘‘a way of repai-ring the wounds of time, people or events’’. C.B
