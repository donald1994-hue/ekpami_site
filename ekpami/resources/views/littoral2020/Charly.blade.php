            <p>Charly d’Almeida fait partie de cette génération d’artistes qui a ouvert la voie de l’art contemporain au Bénin. Son œuvre est à l’image de cette démarche humaniste. Elle est d’abord profondément marquée par la culture africaine à laquelle il est très attaché et dont il cherche encore et toujours à tra-duire les signes, qui sont bien entendu régis par le Vodoun : «  Au  Bénin,  le  Vodoun  est  plus  qu’une  religion,  c’est  une  culture, une tradition. Le Bénin est divisé en deux catégories, les catholiques et les musulmans, mais tous sont animistes, le Vodoun est omniprésent dans la vie des gens ». Mais son travail est également ouvert sur le monde et ses probléma-tiques actuelles, autant sociales que politiques ou environ-nementales. Charly d’Almeida peint depuis 1988 et a débuté la sculpture dans les années 2000. Le métal de récupération est aujourd’hui son moyen d’expression privilégié et le révé-lateur d’une œuvre tout en contraste. D’objets périssables et périmés, rebuts de notre société de consommation, l’artiste tire  des  sculptures  intemporelles  et  destinées  à  défier  le temps. Entre ses mains, le métal utilitaire se mue en support de contemplation et de réflexion ; lourd et encombrant, il donne pourtant naissance à des œuvres tendant vers l’abs-traction, le symbolisme et le conceptuel, dont les titres éthé-rés sont chargés de mysticisme : Croisement, Silhouette, La perfection,  Double  sens,  Parole  sacrée...  Du  matériau  froid  et dur, l’artiste fait naître l’émotion. Pour Charly d’Almeida l’art est « une façon de réparer les blessures du temps, des Hommes ou des événements ». C.B.
