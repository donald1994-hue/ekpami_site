            <p>Dans son nouveau travail Elon-m Tossou semble fasciné par le  mouvement.  Ses  toiles  sont  imprégnées  d’une  énergie  palpable,  les  personnages  s’effacent  presque  derrière  des  silhouettes,  leurs  mouvements  se  superposent,  se  déstruc-turent et font penser aux futuristes italiens, à leur désir de vitesse ; mouvement perpétuel comme s’il voulait pousser le temps hors de son cadre, le faire courir vers un change-ment et une destination nécessaires mais encore inconnus. Une  sorte  d’ossature  apparente  parcourt  parfois  la  toile,  parfois elle « déshabille » le sujet comme dans La Sagesse de l’aigle, comme si pour comprendre, pour aller au delà du perceptible, il lui fallait enlever toute séparation. Ses com-positions aux couleurs apposées au couteau en dégradé, les couches  successives  de  peinture,  rendent  la  toile  matière.  Elon-M  qui  a  une  formation  en  art  publicitaire,  a  fait  une  école  de  métiers  d’art  et  de  culture,  plusieurs  stages  en  décoration,  sculpture  et  cinématographie  est  actuellement  formateur  en  dessin  au  Centre  culturel  chinois  de  Coto-nou. Mouvement, énergie, couleurs, un regard porté sur les rituels,  sur  le  quotidien  et  la  transmission,  les  matières  de  réflexion d’ELon-M. F.B.
            </p>


    </div>
