    <div class="col-md-6 artiste-content-traduction">
            <p>In his new work Elon-m Tossou seems fasci-nated by movement. His canvases are imbued with  a  palpable  energy,  the  characters  almost  fade  behind  silhouettes,  their  movements  overlap,  destructure  themselves  and  make  one  think  of  the  Italian  futurists,  of  their desire for speed; perpetual movement as if he wanted to  push  time  out  of  its  frame,  to  make  it  run  towards  a  necessary but still unknown change and destination. A kind of  apparent  skeleton  sometimes  runs  through  the  can-vas,  sometimes  it  ‘‘undresses’’  the  subject  as  in  La  sagesse  de  l’Aigle,  Wisdom  of  the  Eagle,  as  if  to  understand,  to  go  beyond the perceptible, he had to remove all separation. Her compositions  with  colours  applied  with  a  knife  in  shades,  the successive layers of paint, make the canvas matter. Elon-M,  who  has  a  training  in  advertising  art,  attended  a  school of arts and crafts and culture, several courses in de-coration, sculpture and film is currently a trainer in drawing at the Chinese cultural center in Cotonou. Movement, ener-gy,  colors,  a  look  at  rituals,  everyday  life  and  transmission,  the subjects of reflection of ELon-M. F.B.
            </p>
