            <p>In the work of Valentin Zedahouan, ‘‘nothing is prepared in advance’’.  As  a  young  teenager,  forced  by  circumstances  to  provide for a family by improvised means, art offered him a path that he hastened to follow. Artist composer, rumba and soyoyo (traditional song from Benin) singer, used to the mo-vements of chance, he takes them into painting. He spreads the colours on the canvas in a spontaneous movement, ta-king care to balance, nuance and harmonize them. Then, as he simply says: ‘‘he sits down to read the painting and asks himself  what  it  shows  before  titling  it’’.  This  ‘‘spontaneous’’  movement is born from his observations on society, from the imprint of his life experience, from his view of artistic prac-tices  forged  by  5  years  of  training  with  Dominique  Zinkpé.  Zecas  uses  recycled  materials  in  his  painting,  from  yester-day and today, his canvases and masks are witnesses of his attachment to cultural values and the environment. F.B.
            </p>
