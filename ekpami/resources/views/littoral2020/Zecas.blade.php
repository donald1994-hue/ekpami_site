            <p>Dans  les  travaux  de  Valentin  Zedahouan,  «  rien  n’est  pré-paré  à  l’avance  ».  Jeune  adolescent,  obligé  par  les  circons-tances de subvenir par des moyens improvisés aux besoins d’une famille, l’art lui a offert une piste qu’il s’est empressé de  suivre.  Artiste  compositeur,  chanteur  de  rumba  et  de  soyoyo  (chant  traditionnel  du  Bénin),  habitué  aux  mouve-ments  du  hasard,  il  les  emporte  dans  la  peinture.  Il  étale  les  couleurs  sur  la  toile  dans  un  mouvement  spontané  en  prenant  soin  d’équilibrer,  de  nuancer  et  de  les  harmoniser,  puis,  comme  il  le  dit  simplement:  «il  s’asseoit  pour  lire  le  tableau et s’interroge sur ce qu’il dégage avant de le titrer». Ce mouvement « spontané » naît de ses observations sur la  société,  de  l’empreinte  de  son  vécu,  de  son  regard  sur  les  pratiques  artistiques  forgé  par  5  années  de  formation  auprès de Dominique Zinkpé. Zecas utilise dans sa peinture des  matériaux  de  récupération,  d’hier  et  d’aujourd’hui,  ses  toiles et ses masques sont témoins de son attachement aux valeurs culturelles et à l’environnement. F.B
            </p>


