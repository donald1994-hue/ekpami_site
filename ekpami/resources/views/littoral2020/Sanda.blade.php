    <div class="col-md-6 artiste-content-traduction">
            <p>Between reality and phantasm, between dreams  and  desires,  Amadou  Imorou  San-da’s  imaginary  architectures  are  reinterpretations  of  the  cultural space of the Fulani people. Fabulous topographies that  take  up  the  motifs,  exalt  the  daily  life,  the  nomadic  shepherds’ ancestral way of life, labyrinthinic arrangements, abounding  compositions,  allegorical  imprints  of  a  territory,  which tear them away from time. The fine, precise drawing, graphic  mantra,  hallucinated  by  lucid  visions  that  could  make one think of Piranesi’s imaginary prisons, retains and protects  a  space  of  freedom  that  is  disappearing,  captured  by  the  prejudices,  by  the  systematic  destruction  of  nature.  Freedom  to  keep  out  of  reach  of  modern  conformist  and  unifying  lifestyles,  freedom  to  live  in  an  environement  respectful of the nature. The Fulani form a community wit-hout fading into a false diversity, rites hold them together, tattoos,  jewellery,  make  sense  and  nomadism  makes  them  united.  If  their  lifestyles  are  diversifying,  some  are  settle  down  study,  their  freedom  seems  trapped  in  an  identity-based  withdrawal,  pulaaku,  that  protects  them  and  at  the  same  time  isolates  them.  Safeguarding  and  transmitting,  values that Sanda’s art defends in his name and in the name of his people. His metaphysical interpretation, the richness of  the  details  of  the  drawing  makes  one  dizzy,  provokes  a  kind of glance drunkenness which goes beyond and extends the  construction  of  the  artist.  If  the  motifs  are  decorative,  if elements are figurative, the whole is detached from the concrete and takes the abstract form of a survival. F.B.

            </p>
