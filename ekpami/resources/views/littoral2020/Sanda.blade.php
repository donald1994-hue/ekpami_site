            <p>Entre  réalité  et  fantasme,  entre  rêves  et  désirs,  les  archi-tectures imaginaires de Amadou Imorou sont des relectures de l’espace culturel du peuple peul. Topographies fabuleuses qui reprennent les motifs, exaltent le quotidien, le mode de vie ancestral du peuple de bergers nomades, compositions labyrinthiques,  foisonnantes,  empreintes  allégoriques  d’un  territoire, qui les arrachent au temps. Le dessin fin, précis, mantra  graphique,  halluciné  des  visions  lucides  qui  pour-raient faire penser aux prisons imaginaires de Piranesi, re-tient et protège un espace de liberté en train de disparaître capturé  par  les  préjugés,  par  la  destruction  systématique  de  la  nature.  Liberté  de  se  tenir  hors  d’atteinte  des  modes  de vies modernes conformistes et uniformisants, liberté de vivre dans un environnement respectueux de la nature. Les Peuls  font  communauté  sans  s’effacer  dans  une  diversité  factice,  les  rites  les  tiennent  ensemble,  les  tatouages,  les  bijoux, font sens, le nomadisme les rend solidaires. Si leurs modes de vie se diversifient,  certains se sedentarisent, font des études, leur liberté semble piégée dans un repli identi-taire, pulaaku, qui les protège et en même temps les isole. Sauvegarder  et  transmettre  les  valeurs  que  l’art  de  Sanda  défend en son nom et au nom de son peuple. Son interpré-tation métaphysique, la richesse des détails du dessin donne le vertige, provoque une sorte d’ivresse du regard qui va au-delà et étend sa construction. Si des motifs sont décoratifs, si  des  éléments  sont  figuratifs,  l’ensemble  se  détache  du concret, prend la forme abstraite d’une survivance. F.B.
