            <p>In his painting, Lionel Ogoudjobi Atteré is mainly interested in the human being. Attentive to the expression of faces in his  drawings,  of  the  body  in  body  painting,  he  transcribes  symbols and vodun motifs, expressions of beliefs and links with an invisible world that influences the visible. In body painting,  in  a  marketing  operation,  he  chooses  women’s  body  which  he  considers  the  most  effective  way  to  draw  attention  to  the  ideas  he  wishes  to  materialize.  ‘‘The  body  carries the spirit and the spirit nourishes the body. The body carries  the  mind  and  the  mind  nourishes  the  body’’.  Lionel  Davinci wants to highlight African culture and raise aware-ness of certain realities. F.B.
