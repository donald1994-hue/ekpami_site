            <p>The  activities  of  Hervé  Alladayè,  who  sometimes  signs  Hodall  Beo,  are  multiple  but  he  is  above  all  a  well-known  cartoonist in several Beninese newspapers. After drawing for ‘‘Le  point  au  quotidien’’,  his  cartoons  are  published  in  ‘‘Le  Progrès’’. The humour and talent he has always shown in his press cartoons earned him 2nd prize at the Yaoundé Cartoon Festival  at  the  2002  Fescary.  As  a  book  illustrator,  he  has  already  signed:  Drôle  de  jumelles  with  Félix  Nitwé  (Edicef  /  Le  Flamboyant,  Paris  2000),  Coco  taillé  à  la  boule  zérowith Michèle Nardi, (Cérès Editions), Rose-Fleur, with Koffivi Assem, (éditions Ruisseaux d’Afrique in 2001), Kaïvi l’enfant placée on a text by Béatrice Lalinon Gbado (Cérès Editions, 2002).  He  is  also  a  screenwriter  and  has  published  several  comic books, including Chiottes et cacahuètes in 1999, and above  all  Les  zémidjans  protestent  in  2000,  a  tasty  humo-rous  report  in  eight  comic  strips  on  the  motorcycle  taxis  that criss-cross Cotonou (COPEF printing house), which was a great success and whose sequel, Les zemidjans persistent was published in 2006. Hervé Alladayè was part of the exhi-bition  ‘‘The  heroes  of  the  Beninese  comic  strip’’  organized  in  April  2006  by  the  French  Cultural  Centre  of  Cotonou  to  honor  10  Beninese  cartoonists.  Among  his  many  painting  exhibitions, one of the most important is ‘‘Mutation’’, in 2000 at the Médiathèque des Diasporas in Cotonou. A.B.(adapted)
