    <div class="col-md-6 artiste-content-traduction">
            <p>Silence  as  a  source  and  ‘‘my  inner  man  as  a  mentor’’,  Achille  Adonon’s  art is an explosion of scriptures, moving mind and noumenic, phenomenal symbols. Of the order of metaphysics, for this artist, who constructs with his elders and in books, reality occupies only a tiny, even dis-parate portion. An adept of lyrical abstraction, he adorns his pictorial works with forms and scribbles that he believes to be divine. There is here a quest for invisible realities, a will to put on his canvases his anguishes, his pains, his fears, his desires, his hopes, whether or not they are of divine origin. Thus, he plays on a polychromic palette with the survival of a pale blue, a reflection of water, sky and life that would be a divine grace, a blood red, which forges human self-denial. If Achille Adonon likes to say that his canvases are divine in essence, his works of volume deal rather with tangible and contemporary  realities:  street  children.  His  wall  sculptures  are wooden plates where he associates, as a child, glues and paints in the same primary red of the scraps of the street, the shoes that dot the Beninese cities and pollute them. As for him ‘‘each shoe symbolizes a street child’’, he shelters them, offers them a house, a fixed refuge in time and space. The harmony in the arrangement of the elements reflects the solidarity born of the need to face the vicissitudes of life. A painter  of  metaphysics  and  an  ecological  sculptor,  Adono-non is between attraction towards the divine and immersion in reality. S.A. (adapted)
            </p>
