            <p>Le silence comme source et « mon homme intérieur comme guide ». L’art d’Achille Adonon est une explosion d’écritures, d’esprit  en  mouvement  et  de  symboles  noumèniques,  phé-nomèniques. De l’ordre de la métaphysique, pour cet artiste construit auprès de ses aînés et dans les livres, la réalité n’occupe qu’une portion infime, voire disparate. Adepte de l’abstraction lyrique, il pare ses œuvres picturales de formes, d’écritures  gribouillis  qu’il  pense  être  divines.  Il  y  a  là  une  quête  de  réalités  invisibles,  une  volonté  de  mettre  sur  ses  toiles  ses  angoisses,  ses  peines,  ses  peurs,  ses  envies,  ses  espérances,  qu’elles  soient  ou  non  d’origine  divine.  Ainsi,  joue-t-il  sur  une  palette  polychromique  avec  la  survivance  d’un bleu pâle, reflet de l’eau, du ciel et de la vie qui serait une  grâce  divine,  d’un  rouge  sang,  qui  forge  l’abnégation  humaine.  Si  Achille  Adonon  aime  dire  que  ses  toiles  sont  d’essence divine, ses œuvres de volume abordent plutôt des réalités tangibles et contemporaines : les enfants de la rue. Ses  sculptures  murales  sont  des  plaquettes  de  bois  où  il  associe,  engeance,  colle  et  peint  dans  ce  même  rouge  pri-maire des rebuts de la rue, les chaussures qui parsèment les villes béninoises et les polluent. Comme pour lui « chaque chaussure symbolise un enfant de la rue », il les abrite, leur offre une maison, un refuge fixe dans le temps et l’espace. L’harmonie dans l’agencement des éléments traduit la soli-darité née de la nécessité de faire face aux péripéties de la vie. Peintre de la métaphysique et sculpteur écolo, Adonon entre attraction vers le divin et plongée dans la réalité. S.A. (adapté)
            </p>


