            <p>Kolawolé Tidjani Serpos is a graphic designer, visual artist, illustrator,  engraver  and  photographer.  After  his  training  at  ESMA, a science, arts techniques and crafts school in Benin, a scholarship at the École Supérieure des Arts Visuels in Mar-rakech, Morocco enabled him to obtain a master’s degree in visual communication design. Kolawole Tijani Serpos’s work offers an alternative perspective that questions the notions of legacy, trace, space, limit and loss. He is constantly atten-tive  to  the  chromatic  vibrations  and  forms  that  illuminate  the visible to reshape it in an aesthetic from another dimen-sion,  between  depiction  and  abstraction.  He  plays,  creates  shifts,   diverts,   shapes,   transforms   and   then   places   the   spectator  as  the  main  actor  in  each  of  his  works.  Everyday  life,  modernism,  women,  religion  and  endogenous  African  realities constitute the repertoire from which he draws his inspiration. P.H. (excerpt)
