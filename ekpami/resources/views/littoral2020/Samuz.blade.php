            <p>‘‘The world moves so fast and remains so mysterious’’. Remi Ama  Sossouvi  keeps  the  eyes  of  a  child  who  became  an  adult  who  drew  his  signature  from  a  carved  piece  of  wire:  on his curriculum vitae ‘‘his photo’’ is the image of one of his first creations, a child pushing and running behind a circle. In  his  race  there  is  time  passing  by  and  the  world  that  his  gaze  wanders  through,  the  world  of  little  people,  ordinary  life, mothers protecting their children, young girls going to school,  one  day  becoming  princesses,  a  world  as  round  as  the  child’s  circle.  If  at  the  beginning,  her  sculptures,  inter-lacing  flexible,  coiled,  twisted,  braided  wires  of  her  ama-zement  and  observations  of  birds  nests,  let  reality  show  through in a more faithful way, today they are freeing them-selves, composing with space, with light, with new materials. A  master  turner  by  training,  Samuz  is  attached  to  metal  in  all  its  forms,  an  accessory  to  a  character,  he  will  anchor  it  in  reality  -  never  totally  because  he  likes  to  compose  with  the emptiness of space - otherwise, as in a fairy tale, he will stop  halfway  through  his  work  and  let  the  already  braided  threads  unfold  freely.  Thus,  the  motorbike  that  takes  the  half-sculpted child on a tour of the city becomes the trace of a memory, the athlete who cuts the air from his bust has his legs carried away in the speed of movement, the young schoolgirl  is  the  schoolbag  that  carries  the  weight  of  the  materials,  she  carries  the  recklessness  of  her  age,  remains  mild... Remains aerial, a kind of poetry of the unfinished that can be continued (or not) in thought. F.B.

            </p>
