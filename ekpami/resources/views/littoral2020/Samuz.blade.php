            <p>« Le monde évolue si vite et demeure si mystérieux ». Rémi Ama Sossouvi garde les yeux d’un enfant devenu adulte qui a tiré sa signature d’un ouvrage en fil de fer sculpté : sur son  curriculum  vitae  «  sa  photo  »  est  l’image  d’une  de  ses  premières créations, un enfant qui pousse et court derrière un  cercle.  Dans  sa  course  il  y  a  le  temps  qui  défile  et  le monde que son regard parcourt, celui des petits gens, la vie ordinaire, des mères protègent leur enfant, des jeunes filles vont  à  l’école,  deviendront  un  jour  princesses,  un  monde  rond comme le cercle de l’enfant. Si à ses débuts, ses sculp-tures, entrelacs de fils de fer souples, enroulés, tordus, tres-sés de son émerveillement et des observations sur les nids d’oiseaux,  laissaient  transparaître  la  réalité  d’une  manière plus  fidèle,  aujourd’hui  elles  s’affranchissent,  composent avec l’espace, avec la lumière, avec des nouvelles matières. Maître tourneur de formation, Samuz est attaché au métal sous  toutes  ses  formes,  accessoire  d’un  personnage,  il  an-crera celui-ci dans la réalité - jamais totalement car il aime composer avec le vide de l’espace – sinon, comme dans un conte,  il  arrêtera  à  mi-chemin  son  ouvrage  et  laissera  les  fils  déjà  tressés  se  déployer  librement. Ainsi,  la  moto  qui fait parcourir la ville à l’enfant sculptée à moitié, devient la trace d’un souvenir, l’athlète qui coupe l’air de son buste a les jambes emportées dans la vitesse du mouvement, la jeune écolière,  c’est  le  cartable  qui  porte  le  poids  des  matières,  elle,  elle  porte  l’insouciance  de  son  âge,  reste  légère...  De-meure aérienne, une sorte de poésie de l’inachevé que l’on peut continuer (ou non) en pensée. F.B.
            </p>


