            <p>‘‘Today  I  am  exploring  the  sensitivity  of materials while in the universe eve-rything  is  matter  at  various  degrees.  I’ve  been  looking  at  its  instantaneous  side,  completely  surpri-sing. A free way of conceiving the existing without leaving out the real which remains the foundation of my work. I try to  gather  my  questions  about  Man  in  a  projection.  I  hide  with silk paper, matchboxes and bits of fabric, I add strings to establish the link between body and mind and then I spread unknown faces; I veil the expression of faces by colours that meet by instinct, that like a flame set the canvas ablaze. I don’t  adjust  the  outline  because  perfection  is  not  of  this  world. I seek my sincerity. I let the material lead me towards mystery, where constraints do not exist. Man is at the center. He is the ladder that leads to the top and through him eve-rything can come down. The stages of life are conditioned by directions and paths that I allow myself to add using razor blades. The mouth is open because the speech is sacred. It is poetry without words. A poetry of African ancestral values’’. (adapted)Aris paints and writes, he is an author, composer and archi-tect collaborator. To allow the emergence of talents, to serve and defend African artistic and cultural values, he joins the initiative Festival Ilé ya Africa, in Yoruba, Africa, it’s time to go home.
