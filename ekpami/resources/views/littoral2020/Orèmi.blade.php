            <p>Iniagna   Cossi   Oré   recovered   one   day  on  a  beach  in  Cotonou,  Fidjrossé,  a  calabash  with  sacrificial  remains  that he  carved  and  transformed  into  a  mask.  Chance  brought  him  to  creation.  Attracted  today  by  different  mediums  and  by  a  collaborative  work,  he  proposes  installations  in  duo  with  Harold  Kuassi  to  tell  the  faults  of  Beninese  society:  Le Mutisme,  the  silence  on  things  that  hurt,  on  the  astro-nomical  expenses  that  put  the  family  into  debt  during  the  burial  ceremonies  in  Agô, tu me dois combien? (Agô,  how  much you owe me?) Grandson of ‘‘Pygmies’’ from Gabon, he explores the universe of this people who live in the forests of Congo, Cameroon, but also in the savannah, who have a reputation as outstanding hunters but who are also crafts-men in Rwanda, fishermen in Angola. Fascinated by the ways of life, by the architectural prowess of the dwellings, by the customs and the rites of pasture, his painting records ‘‘traces, joys and sufferings of a people who made a choice, to living in nature and remaining faithful to it for eternity’’.  In truth, Pygmies don’t exist. The men who do exist have the names Baka, BaBongo, BaKola, BaAka, BaSua, Èfè, Asua, BaTwa... Who will  ever  know  what  they  have  in  common,  other  than  to  exalt  the  imagination  of  the  West.  H.V.Valois,  1980,  article,  Bulletin et mémoires de la société d’anthropologie Paris.Orémi must have the intuition, his paintings guess particu-lar universes like imprints. F.B

