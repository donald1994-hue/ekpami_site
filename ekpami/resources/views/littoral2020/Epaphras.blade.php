            <p>Epaphroditos,   ‘‘devoted   to   Aphrodite‘‘.   Epaphas Toihien is devoted to bringing beauty out  of  the  wood  as  one  does  in  his  family  Donvidè,  in  his  region of Agonli. He searched for a writing, he found several spellings and a signature. The statuary, stylized silhouettes, which  from  being  without  arms  embraced  better  by  their  bodies,  that  he  carves  out  of  the  wood  as  one  would  take  out  the  masks,  as  from  a  natural  soil  that  engenders  and  contains them while leaving them free in their movements. The verticals, which could have been inspired by Brâncusi’s wooden sculptures in a game of cross influences, a mirror game  with  the  so  modern  abstract  archaic  art  of  the  Ro-manian sculptor influenced by African art, rise up, Hwedo posts,  protectors,  elegant  entanglements  of  faces,  hands,  textures. His Forêt des murmures,Forest of whispers, seems to  emerge  from  the  archetypes  that  cross  the  myths,  sus-pended between here and elsewhere, it murmures incanta-tions that still make us shiver. The signature, the stripes he prints on the wood, life events among peoples in Africa, sca-rifications of identity in Benin. Epaphras has to take a long look  at  the  wood  and  plunge  into  his  soul,  he  also  needs  to  compel  it,  keep  track  of  his  doubts,  use  spaces’  empti-ness. And it is there, in those interstices where beauty does not look for an agreed reflection, that it returns pure to the wood and finds its sap. Enrolled at the Faculty of Fine arts in,  Brazil,  Epaphras  also  lives  and  works  in  Sao  Paolo  andCotonou, Benin. F.B.
            </p>
