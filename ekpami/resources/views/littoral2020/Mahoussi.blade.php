            <p>Pierre  Mahoussi  Ahodoto  a  grandi  sans  famille  et  a  connu  la rue, c’est le « pinceau » qui a été son salut, son rempart contre la solitude. Quelques années auprès de l’artiste Ado-gra qui l’encourage et lui « inculque le sens du travail réalisé à mains nues » canalisent son travail, quelques expositions, le  mettent  dans  la  bonne  voie.  Les  sculptures  brutes  com-posent  avec  des  matériaux  non  renouvelables,  récupérés,  « collectés », montrent son engagement pour l’environne-ment et dans la vie sociale qu’elles dévoilent, des éléments clés de sa culture, la calebasse à offrandes, les tablettes aux signes divinatoires du Fâ, des potions, lui permet une lecture géopolitique du continent africain dans une installation. Sa peinture  qui  s’est  vue  allégée  de  la  plupart  des  éléments  figuratifs, on remarquera une roue, celle de la vie peut être qui revient leit-motif, des traits noirs ou blancs qui suivent un  élément  et  le  renforcent,  a  fait  place  à  l’unique  variété  des  couleurs  pour  des  toiles  vibrantes,  lumineuses.  Dans  un  jeu  de  mots  qu’il  affectionne,  Mahoussi  «  parti  de  zéro,  pour  devenir  héro  »  d’un  art  qui  allège  l’environnement  et  apporte l’émotion dans le quotidien. F.B.
            </p>


