    <div class="col-md-6 artiste-content-traduction">
            <p>Pierre  Mahoussi  Ahodoto  grew  up  without  a  family  and  knew the street, the ‘‘paintbrush’’ was his salvation, his bu-lwark against loneliness. A few years with the artist Adogra, who encouraged him and ‘‘instilled in him the sense of work done  with  bare  hands’’,  channeled  his  work,  a  few  exhibi-tions,  put  him  on  the  right  track.  The  raw  sculptures  made  with  non-renewable  materials,  recovered,  ‘‘collected’’,  show  his  commitment  to  the  environment  and  in  the  social  life  they  reveal,  key  elements  of  his  culture,  the  offering  cala-bash,  the  tablets  with  divinatory  signs  of  the  Fâ,  potions,  allows him a geopolitical reading of the African continent in an installation. His painting, which has been eased by most of the figurative elements, we will notice a wheel, that of life itself maybe, that returns leit-motif, black or white lines that follow an element and reinforce it, has given way to the unique variety of colors for vibrant, luminous canvases. In a pun  that  he  loves,  Mahoussi  ‘‘started  from  zero,  to  become  a hero’’ of an art that lightens the environment and brings emotion into everyday life. F.B
            </p>
