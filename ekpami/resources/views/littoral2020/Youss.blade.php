            <p>His  pictorial  work,  which,  as  it  is  often  the  case  here,  has  freed  itself  from  the  encounter  with  Basquiat  and,  more  rarely, with Pollock’s dripping, is above all imbued with the personality of his brush. Beyond references, Youssouf Anato is well on the way to his own discoveries. He does not work undercover, neither with tradition nor ‘‘contemporary art’’, he seeks, explores and tries to define his brands. A portrait of a woman in squares and rectangles, a magma of abundant pictorial  matter,  succulent,  gorged  with  blue,  adorned  with  the flows of red, the sap of yellow, the dense purity of the tears of white, round arms, fingers spread out over the por-trait of her receptive heart. The hands are always present on her canvases, symbols of gift, of work, more present than the face, even if it is visible, more than the body sketched in a few strokes, the essence, the homo faber is there, in his abi-lity  to  transform  his  world.  There  is  the  powerful  vibration  of colours, that of his earth, the energies cross in his pain-tings, there is also the audacity of his ‘‘urban’’ drawing which does not seek to reproduce but to concentrate in a figure elements of today, the modernity which reduces man, auto-mated by too much science, by too much reason, too much progress, takes away a part of his soul. Only certainties, the earth, the hand, the queen mother and the woman.F.B.
