    <div class="col-md-6 artiste-content-traduction">
            <p>Gandhi Tomedè’s essence and ferocious existence are driven by  a  curiosity  that  animates  her  thematic  orientations,  her  artistic  explorations  and  her  life  choices.  In  this,  it  is  self-taught, in its foundation as in its functioning, in its deploy-ment as in its renewal. It is from plastic art that she draws her  sense  of  self-control,  her  desire  to  learn,  to  constitute  herself and to become from the self. She finds in it a true consoling  tranquility,  a  power  that  frees  her  from  the  shackles of the world, from the so-called orthodox demands, from the unfailing certainties of society. Thus, Gandhi consi-dered plastic art as a place for observing the world around him, a base from which his connection with his share of inti-macy is born and a channel for expressing his impressions of the surrounding realities. His creation is therefore a plunge into  his  inner  being,  into  his  existential  disorder,  into  the  full  nature  of  his  delicate  heart  and  into  the  setting  of  his  hyperactive sensitivity. After studying economics and socio-logy, which helped redefine her relationship with the world, she chose art for consolation, for introspection, for reflexive approach.  She  has  been  invited  to  residencies,  workshops  and exhibitions in Benin, at the Goethe Institute in Togo, at the  CCF  in  Niamey  in  Niger  and  Lagos  in  Nigeria,  at  ENSA  Dijon and MacVal in Ivry sur Seine, in France. Today, Gandhi is experimenting with the dematerialization of writing. For her, it is a question of exploring the metamorphic dimension of  words,  but  above  all  of  establishing  the  subversive  cor-relation between the act of painting and the act of writing.
            </p>
