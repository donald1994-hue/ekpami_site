            <p>D’essence et d’existence fureteuse, Gandhi Tomedè est mue par une curiosité qui anime autant ses orientations théma-tiques, ses prospections artistiques que ses choix de vie. En cela, elle est autodidacte, dans le fondement, comme dans le fonctionnement, dans le déploiement comme dans le renou-vellement. C’est dans l’art plastique qu’elle puise son sens du contrôle de soi, son désir d’apprendre, de se constituer et de devenir à partir de soi. Elle y trouve une véritable quiétude consolatrice, un pouvoir qui la libère des carcans du monde, des exigences dites orthodoxes, des certitudes indéfectibles de la société. Ainsi, Gandhi considère l’art plastique comme étant un lieu d’observation du monde qui l’entoure, un socle d’où naît sa connexion avec sa part d’intimité et un canal d’expression de ses impressions vis-à-vis des réalités envi-ronnantes. Sa création est de ce fait, une plongée dans son intérieur d’être, dans son trouble existentiel, dans la pleine nature de son cœur épris de délicatesse et dans l’écrin de sa sensibilité  hyperactive.  Après  un  parcours  en  études  d’éco-nomie et de sociologie qui ont contribué à redéfinir son rap-port au monde, elle choisit l’art par consolation, par volonté d’introspection, par démarche réflexive. Elle a été invitée à des résidences, ateliers et expositions au Bénin, à l’Institut Goethe au Togo, au CCF de Niamey au Niger et de Lagos au Nigéria, à l’ENSA Dijon et MacVal à Ivry sur Seine en France. Gandhi  expérimente  aujourd’hui,  la  dématérialisation  de  l’écriture.  Il  s’agit  pour  elle,  d’explorer  la  dimension  méta-morphique  des  mots,  mais  surtout  d’établir  la  corrélation  subversive  entre  l’acte  de  peindre  et  celui  d’écrire.  
            </p>


    </div>
