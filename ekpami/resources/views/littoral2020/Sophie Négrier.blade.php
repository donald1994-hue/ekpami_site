            <p>«Borderline»  is  a  meeting  of  vibratory  lines  around  the  frequency 528HZ.Visual  encounter  between  natural  elements,  coming  from  nature and an extension of the body; the sea and the hair ...Borderline brings together drawing and photography.Lines  that  are  drawn,  loosened,  cut  or  embraced.  From  nature,  to  the  societal  question:  do  the  vibrations  become  virtual or pixels? Sophie  Négrier  has  been  living  and  working  in  Benin  for  about ten years. She has been working in the photographic medium  in  order  to  propose  a  look  towards  «the  other»  in  close discussion with the living, the vibrations.

