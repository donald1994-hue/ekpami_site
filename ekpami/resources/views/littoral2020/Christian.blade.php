    <div class="col-md-6 artiste-content-traduction">
            <p>Christian  Gbegnon  trained  in  graphic  arts  and  decoration  in  the  workshop  of  Joseph  Kpobly,  a  recognized  master  in  Cotonou.  Oil  pain-ting,  gouache,  knives,  brushes  and  titles  open  the  door  to  interpretation. The canvases are bathed in a disturbing, mys-terious silence that seems prophetic. The landscape seems rarefied, familiar places and yet out of nowhere, colours with minute  variations  thwart  without  disturbing  the  habits  of  the eye, participate in the strangeness of the atmosphere. A colour reigns above all, nature, characters, animals carry its nuances, a few fine touches of yellow or white illuminate it in places and enhance it even more. The slender silhouettes that cross or freeze the landscape, seem to be from another world. Without face, without external signs, simple accesso-ries melted into the decor or hieratic figures of an imposing presence, they are there, they could not be elsewhere. Their outline affirms them, they are often larger than life in an almost biblical, almost supernatural setting. What may seem indifferent,  softens  with  a  deeper  look,  becomes  nuanced.  One  notices  the  almost  total  absence  of  angles,  the  lines  of the drawing are rounded, the houses all curved, the hilly landscape, one can guess a mark of tenderness. His charac-ters are draped, almost as if they were hidden, not covered but protected by their clothes, their fragility, their sensitivity is sheltered. Christian Gbegnon is looking for the bridge that connects  people  and  beings  to  their  environment,  subli-mates landscapes and paints presses. F.B.
            </p>
