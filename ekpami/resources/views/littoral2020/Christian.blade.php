            <p>Christian Gbegnon s’est formé aux arts graphiques et déco-rations  dans  l’atelier  de  Joseph  Kpobly,  maître  reconnu  à Cotonou.  Peinture  à  l’huile,  gouache,  couteaux,  pinceaux  et  titres  qui  ouvrent  la  porte  à  l’interprétation.  Les  toiles  baignent  dans  un  silence  inquiétant,  mystérieux,  qu’on  di-rait  prophétique.  Le  paysage  est  comme  raréfié,  des  lieux familiers  et  pourtant  de  nulle  part,  des  couleurs  avec  des  variations infimes contrarient sans déranger les habitudes du  regard,  participent  de  l’étrangeté  de  l’atmosphère.  Une  couleur  règne  en  maître,  nature,  personnages,  animaux portent ses nuances, quelques fines touches de jaune ou de blanc l’éclairent à des endroits et la rehaussent encore. Les silhouettes longilignes qui traversent ou figent le paysage, semblent  d’un  autre  monde.  Sans  visage,  sans  signes  exté-rieurs, simples accessoires fondus dans le décor ou figures hiératiques d’une présence qui impose, elles sont là, elles ne pourraient être ailleurs. Leur contour les affirme, elles sont souvent plus grandes que nature dans un décor presque bi-blique, presque surnaturel. Ce qui peut sembler indifférent, s’adoucit  à  un  regard  approfondi,  se  nuance.  On  remarque  l’absence  presque  totale  d’angles,  les  traits  du  dessin  sont  arrondis, les maisons tout en courbes, le paysage vallonné, on y devine une marque de tendresse. Ses personnages sont drapés, on dirait cachés presque, non pas couverts mais pro-tégés par leurs vêtements, leur fragilité, leur sensibilité est mise à l’abri. Christian Gbegnon cherche le pont qui relie les êtres  et  les  êtres  à  leur  environnement,  sublime  des  pay-sages et peint des présages. F.B.
            </p>


