    <div class="col-md-6 artiste-content-traduction">
            <p>A self-taught artist, Romuald Mevo Guezo was first interested in painting and portraiture. But after several years of prac-tice, he turned away from this orientation and came up with a more unique and innovative idea. He then began to prac-tice  sculpture,  initially  on  wood,  to  end  up  for  a  few  years  now  in  what  he  calls  ecoplastic  sculpture.  Romuald  Mevo  Guezo is the inventor of this very personal technique which he calls ‘‘universal artistic conception’’. It consists of melting recycled  plastic  to  which  the  artist  adds  various  recycled  materials  that  only  he  has  the  secret,  to  create  sculptural  works that are so singular. The art of recovery and recycling represents for him an essential artistic choice: ‘‘This is what will make our strength and determine our uniqueness’’. This art, associated with elements of Beninese culture, is a possi-bility for the artist to create new works of art. G.D
            </p>
