            <p>Artiste  autodidacte,  Romuald  Mevo  Guezo  s’est  d’abord  intéressé  à  la  peinture  et  au  portrait.  Mais  après  plusieurs  années  de  pratique,  il  se  détourne  de  cette  orientation  et  réfléchit à une idée plus originale et innovante. Il s’exerce alors à la sculpture, au départ sur bois, pour aboutir depuis quelques  années  à  ce  qu’il  appelle  ecoplastic  sculpture.  Romuald  Mevo  Guezo  est  l’inventeur  de  cette  technique  si  personnelle qu’il qualifie de « conception artistique univer-selle ». Elle consiste à faire fondre des plastiques de récu-pération auxquels l’artiste ajoute divers matériaux recyclés dont lui seul a le secret, pour aboutir à des œuvres sculptu-rales si singulières. L’art de la récupération et du recyclage représente pour lui un choix artistique essentiel : « C’est cela qui fera notre force et déterminera notre particularité ». Cet art, associé à des éléments de la culture béninoise, est une possibilité  pour  l’artiste  d’imprimer  son  identité  culturelle  au cœur de ses œuvres. Mais aussi de nous délivrer un mes-sage  écologique,  que  ce  soit  dans  ses  choix  techniques  ou  dans les sujets traités. G.D.
            </p>


