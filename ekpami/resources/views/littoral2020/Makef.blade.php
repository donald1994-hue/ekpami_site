    <div class="col-md-6 artiste-content-traduction">
            <p>Fulbert  Enagnon  Makoutodé  alias  Makef  is said to have encountered art in primary school  through  his  teacher.  He  trained  with  the  Beninese  painter  Amédée  Magou  and  the  Frenchman  François Zenner, with whom he developed a predilection for the figurative, which was not without being influenced by Magou and cubism. He indeed took over a style that is said to have been inspired by Picasso, Braque and Léger through statuary  and  African  masks.  A  new,  perhaps  more  complete  digestion of this African art, often wrongfully referred to as primitive  art.  The  «neo-cubist»  style  that  it  echoes  serves  to  show  a  Beninese  society  that  is  fragmented  but  is  see-king to rebuild itself. The partitions and compartments that appear in his canvases are not hermetic, they communicate with each other, so that the wounds generated by the des-tructuring  of  society  can  be  sutured  through  painting.  The  themes tackled by the artist are eclectic, but they touch the Beninese people’s everyday life and question the very mea-ning  of  existence,  of  life  itself.  The  drawings  in  his  series  ‘‘Mes  nuits  insomniaques’’,  My  insomniac  nights,  appear  as  snapshots of our actions that we would not necessarily have liked  to  display  in  broad  daylight.  Makef’s  images  are  as  many  mirrors  of  our  inner  being,  reminding  us  how  much  the duality that underlies us can be powerful. D.H.
            </p>
