            <p>Fulbert  Enagnon  Makoutodé  alias  Makef  aurait  rencontré  l’art  à  l’école  primaire  par  l’entremise  de  son  instituteur.  Il  se  forme  auprès  du  peintre  béninois  Amédée  Magou  et  du  français François Zenner chez qui il développe sa prédilec-tion pour le figuratif qui n’est pas sans être influencé par Magou et le cubisme. En effet, il reprend  à son compte un style qui aurait été inspiré, dit-on, à Picasso, Braque et Léger par la statuaire et les masques africains. Une nouvelle diges-tion, peut-être plus complète de cet art africain désigné très souvent à tort comme un art primitif. Le style «néo-cubiste» dont il se fait l’écho lui sert à montrer une société béninoise fragmentée  mais  qui  cherche  à  se  reconstruire.  Les  cloi-sons et les compartiments qui apparaissent dans ses toiles ne  sont  pas  hermétiques,  ils  communiquent  entre  eux,  de  sorte  que  les  blessures  générées  par  la  déstructuration  de  la  société  puissent  être  suturées  par  l’intermédiaire  de  la  peinture.  Les  thèmes  abordés  par  l’artiste  sont  éclectiques,  mais ils touchent au quotidien des Béninois et questionnent le sens même de l’existence, de la vie. Les dessins de sa série «Mes nuits insomniaques» apparaissent comme des instan-tanés de nos actions que nous n’aurions pas forcément aimé afficher au grand jour. Les images de Makef sont autant de miroirs sur notre être intérieur, qui nous rappellent combien la dualité qui nous fonde peut être prégnante. D.H.
            </p>


