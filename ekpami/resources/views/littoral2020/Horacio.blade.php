            <p>In  a  visual  arts  landscape  where  many  artists  choose  or  turn  to  abstract  art,  Horacio  Djagbessi,  who  began  his  career  with  concrete  sculpture,  favours  and  empha-sises  drawing  and  the  representation  of  reality.  For  him,  drawing  is  the  basis  of  everything,  in  art  as  in  any  human  activity. Various training courses in China and Burkina Faso have given him the technique and training courses in Benin with cultural institutions, the knowledge that allows him to inscribe  his  creations  in  a  social  approach.  He  draws  with  Indian ink and bets on the absence of colors, to reveal the drawing, the skeleton of any construction. Horacio D. belie-ves that nothing can be changed because everything already exists, «it only takes creativity to lift the veil. «He then draws man’s daily life to transform it and lead him to improve his living  conditions.  In  the  same  vein,  in  order  to  encourage  civic  commitment  among  the  younger  generation,  he  is  convinced  by  his  own  experience  of  the  need  for  artistic  training, because after two years of legal studies, it was art that allowed him to express himself. This interest in trans-mission, which materialized in the illustration of a children’s book Ariel  in  Benin  for  a  Spanish  publishing  house  trans-lated into English and French, continues through interven-tions  in  schools  and  the  creation  of  a  cultural  association  where Horacio D. combines his desire for social involvement with his pedagogical and artistic qualities. F.B
