            <p>Dans un paysage d’arts visuels où de nombreux artistes choi-sissent ou se tournent vers l’art abstrait, Horacio Djagbessi qui a débuté sa carrière par la sculpture en béton, privilégie et  met  l’accent  sur  le  dessin  et  sur  la  représentation  de  la  réalité. Le dessin est pour lui la base de tout, en art comme dans toute activité humaine. Diverses formations en Chine, au Burkina Faso, lui ont apporté la technique et des stages au  Bénin  auprès  des  institutions  culturelles,  les  connais-sances qui lui permettent d’inscrire ses créations dans une démarche  sociale.  Il  dessine  à  l’encre  de  Chine  et  mise  sur  l’absence de couleurs, pour révéler encore le dessin, ossature de toute construction. Horacio D. pense que rien ne peut être changé car tout existe déjà, « il suffit de la créativité pour lever le voile. » Il dessine alors le quotidien de l’homme pour le transformer et l’amener à améliorer ses conditions de vie. Dans  le  même  élan,  pour  susciter  l’engagement  citoyen  au  sein de la jeune génération, il est persuadé par sa propre ex-périence de la nécessité des formations artistiques car après deux années d’études juridiques, c’est l’art qui lui a permis de s’exprimer. Cet intérêt pour la transmission qui s’est matéria-lisé dans l’illustration d’un livre pour enfants Ariel au Bénin pour  une  maison  d’édition  espagnole  traduit  en  anglais  et  français, se poursuit par des interventions dans des établis-sements scolaires et par la création d’une association cultu-relle où Horacio D. conjugue son désir d’implication sociale à ses qualités pédagogiques et artistiques. F.B.
            </p>


