            <p>Valentin  Salako  aime  surprendre  l’instant  qui  se  présente  pour  capter  la  vérité  du  moment.  Il  ne  met  pas  en  scène,  il  saisit  la  réalité  telle  qu’il  la  voit.  Photos  reportage.  Son  regard ne fouille pas, il ne prend pas le sujet comme proie, il ne harcèle pas, son appareil n’affronte pas le sujet mais se pose sur lui avec une objectivité non-dénuée d’une certaine tendresse.  Ancien  dans  le  métier,  sa  profession  l’amène  à  couvrir des festivals, des défilés de mode, des événements divers  mais  son  sujet  de  prédilection  reste  le  quotidien,  la  rue,  les  routes,  les  vendeurs  ambulants,  tout  un  peuple  au  travail, en déplacement ou au repos. Sur ses photos, ses su-jets semblent heureux de se laisser « immortaliser », Valen-tin Salako choisit instinctivement des nuances dans le décor ambiant qui s’accordent à un détail, à la couleur des tissus et ses personnages deviennent partie intégrante du paysage ainsi capté. Ces images, si on les animait, un film documen-taire  prendrait  naissance.  Peu  d’images  d’intérieur,  la  vie  pour cet homme de terrain est dehors. Dehors est le terroir de  ses  plus  belles  photos.  Belles,  ce  n’est  peut  être  pas  le  mot  le  plus  juste,  les  plus  sincères,  puisqu’elles  dévoilent  l’artiste autant qu’elles montrent la vie. F.B.
