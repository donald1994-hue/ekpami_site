            <p>Valentin  Salako  likes  to  surprise  the  mo-ment  to  capture  it’s  truth.  He  doesn’t  stage,  he captures reality as he sees it. Report photos. His gaze does  not  search,  he  does  not  take  the  subject  as  prey,  he  does  not  harass,  his  camera  does  not  confront  the  subject  but  rests  on  him  with  an  objectivity  not  devoid  of  a  cer-tain tenderness. A former professional, his profession leads him  to  cover  festivals,  fashion  shows,  and  various  events,  but  his  favourite  subject  remains  the  everyday,  the  street,  roads, street vendors, a whole people at work, on the move or at rest. In his photos, his subjects seem to be happy to let themselves be ‘‘immortalized’’, Valentin Salako instinctively chooses  nuances  in  the  surrounding  scenery  that  match  a  detail,  the  colour  of  the  fabrics  and  his  characters  become  an  integral  part  of  the  landscape  thus  captured.  If  these  images were animated, a documentary film would be born. Few indoor images, life for this man of the field is outdoors. Outside is the land of his most beautiful photos. Beautiful, it is perhaps not the most accurate word, the most sincere, since they reveal the artist as much as they show life. F.B.

