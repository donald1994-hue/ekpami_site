            <p>Etienne  Aremon  is  convinced  of  the  social role an artist has. He explores the ills  that  plague  African  society  and  fervently  believes  that  art  can  bring  men  out  of  a  certain  inertia  their  condition  confines them into. In his works which find their roots in his ancestral  culture,  in  his  environment  he  uses  recuperated  objects such as: wood, wire, spikes, laterite, bone... In a syn-cretic  approach,  he  borrows  signs  from  different  religions  and  whether  he  looks  at  the  signs  of  the  FA  (geomancy  of  Yoruba  origin)  or  declines  the  steps  that  a  man  accom-plishes  in  his  existence,  he  does  it  with  the  certainty  that  man always finds a way out of his condition by putting his abilities  to  work.  On  his  canvases,  Aremon  signs  the  pact  between the tradition in which he was born and the moder-nity that he makes his own with discernment.F.B
            </p>
