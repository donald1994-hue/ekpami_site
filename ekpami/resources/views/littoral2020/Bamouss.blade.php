            <p>Basile Moussougan has a long career behind him. Graduate of the College of Art and Design of Ghana, painter, sculptor, decorator, designer, he has a great ability to handle materials. His ingenious and inventive composite works combine wood with  paint  and  various  objects,  masks,  cut  pieces  of  wood,  cowries,  are  cleverly  grafted  onto  the  surface  that  seems  to  be  waiting  for  them.  His  interventions  on  wood  that  he  sews, where he makes collages, that he paints following his cracks, his lines of force, are not aggressive, do not hurt him, on  the  contrary,  they  heal  his  wounds,  accompany  him  and  transform him into a storyteller. Bamouss nourishes his art with  his  culture,  the  elements  of  his  environment,  also  an  inexhaustible source, help him to transpose it into his pain-tings,  those  of  history  allow  him  to  talk  about  his  people,  religion, voodoo. His design furniture, other witnesses of his creative spirit, of his aesthetic research, of his desire to leave the imprint of his identity. F.B.
