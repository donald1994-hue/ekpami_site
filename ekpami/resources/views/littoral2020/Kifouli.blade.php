            <p>His  sculptural  work  focused  on  the  creation   of   the   Gélédé   masks,   ritual   masks  intended  to  be  worn  during  ceremonies  open  only  to  insiders, found its singularity in the landscape, which is never-theless  rich  in  this  material.  Since  he  was  18  years  old,  with  strength, rigour, accuracy, sense of balance, inventiveness, res-pect for traditions, Kifouli Dossou he brings out of hlan can cui, kpon-kpon,  merina  wood,  Yoruba  faces,  almond-shaped  eyes,  scarified, helmets on which he superimposes scenes of life. The fruit of years of research, in his recent work, he has sculpte life-size Gélédé. Scenes of life, tangle of activities, characters. Wo-men. Woman in front of her sewing machine, sculpture too, feet on a Go mask, sculpted in homage to the dead, young mother, child in her arms, also sitting on a Go mask. If the carved wood of the child and the Go mask has kept the same appearance, the mother, a volume in lattice wood, becomes by a technical feat, a sieve, a filter of transmission. The mythical origin of the Gélédé recalls  the  passage  of  power  in  a  matriarchal  society.  Kifouli  Dossou,  insider  interpreter  of  his  culture,  pays  homage  to  Yia  Nla, the ancestral Grandmother, all-powerful, who ensures the stability of the world and to appeases its anger, sculpts a cult that men have decided to redeem themselves for having stolen power from women. The oral heritage of Gélédé is inscribed in the intangible heritage of humanity by UNESCO. .F.B
