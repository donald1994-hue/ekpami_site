            <p>Son travail de sculpture concentré sur la réalisation des masques Gélédé, masques rituels destinés à être portés lors des cérémo-nies ouvertes seulement aux initiés, a trouvé sa singularité dans le paysage pourtant riche en la matière. Depuis ses 18 ans, avec force,  rigueur,  justesse,  sens  de  l’équilibre,  inventivité,  respect  des  traditions,  Kifouli  Dossou  sort  du  bois  du  hlan  can  cui,  du  kpon-kpon, du mérina, des visages yoruba, yeux en amande, sca-rifiés, heaumes sur lesquels il superpose de scènes de vie. Fruits des années de recherches, dans son travail récent, il sculpte des Gélédé grandeur nature. Scènes de vie, enchevêtrement d’activi-tés, personnages. Femmes. Femme devant sa machine à coudre, sculpture  elle  aussi,  pieds  posés  sur  un  masque  Go,  sculpté  en  hommage  aux  morts,  jeune  mère,  enfant  dans  ses  bras,  assise  elle aussi sur un masque Go. SI le bois sculpté de l’enfant et du masque Go a conservé la même apparence, la mère, volume en bois treillis, devient par une prouesse technique, tamis, filtre de la transmission. L’origine mythique du Gélédé rappelle le passage du pouvoir d’une société matriarcale. Kifouli Dossou, interprète initié  de  sa  culture,  rend  hommage  à  Yia  Nla,  la  Grande-Mère  ancestrale  qui  assure  la  stabilité  du  monde  et  pour  apaiser  sa  colère sculpte un culte qu’ont décidé les hommes pour se rache-ter d’avoir dérobé le pouvoir aux femmes. Le patrimoine oral du Gélédé est inscrit dans le patrimoine immatériel de l’humanité par l’UNESCO. F.B
            </p>


