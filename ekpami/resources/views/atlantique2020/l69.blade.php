            <p>Fabrice  Odjarado,  a  doctor  of  pharmacy  and  epidemiologist,  uses painting to make a chromatic incursion into the imagina-tion. The colours, a sort of visual thoughts, whose core is loca-ted in the lower half of the painting, are as if propelled by an energy that accumulates in a vertical movement, in an explo-sion that brings them together and melts them into a luminous magma. Inspired by nature, by flora, by birds, loving ‘‘what is simple  and  subtly  beautiful’’,  Phabrix  isolates  himself  early  in  the morning or late in the evening from the too concrete, relies on the colour of his ideas and renders them into pictorial emo-tions.  He  takes  the  yellow  and  red  of  yellow  and  red  things,  analyzes concordances, creates a bouquet of reality and makes it  sing.  Abstracted,  or  with  figurative  elements,  his  painting separates  itself  from  reality  to  seek  a  meeting  point,  a  focal  point, between his inner gaze and that of the viewer.F.B</p>
