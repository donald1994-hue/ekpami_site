            <p>Anathole Wankpon fait des assemblages ingénieux en bois et métal, divers outils de menuiserie ou de cuisine, petites pelles, grattoirs, écumoires, cordes, cauris... Le tout cousu, cloué, vis-sé avec du fil en cuivre parfois coloré. Dans son travail très soigné  -  il  prête  attention  à  chaque  détail,  il  n’oublie  jamais  d’insérer un morceau d’espoir : un petit bout de filet de pêche. Le filet de pêche, objet fétiche de son travail comme garant, lorsqu’il est là présent, la vie peut continuer. Ses toiles, il fait aussi  de  la  peinture,  sont  des  morceaux  de  vie  colorés.  Mor-ceaux  puisque  la  toile  plissée,  voilée,  accueille  des  collages  des cartons, tissus divers, maintes matières se croisent et s’en-trecroisent ; morceaux encore puisque symboles d’un adage, d’une  tradition,  d’un  fait,  extraits  du  vécu.  Nathaole  n’est  pas  l’artiste torturé qui, insomniaque, balance ses angoisses dans le monde, non, ses promenades dans la matière sont des pro-messes de quiétude, les ocres s’éteignent doucement sous le bleu  qui  fonce  en  traits  épais  et  révèle  en  même  temps  les  silhouettes longilignes des femmes... Sa maîtrise de la tech-nique, son harmonieux sens des couleurs ne font pas de lui un aventurier, Nathaole lance son filet d’une main sûre et dans des eaux connues. F.B
            </p>


