    <div class="col-md-6 artiste-content-traduction">
            <p>Anathole  Wankpon  makes  inge-nious  assemblies  of  wood  and  metal, various carpentry or kitchen tools,  small  shovels,  scrapers,  skim-mers,  ropes,  cowries  ...  All  sewn,  nailed,  screwed  with  copper  wire  sometimes  coloured.  In  his  very  meticulous  work  -  he  pays  attention  to  every  detail,  he  never  forgets to insert a piece of hope: a small piece of fishing net. The fishing net, the fetish object of his work as a guarantor, when it is there, life can go on. His canvases, he also paints, are colourful pieces of life. Pieces since the pleated, veiled canvas, hosts  collages  of  cardboard,  various  fabrics,  many  materials  intersect  and  intertwine;  pieces  again  since  symbols  of  an adage,  a  tradition,  a  fact,  extracted  from  the  lived.  Nathaole  is  not  the  tortured  artist  who,  insomniac,  throws  his  anguish  into the world, no, his walks into matter are promises of tran-quility,  the  ochres  slowly  fade  under  the  blue  which  darkens  into thick lines and reveals at the same time the women’s long silhouettes...  His  technique  mastery,  his  harmonious  sense  of  colours do not make him an adventurer, Nathaole casts his net with a sure hand and in known waters. F.B</p>
