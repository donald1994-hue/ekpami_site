fortunate enough to have a family in love with art, she had
their support when she wanted to enter a school of arts and
crafts. This choice allowed her to become a graphic designer
and to be autonomous. Interested in personal development,
nourished by esoteric readings and books devoted to spiritua-
lity, she continues to learn by participating in workshops and
became a mother. In order not to stop her flight, she initiated
