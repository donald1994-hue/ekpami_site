            <p >‘‘Super-realism is what I’ve always sought in my work. The
goal is to show the public the reality in front of them. To do
that, you need a good cast and papier-mâché’’. André Atsiné,
painter at his beginning, directs his work towards sculpture.
Interested in history and its transmission in rites, in 2019 he
starts a series of Caléta masks. Originally a representation of
the anger of slaves towards their master during the festivities
that took place after the harvests on the farms, the Caléta
comes from the Portuguese word ‘‘karita’’ which means grimace. This mask, intended to ridicule the master, was often
represented by hideous animals to which the master was
compared. Today, the ritual often arises at the approach of
Christmas. Avrokpo’s casts are white, or painted in royal blue,
gold or ochre. Faces prints hidden under another face, masks
with hollow eye sockets that we put on or take off as we
please, closed eyelid casts, with various expressions, they are
disturbing portraits of our undersides masks that strangely
still seem to mask us. Through portraits and installationsprocessions of faces from nowadays, the artist perpetrates
ancient rituals that necessarily irrigate the present. </p>
