            <p>«Je peins essentiellement des remèdes imaginaires aux vertus incroyablement  efficaces,  en  m’inspirant  des  principes  actifs existants ». Doto Kuassi (doto, médecin en langue fon) diagnos-tique et traite par l’art les tares de la société. Ses remèdes: ima-gination, création, expression ; leurs vertus : lecture, relecture et détournement des rites et des traditions, valorisation d’un savoir ancestral, l’herboristerie et la fito-thérapie ; les principes actifs :  parole,  écoute,  analyse.  Regard  clinique,  attentif  à  ce  qu’on  enfouit,  à  ce  qu’on  tait  car/et  ça  fait  mal,  explore  des  zones  interdites, pointe le non-dit dans la société. « Si on ne distingue aucune  représentation  imagée,  mon  art  n’est  pas  pour  autant  abstrait  car  les  idées  que  sous-tendent  mes  œuvres,  n’en  sont  pas moins concrètes ». La traversée du Mutisme est une toile structurée comme une cérémonie, tous les éléments concourent à rendre serein et grave l’envoi du non-dit dans l’au-delà, vers la lumière de la parole, l’installation La maison du docteurnous introduit de plein pied à une séance de consultation chez le médecin traditionnel. À travers des dessins, mots, formules en plusieurs langues qui envahissent les murs et récréent l’intimité de l’espace, le lieu est chargé, exutoire. A l’instar d’un homme-médecine, Harold Doto Kuassi, artiste-médecine, garanti l’équi-libre de la communauté. F.B
            </p>


