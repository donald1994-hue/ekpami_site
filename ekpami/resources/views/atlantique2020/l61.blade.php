    <div class="col-md-6 artiste-content-traduction">
            <p>‘‘I  essentially  paint  imaginary  reme-dies  with  incredibly  effective  virtues,  inspired  by  existing  active  ingredients’’.  Doto  Kuassi  (doto,  doctor  in  Fon  language)  diagnoses  and  treats  society’s  defects  through  art.  His  reme-dies:  imagination,  creation,  expression  ;  their virtues:  reading, rereading and diversion of rites and traditions, enhancement of ancestral  knowledge,  herbal  medicine  and  phytotherapy  ;  the active  principles  :  speaking,  listening,  analysis.  The  clinical  eye,  attentive  to  what  we  bury,  to  what  we  keep  silent  because  it  hurts, explores forbidden areas, points out the unspoken in so-ciety. ‘‘Even if we don’t see any pictorial representation, it doesn’t make my art abstract because the ideas behind my works are no less  concrete’’.  The  crossing  of  Mutism  is  a  canvas  structured  like  a  ceremony,  all  the  elements  contribute  to  making  serene  and serious the sending of the unsaid in the beyond, towards the word’s  light,  the  installation  The  Doctor’s  House  introduces  us  to  a  consultation  session  with  a  traditional  doctor.  Through  drawings, words, formulas in several languages that invade the walls and recreate the space’s intimacy, the place is charged, an outlet. Like a medicine man, Harold Doto Kuassi, a medicine-ar-tist, guarantees the balance of the community.  F.B</p>
