décoration, installation, collages, sérigraphie, calligraphie,
chantournage, marqueterie, trophées etc... se rencontrent sans
hésitation, sans hiérarchie. Pluridisciplinaire, Coff Eugène
Atchotin intervient dans le domaine des arts plastiques non
seulement en tant que peintre décorateur des entreprises, des
établissements publiques et privés, des lieux de cultes et de
spectacles, mais aussi comme formateur de jeunes talents qui
désirent apprendre de son savoir-faire».
