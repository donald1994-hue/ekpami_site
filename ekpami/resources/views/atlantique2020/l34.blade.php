            <p>He admires the first sculptors of Benin, Basquiat and Andy
Warhol, reads Franz Fanon. Art is his ‘‘breath’’, he is in search
of the new. Through his fresh look, through the freedom with
which he appropriates materials, mediums and space. Gael
Daavo does not feed on nostalgia, he questions the present
and what could be. Today, he is concerned with the environ-
ment: urbanism, ecosystem, wars... With ‘‘Tomiton’’ (Our City),
he imagines the new faces of african cities, convinced that a
foreign eye will not be able to read the country’s needs bet-
ter, confident in his compatriots’ skills. ‘‘AHOUANHO’’, History
of War, Aleppo-Syria, will again show his attachment to cities,
plots of humanities in danger. Attached to a cross, crucified, a
list of names, anonymous Syrian victims, next to it, the model
of the destroyed city of Aleppo: barbed wire, red stains, gaping
holes, burnt buildings, littered with corpses. Cardboard ins-
tallation - the fragility of a place is all the more striking. His
sculptures are connected to energy by braided electric wire,
in project, the spider of the electrical installations of African
countries. Gael Sankara Daavo stops his studies in civil engi-
neering and registers at INMAAC. Art echoes the thought of
Franz Fanon: If we want humanity to advance a step
farther, if we want to bring it up to a different level
than that which Europe has shown it, then we must
invent and we must make discoveries. {...} we must
turn over a new leaf, we must work out new concepts,
and try to set afoot a new man.. F.B</p>
