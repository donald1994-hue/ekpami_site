the look and the canvas, encourages the movement of sharing.
The work recharges us by contemplation, by meditation ... it
suddenly takes us to heal ourselves and allow us to regain
balance in ourselves’’. Gabin Ayanhouannon’s canvases are
poetic whirlpools with rhyming verses of colour that attract
each other as the words call each other. No ‘‘hidden flws’’, no
cracks, an airy connection between here and there, between
here and there, between here and an elsewhere without fail.
An elsewhere like a return to the beginning of the world or of
a world, pure as nature after the storm. </p>
