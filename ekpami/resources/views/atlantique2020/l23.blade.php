            <p>His vitality and inquisitive mind
            have taken him in a career spanning 30 years from painting to
            sculpture, photography and music.
            Akpinkou Aimé, who was inducted at
            the age of 10 as a priest of the vodun Loko, learned to read the
            signs of the Fa in a convent. After training at Tchogbo school
            in Nigeria, an art school centred on deities drawing, that show
            their metamorphosis, back in Benin, his meeting with a vodun
            priestess prompted him to question the concept of Fâ; not the
            abstract Fâ, reduced to an oracle, but the human-Fâ, a being
            who, like other prophets, would have had a birth, a life and a
            death. ‘‘As I have noticed that many people rely on this divinatory art to explain a certain number of facts, and that I have
            taken my time to learn more in this fild, I have started by
            having revelations and duplications of reality’’. Azebaba works
            with natural materials, cotton for his canvases and for the
            manufacture of the pigments for his oil paintings he uses a
            raw material called zin in his region, which, along with caolin, is one of the fundamental elements used in the traditions.
            Azebaba grants to his paintings healing virtues, the signs and
            symbols he paints are invested with positive energies to establish inner peace. Painting, lightpainting, music, his work has
            a mystical dimension and is deeply rooted in his culture. </p>
