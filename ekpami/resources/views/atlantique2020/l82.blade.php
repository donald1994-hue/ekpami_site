            <p>Franck Zannou, ancien tatoueur, est épris de cet art qui s’impo-sera  sur  ses  toiles  et  d’une  manière  détournée  marquera  ses  sculptures.  Ses  tableaux,  toiles  quadrillées,  urbaines,  graffitis colorés, aux dessins qui exacerbent et tourmentent les éléments du  quotidien,  tourbillon  de  mouvements,  des  scènes  surprises  sur le vif, sont des prises de contact par l’acrylique, les crayons, le  collage,  avec  le  présent.  Le  contact  avec  les  matières  natu-relles lui imposera une approche différente. Ses Vénus, femmes sans  bras  aux  allures  de  déesses,  sortent  d’un  territoire  précis,  celui d’une ancienne tradition du travail sur bois qui rencontre les lignes sculpturales du corps des femmes que le bois et son regard contient et la pointe métallique des clous de tailles di-verses qui les marquent, bois et corps. En surface, identifiables, les clous rajoutent un élément imaginaire, tordus, ils deviennent bijoux,  enfoncés,  martelés,  ils  scarifient  pour  marquer  à  vie, parures du vécu. Ces Vénus, naissent d’un socle parfois lui même une création. Une autre série de corps de femme sculptés, gran-deur  nature,  en  bois  brut,  aux  incisions  des  outils  apparentes,  sont issues de l’union de deux découpes parfois inégales où la fente  de  séparation  est  soulignée  vivement  par  le  pinceau  qui  les  relie  et  les  habille  par  petites  touches  colorées.  Des  objets  en bois, des ustensiles de tailles diverses remplissent la partie supérieure du corps laissé vide et c’est précisément ce mouve-ment tiré vers le haut qui fait leur force. Inscrit dans son temps, Zanfanhouédé nourrit son travail aussi bien à son présent qu’à des  valeurs  ancestrales  et  traditions.  Il  puise  liberté  d’expres-sion,  force,  équilibre  pour  inventer  et  réinventer  son  art  d’une  démarche qui le tatoue sans hésitation. F.B
            </p>


