    <div class="col-md-6 artiste-content-traduction">
            <p>Franck Zannou, a former tattooist, is in love with this art which will impose itself on  his  canvases  and  in  a  roundabout  way  will  mark  his  sculptures.  His  paintings,  squared  canvas,  urban,  coloured  graffiti,  with  drawings  that  exacerbate  and  torment the  elements  of  everyday  life,  whirlwind  of  movements,  sur-prise  scenes  on  the  spot,  are  contact  with  the  present  through  acrylic, pencils, collage. The contact with natural materials will impose a different approach. His Venuses, armless women who look  like  goddesses,  come  out  of  a  precise  territory,  that  of  an  ancient tradition of woodwork that meets the sculptural lines of the women’s bodies that the wood and her eyes contain and the metal point of the nails of various sizes that mark them, wood and body. On the surface, identifiable, the nails add an imaginary element, twisted, they become jewels, driven in, hammered, they scarify to mark for life, adornments of the lived. These Venuses, are born from a base which is sometimes itself a creation. Ano-ther series of sculpted women’s bodies, full-scale, in raw wood, with the incisions of visible tools, are the result of the union of two sometimes unequal cuts where the separation slit is sharply underlined  by  the  brush  that  connects  them  and  dresses  them  with  small  coloured  touches.  Wooden  objects  and  utensils  of  various sizes fill the upper part of the body left empty, and it is precisely this upward movement that gives them their strength. In keeping with his time, Zanfanhouédé nourishes his work both with  the  present  and  with  ancestral  values  and  traditions.  He  draws  freedom  of  expression,  strength,  balance  to  invent  and  reinvent his art in a way that tattoos it without hesitation.F.B
</p>
