    <div class="col-md-6 artiste-content-traduction">
            <p>‘In order to correct man’s imperfect nature, it is urgent that
education based on wisdom be used... ‘‘. An agricultural engi-
neer and sociologist, Paterne Dokou found in arts (he is also
an actor and director), the filter of a moralist vocation inspired
by his reading of nature and human nature. The materials he
chooses, the objects he uses, wood, iron, rubber, nails, jute,
nets, ropes, cowries, contribute to give his artistic proposals
meaning. Charged with the messages they carry, his sculp-
tures, which find a beautiful balance there like his paintings
their reliefs, are centred on the principles of Nature - Culture
- Tradition - Recovery, with Man, imperfect being, at the centre
of creation. Paterne Dokou who quotes Nelson Mandela with
respect: Education is the most powerful weapon we can
use to change the world, inscribes his art in what he calls
‘‘conscious realism’’. Realism because it is reality that he por-
trays, conscious because he does not imitate it, he observes it,
examines it and delivers the ripe fruit of his reading. F.B</p>
