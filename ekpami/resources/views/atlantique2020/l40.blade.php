            <p>« Il est urgent pour corriger la nature imparfaite de l’homme
que l’on fasse usage de l’éducation partant de la sagesse... ».
Ingénieur agronome, sociologue, Paterne Dokou a trouvé dans
les arts (il est également comédien et metteur en scène) le
filtre d’une vocation de moraliste inspirée de ses lectures de
la nature et de la nature humaine. Les matières qu’il choisit,
les matériaux qu’il utilise, les objets récupérés, bois, fer, caout-
chouc, clous, jute, filets, cordes, cauris, contribuent à donner
sens à ses propositions artistiques. Chargées des messages
qu’elles portent, ses sculptures qu’y trouvant un bel équilibre
comme ses tableaux leurs reliefs, sont axées sur les principes
de: Nature – Culture - Tradition – Récupération, avec l’Homme,
être imparfait, au centre de la création. Paterne Dokou qui
cite avec respect Nelson Mandela : L’éducation est l’arme
la plus puissante qu’on puisse utiliser pour chan-
ger le monde, inscrit son art dans ce qu’il appelle « réa-
lisme conscient ». Réalisme car c’est la réalité qu’il dépeint,
conscient car il ne l’imite pas, il l’observe, l’ausculte et livre
mûr le fruit de sa lecture. F.B
            </p>
