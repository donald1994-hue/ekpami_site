            <p>‘Isaac Dossou decorates schools,
companies, shows, he also gives
drawing lessons but to express his
being more deeply he must deliver his messages. To give shape
to this duty he starts to paint. In his own way. Isaac works with
natural materials: sand, charcoal, gourds, palm wood, cowries,
wood of different colours, reds, blacks, chestnuts that he
assembles by composing the images that he will then paint
and glue on plywood that he burns creating the frame of the
images that he will then pyrography. To increase the variety of
colours he inserts beads in his works. The genesis of his work
is the inspiration, he draws the image he has «seen» and makes
models that he then transposes to the painting. The subjects
he tackles are multiple, the daily life, the facts of society, spiri-
tuality, religion, astrology, everything that touches the human
being and his destiny. F.B</p>
