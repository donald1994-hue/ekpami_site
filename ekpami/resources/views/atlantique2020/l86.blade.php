            <p>Séraphin  Zounièkpé  s’est  formé  en  photographie  à  Ouidah  et en vidéo dans les ateliers organisés lors de Quintessence auprès  du  cinéaste  béninois  Jean  Odoutan.  Photographe,  vidéaste,  il  se  balade  souvent  avec  son  matériel  à  l’affût  des images insolites sur le quotidien de ses compatriotes et les  différentes  facettes  de  la  culture  béninoise.  Ces  travaux  reflètent  essentiellement  les  conditions  sociales  des  béni-nois afin d’amener les autorités du pays à se pencher sur les solutions à la lutte contre la pauvreté, une pauvreté qui est visible un peu partout au Bénin. Depuis, son parcours s’étoffe des  nombreux  prix,  expositions  et  expériences  à  travers  le  monde.  Il  acquiert  ainsi  la  reconnaissance  de  ses  photos  reportage,  des  ses  vidéos,  de  son  cinéma  documentaire  à  Beijing, Pozega, Brême, il montre son travail à Manchester, à Sao Paolo, à la Biennale de Venise. De 2009 à 2011, il est le photographe officiel du Centre culturel chinois de Cotonou. Lauréat  en  2017du  concours  mondial  La  fête  du  Printemps  lancé  par  les  centres  culturels  chinois,  il  est  freelance  pour  l’agence  chinoise  XINHUA  depuis  2016.  Depuis  quelques  années, il photographie la vie amoureuse des pigeons et des oiseaux de tout espèce et souhaite devenir le premier photo-graphe animalier africain. (adapté) 
            </p>


