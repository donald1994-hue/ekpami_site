    <div class="col-md-6 artiste-content-traduction">
            <p>Séraphin Zounièkpé trained in photography in Ouidah and in video  in  the  workshops  organized  during  Quintessence  with  the Beninese filmmaker Jean Odoutan. As a photographer and video  maker,  he  often  travels  around  with  his  equipment  on  the  lookout  for  unusual  daily  life  images  of  his  compatriots  and  the  different  facets  of  Beninese  culture.  These  works  essentially reflect the social conditions of Beninese people in order to bring the country’s authorities to seek solutions and fight against poverty, a poverty that is visible just about eve-rywhere in Benin. Since then, his career has been enriched by numerous prizes, exhibitions and experiences throughout the world.  He has gained recognition for his photo reports, videos and documentary films in Beijing, Pozega and Bremen, and has shown  his  work  in  Manchester,  Sao  Paolo  and  at  the  Venice  Biennale. From 2009 to 2011, he is the official photographer of the Chinese Cultural Centre in Cotonou. Winner in 2017 of the worldwide competition La fête du Printemps launched by the Chinese cultural centres, he is a freelance for the Chinese agency  XINHUA  since  2016.  For  the  past  few  years,  he  has  been photographing the love life of pigeons and birds of all species and hopes to become the first African wildlife photo-grapher. (adapted)
            </p>
