      <p>Adeladja uses figurative elements
    that he breaks down into geometric
    figures. The background of the painting
    is in shades of colors that connect the floor with the sky, which
    sometimes divide it and create spaces with different atmospheres. Inside the drawing’s sharp lines, he sets colors, blue
    in the circle, yellow in the triangle…and creates corners to
    pose small characters. A huge guitar behind a podium, notes
    of a musical score written in the air, symbols, man, woman,
    scales, musical instruments, black and white keys of a piano…
    Adeladja colors characters in different colors that reveal their
    diversity and brings up a sound score where these creatures
    are activated but do not seem to be the actors. Pages of a
    coloring book, scenes with the hue of a naive surrealism, have
    as protagonist perhaps music</p>
