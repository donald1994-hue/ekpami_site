      <p>Adeladja utilise des éléments figuratifs qu’il décompose en figures géométriques. Le fond de la toile se décline en dégradés
  de couleurs qui relient le sol avec le ciel, qui la divisent parfois
  et créent des espaces aux ambiances différentes. A l’intérieur
  des traits nets du dessin il pose des couleurs, du bleu dans le
  cercle, du jaune dans le triangle… et crée ainsi des recoins
  pour poser des petits personnages. Une guitare immense derrière un podium, des notes d’une partition musicale écrites
  dans les airs, des symboles, homme, femme, des échelles,
  instruments de musique, les touches noires et blanches d’un
  piano… Adéladja colorie les personnages en couleurs différentes qui révèlent leur diversité et fait surgir une partition
  sonore où ces créatures s’activent mais ne semblent pas être
  les acteurs. Pages d’un livre de coloriage, les scènes aux relents d’un surréalisme naïf, ont comme protagoniste peut être
  la musique.
  </p>
