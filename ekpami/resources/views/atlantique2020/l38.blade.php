            <p>L’œuvre de Charly Djikou est le fruit d’une passion, d’un amour
profond pour une matière qui paraît indomptable, qu’il a ap-
pris à attendrir. Dans les carrières l’artiste se laisse séduire par
la couleur de la pierre, sa texture, sa morphologie. Il s’emploie
à l’interroger, il étudie ses sons, sa réaction à l’eau, il évalue
sa brillance et ce qu’il révèlera au cours du travail. Loin de la
soumettre, il est dans les premiers temps son serviteur. L’ar-
tiste courtise. S’ensuit le dialogue avec sa promise où l’artiste
s’engage complétement, et d’un travail lent et usant, parvient
magistralement à donner l’illusion de la facilità qui cache
son combat dans la douceur des lignes qu’il offre à voir. La
matière chante : éclat des textures, souplesse des formes, fi-
nesse des traits, variations et subtilité des couleurs, la matière
est conquise. Charly Djikou a découvert et fait rayonner une
richesse locale qui n’avait jusqu’alors pas été célébrée dans
l’art du Bénin : le calcaire, le marbre et le granit des carrières
de Dassa et de Lokossa. Sont art célèbre la rencontre entre le
corps et la matière. Un mariage intemporel qui offre à l’artiste
une réponse au temps qui passe et qui tourmente celui qui a
faim de vie et ne s’imagine pas mourir. La pierre demeure, elle
survit au corps et reste longtemps après la peau. F.B
            </p>
