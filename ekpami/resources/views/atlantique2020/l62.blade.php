            <p>Son mémoire de fin d’études d’art plastique à l’INMAAC oriente la recherche de Sylvanus Loko vers l’utilisation des boîtes de conserves  dans  l’art  du  Bénin  depuis  les  années  2000.  Le  travail avec l’artiste Benjamin Deguenon lui apporte l’appro-fondissement  du  sujet,  la  connaissance  des  matières,  des  différentes  techniques,  le  goût  d’explorer  les  disciplines.  Ses  toiles sont des preuves de sa quête, peinture avec les doigts, couture, collage, récupération, utilisation des coupons des tis-sus, fils de fer, fer des boîtes de conserves. Ses personnages donnent  du  relief  et  rehaussent  les  couleurs,  introduisent  la  vie  et  ses  moments  du  quotidien  ou,  simples  silhouettes  ou  têtes  sans  corps,  pour  dénoncer  le  méfait  de  certains  rituels  qu’il entend combattre. Son intérêt pour les valeurs et les tra-ditions de sa culture dont certaines sont en train de se perdre se  révèle  dans  sa  série  sur  le  fruit  du  katin,  Ka,  la  calebasse.  Objet  incontournable  du  quotidien,  objet  utilisé  dans  la  cui-sine,  tabouret,  récipient  pour  le  bain  d’un  enfant,  mais  aussi  des rituels (il contient les graines que le prêtre Fa lance lors des  séances  de  consultation),  il  sera  peint  chez  les  peuls  et  décorera  une  table  lors  des  fêtes,  objet  symbolique  aussi,  sa  rondeur représente la fécondité, l’abondance et par extension la  femme...  il  est  supplanté  par  le  plastique  envahissant  et  pour Sylvain Loko est important de lui redonner sa place. La sculpture  est  le  médium  qu’il  a  envie  de  découvrir,  il  n’aura  aucun mal, elle est déjà là, cachée dans ses toiles. F.B
            </p>


