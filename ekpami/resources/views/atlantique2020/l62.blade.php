    <div class="col-md-6 artiste-content-traduction">
            <p>His thesis at the end of his art plastic studies  at  INMAAC  has  oriented  Syl-vanus  Loko’s  research  towards  the  use  of  cans  in  the  art  of  Benin  since  the  2000s.  Working  with  the  artist  Benjamin  Deguenon  brings  him  the  deepening  of  the  subject,  the  knowledge  of  the  materials,  the  different  tech-niques, the taste to explore disciplines. His canvases are evi-dence of his quest, finger painting, sewing, collage, recovery, use of fabric coupons, wire, iron cans. His characters give relief and enhance colours, introduce life and its everyday moments or,  simple  silhouettes  or  heads  without  bodies,  to  denounce  the misdeed of certain rituals he intends to fight. His interest in the values and traditions of his culture, some of which are being  lost,  is  revealed  in  his  series  on  the  fruit  of  the  katin,  Ka, the calabash. An unavoidable object of daily life, an object used in the kitchen, a stool, a child’s bath container, but also for  rituals  (it  contains  the  seeds  that  the  Fâ  priest  throws  during  consultation  sessions),  it  will  be  painted  among  the  Fulani  and  will  decorate  a  table  during  celebrations,  also  a  symbolic object, its roundness represents fertility, abundance and by extension the woman... it is supplanted by the invading plastic and for Sylvain Loko it is important to give it back its place. Sculpture is the medium he wants to discover, he will have no trouble, it is already there, hidden in his canvases. F.B</p>
