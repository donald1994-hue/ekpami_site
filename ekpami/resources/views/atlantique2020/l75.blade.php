            <p>« Ce qui m’intéresse, c’est de dévoiler les choses que l’on voit sans  réellement  regarder  ».  Léonard  Cosme  Totchémecheho  est  convaincu  que  l’homme  pâtit  d’avoir  brisé  son  lien  avec  la  nature,  ses  valeurs  ancestrales  s’insurgent  contre  cet  an-thropocentrisme ravageur qui sévit. Son deuxième prénom le prédisposait à être à l’écoute du bruit de fond de l’univers et, comme le disait Bigara Diop dans Souffle, à « écouter plus souvent les choses que les êtres ». Pour tenter de comprendre le monde et ses semblables, la portée de leurs actes, sa sensi-bilité s’est retrouvée un jour par un heureux hasard face à une caméra. Vidéaste, photographe, attentif à la pluralité de l’être et  ouvert  à  tous  les  moyens  d’expressions,  Totché  explore  le  champ  des  possibles  dans  des  compositions  poétiques,  intenses, lève avec tendresse un pan sur un monde qu’on croit connaître et le monde retrouve son mystère. « Dame Nature m’inspire beaucoup grâce à son rythme. Les rapports entre les choses, les êtres et la Nature font partie de mes intérêts et je crois  que  mon  travail  en  témoigne.  Ma  démarche  artistique  n’est pas dissociée de mon quotidien, c’est ce que j’expose au public tout en demeurant authentique ». Sa sagesse de dévoi-ler ce qui est dévoilable laisse l’espace ouvert à des questions auxquelles  il  ne  prétend  pas  répondre,  la  vie  est  en  mouve-ment, Totchè, dans son rythme. F.B
            </p>


    </div>
    <hr>
    <div class="col-md-6 artiste-content-traduction">
            <p>‘‘What  I’m  interested  in  is  revealing  the  things  you  see  without  really  loo-king’’.   Léonard   Cosme   Totchémecheho   is   convinced  that  man  suffers  from  having  broken  his  link  with  nature,  his  ancestral  values  are  rebelling  against  this  devas-tating anthropocentrism. His middle name predisposed him to listen to the background noise of the universe and, as Bigara Diop  said  in  Breath,  to  ‘‘listen  more  often  to  things  than  to  people’’. In an attempt to understand the world and his fellow human  beings,  the  scope  of  their  actions,  his  sensitivity  was  one  day  found  by  a  happy  coincidence  in  front  of  a  camera.  Video  maker,  photographer,  attentive  to  the  plurality  of  the  being and open to all means of expression, Totché explores the field of possibilities in poetic, intense compositions, tenderly raises a side on a world we think we know and the world finds its  mystery.  ‘‘Mother  Nature  inspires  me  a  lot  thanks  to  her  rhythm.  The  relationship  between  things,  beings  and  Nature  is part of my interests and I believe that my work reflects this. My artistic approach is not dissociated from my daily life, it is what  I  expose  to  the  public  while  remaining  authentic’’.  His  wisdom to unveil what can be unveiled leaves the space open to questions that he does not claim to answer, life is in motion, Totchè, in its rhythm. F.B
</p>
