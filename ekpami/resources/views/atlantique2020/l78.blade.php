            <p>Since   her   childhood,   Agathe   Ye-houenou,  who  was  born  and  grew  up  in  Côte  d’Ivoire,  has  never  ceased  to  immerse  herself  in  different  artistic  disciplines.  Theatre,  de-sign,  accessories  creation,  fabrics  collages.  Currently  studying  for  a  professional  art  degree  at  INMAAC,  she  is  looking  for  links between painting and model making. Agathe Yenouenou observes to positively reinvent the defects of society, inquiring about an awareness of the place of women in African society, asks  questions  about  appearance  and  self-confidence,  about art in society. Death, our origins, music, are sources of inspira-tion. Agathe communicates her vision of the phenomena that surround her through paintings such as Voyage incontournable to  evoke  death  or  Mystère  féminin  which  refers  to  what  we  don’t  know  about  women’s  personality.  Agathe  Yéhouénou  offers  us  travels.  Their  destination  remains  mysterious,  like  death, like a woman. A. Z. (adapted)
