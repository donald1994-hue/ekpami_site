    <div class="col-md-6 artiste-content-traduction">
            <p>Women are still rare in the visual arts landscape in Benin. In a social context where the role of women is still mainly confined to the home, where women are destined first and foremost to be mothers, to resign themselves  to  a  secondary  role,  Romi  has  long  since  chosen  to  paint,  which  even  for  a  man  is  an  arduous  task.  Obviously,  on  her  canvases,  she  speaks  about  the  feminine  condition.  Whether  through  like  in  his  earlier  works,  a  technique  close  to  aboriginal  painting  -  captured  here  on  the  bare  shoulders  of  the  vodun  adepts,  the  vodunsi  where  monochrome  dotted  tattoos  pearl  in  rows  from  the  top  of  their  shoulders  to  their  arms and chest - or today in a a purified figurative, reduced to a few strokes and drips of paint, or through abstract where she inserts collages, Romi shows her sensitivity to the feminine condition she describes in order to make it evolve. That doesn’t mean  that  her  canvases  are  grey  because  of  it,  from  her  play  of  colours,  red,  blue,  green,  white,  yellow,  from  her  search  for  harmony,  hope  springs  forth  :  life  always  offers  alternatives  that one only has to know how to recognize. F.B</p>
