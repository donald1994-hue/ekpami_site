            <p>Les femmes sont encore rares dans le paysage des arts visuels au Bénin. Dans un contexte social où le rôle de la femme est encore principalement confiné au foyer, où la femme est des-tinée  avant  tout  à  être  mère,  à  se  résigner  à  un  rôle  second,  Romi a choisi depuis longtemps de peindre. Ce qui même pour un  homme  est  tache  ardue.  Evidemment,  sur  ses  toiles,  elle  parle  de  la  condition  féminine.  Que  ce  soit  comme  dans  ses  travaux  antérieurs,  par  une  technique  proche  de  la  peinture  aborigène - saisie ici sur les épaules dénudées des adeptes du vodoun, les vodunsi où tatouages en pointillés monochromes perlent en rangées de hauts de leurs épaules sur leurs bras et poitrine - ou comme aujourd’hui, par un figuratif épuré, réduit à quelques traits et coulées de peintures ou alors par de l’abs-trait  où  elle  insère  des  collages,  Romi  montre  sa  sensibilité  face à la condition féminine qu’elle décrit pour la faire évoluer. Ses toiles ne sont pas grises pour autant, de son jeu de cou-leurs, rouges, bleues, vertes, blanches, jaunes, de sa recherche de l’harmonie, jaillit l’espoir : la vie propose toujours des alter-natives qu’il faut seulement savoir reconnaître. F.B
