            <p>During  his  studies  for  a  professio-nal  degree  in  plastic  arts  at  INMAAC,  Michael    Arthur    Ayite    met    the    artist    Sebastian  Boko  with  whom  he  was  going  to  ‘‘immerse  him-self’’ in woodworking techniques. His fascination for masks is reflected in both his sculpture and his painting. He carves in wood anthropomorphic masks, but also stylised figures, busts with  women’s  bodies  as  models  for  his  observations  on  the  fashion’s influence and impact on young people’s behaviour. The precisely and finely carved shape of the wood, repainted and  varnished,  will  bring  out  a  head  all  the  more  limpid  in  its lines as it has no face, a few notches around the neck as ornaments  and  a  single  piece  of  jewellery,  a  manufactured  object  which,  far  from  being  a  simple  coquettish  accessory,  will be the ultimate sovereign gesture that it will affix like a signature. The shell will seduce him by its fragility and he will use it in the realization of his ‘‘portraits’’ of masks. Whole or in pieces, glued in half or in part, serving as a canopy to shelter a smaller or painted shell, a sought-after contrast of a colour that it is close to in a constant search for harmony. In his pain-ting, on the pure flatness of the pastel colours, clothing ele-ments, logos of well-known brands display our desires. Wor-king with a material that was once alive, resurrecting it under a  new  appearance,  returning  to  ancient  sources,  MichArthur  invokes and summons at the same time the subtle traces of cultural values of the past into the future he projects. F.B</p>
