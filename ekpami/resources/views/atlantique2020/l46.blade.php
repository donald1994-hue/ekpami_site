            <p>The particularity of Camille
Ganon’s work is the use of a semi-
aquatic plant, gbakpo. He draws the
subject on the plywood, then takes the
drawing on the canvas where he will glue the plants he has
sculpted and that he will paint. On the black background of
the canvas emerges his meticulous, careful, delicate marquetry
work where he transcribes with many details, scenes of life.
‘‘My intranquility to capture everything and nothing leads
me to focus my work on women as a source of life, history,
traditions and human existence. The particularity of my work
is based on a mix of sculpture, collage and acrylic painting.
This quest for innovation leads me to sculpt gbakpo, to paint
nothingness with acrylic, to glue one by one the plants, to give
life to the material. I remember that a ‘‘knower’’ of the field had
discouraged me on this new site. But know that ‘‘What you are
refused to undertake is often the best of your existence’’. F.B</p>
