pas tout exprimer. C’est ce qui m’amène à utiliser le canal ar-
tistique à travers la peinture pour faire sortir les émotions, les
non-dits et l’expression du moi intérieur qui souvent ont été
étouffés par l’éducation et l’école. Notre société ne laisse pas
trop de place à l’expression spontanée. Mon premier projet a
été d’écrire les mathématiques en bande dessinée pour lais-
ser aux apprenants une approche ludique tout en gardant les
fondements de la théorie mathématique. Cette soif du partage
m’a aussitôt conduite à créer un atelier mobile créatif avec les
enfants de 6 à 12 ans. J’utilise les fondements de la science et
des mathématiques pour m’exprimer de façon ludique à tra-
vers l’expression artistique, notamment la peinture. J’utilise les
formes géométriques, la symétrie axiale, la symétrie centrale,
la projection orthogonale, le fractale pour donner forme à mes
créations ». Claudine Dan peint les mathématiques, formule
sa joie de vivre, avec une intensité transparente. Son énergie
de vie qui se déploie « sur toute matière » se tourne aussi
naturellement vers des meubles de famille anciens auxquels
elle restitue une nouvelle grâce avec délicatesse et sensibilité.
Ces tabourets, tables, chaises, objets utilitaires des vies parfois
disparues, deviennent dans ses mains joyeuses des liens colo-
rés qui rayonnent d’une vie nouvelle dans d’autres vies. F.B
