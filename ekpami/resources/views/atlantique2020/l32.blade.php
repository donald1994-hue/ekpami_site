            <p>‘‘From mathematics to painting,
my vision is to put on canvas the
societal problems of the soul’s cé-
léste. Words cannot say everything, cannot
express everything. This is what leads me to use the artistic
channel through painting to bring out the emotions, the un-
said and the expression of the inner self that often have been
stifled by education and school. Our society does not leave
too much space for spontaneous expression. My first project
was to write mathematics in comics to give learners a playful
approach while keeping the foundations of mathematical
theory. This thirst for sharing immediately led me to create
a creative mobile workshop with 6 to 12 years old childred.
I use the foundations of science and mathematics to express
myself in a playful way through artistic expression, especially
painting. I use geometric shapes, axial symmetry, central
symmetry, orthogonal projection, fractal to give shape to my
creations’’. Claudine Dan paints mathematics, formulates her
joie de vivre, with transparent intensity. Her energy of life that
unfolds ‘‘on any material’’ also naturally turns to antique fami-
ly furniture to which she restores a new grace with delicacy
and sensitivity. These stools, tables, chairs, utilitarian objects
of life that have sometimes disappeared, become in her joyful
hands coloured links that radiate new life into other lives. F.B</p>
