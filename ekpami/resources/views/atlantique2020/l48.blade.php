    <div class="col-md-6 artiste-content-traduction">
            <p>Art technician specialising in gra-phic  design,  bachelor’s  degree  in  art  history,  Ulrich  Gbaguidi  through  associative  actions,  is  interested  in  the  future  of  youth,  the  inclusion  of  vulnerable  people  through  theatre  and  plastic  art.  As  a  painter,  he  is  also  attracted  by  recovery and starts making sculpture. Recycling, because ‘‘the materials that come out of the factory have no history. Those that  come  from  recovery,  already  have  the  necessary  infor-mative  legacy  to  consolidate  and  preserve  the  new  history.’’  He makes anthropomorphic masks, assemblages on which he paints characters following the wood lines, bringing together materials,  wood  meets  iron,  plastic,  in  the  image  of  a  chan-ging  society  that  must  preserve  its  roots.  The  injustices  and  discriminations, the rights of the most fragile, themes that he explores because ‘‘the world would be better if the individual would  ask  himself  the  right  questions’’.  His  paintings  reveal  his view of current events, of daily life, but also of the lives of personalities who have made their voices heard and thus gi-ven a voice to Africa, such as Miriam Makeba, on the underva-lued contribution of the black continent to the world. Writings also  as  messages,  quotations,  symbols,  arrows,  spirals,  faces  and silhouettes, on canvases where he lets colours guide his emotions. Life is a one-way street for Ulrich Gbaguidi, he com-poses with the present to participate in a renewal that makes sense. F.B</p>
