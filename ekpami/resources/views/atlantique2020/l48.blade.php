              à  travers  des  actions  associatives  du  devenir  de  la  jeunesse, 
               à  l’inclusion  des  personnes  vulnérables  par  le  biais  du  théâtre
                 et  de  l’art  plastique.  Peintre,  il  est  attiré  aussi  par  la 
                  récupération  et  commence  à  faire  de  la  sculpture.  La  récupération, 
                   car  «  les  matériaux  sorties  d’usine  n’ont  aucune  histoire.  Ceux 
                    qui  proviennent  de  la  récupération,  ont  déjà  le  lot  d’héritage 
                     informatif nécessaire à la consolidation et conservation de la nouvelle 
                     histoire ». Il réalise des masques anthropomorphes, des  assemblages  sur 
                      lesquels  il  peint  des  personnages  sui-vant  les  lignes  du  bois 
                       faisant  se  rencontrer  les  matières,  le  bois avec le fer, avec le 
                       plastique, à l’image d’une société en mutation  qui  doit  conserver 
                        ses  racines.  Les  injustices  et  les  discriminations,  les  droits 
                         des  plus  fragiles,  des  thèmes  qu’il  explore  car  «  le  monde  
                         se  porterait  mieux  si  l’individu  se  poserait les bonnes questions »
                         . Ses toiles révèlent son regard sur l’actualité, sur le quotidien mais
                          aussi sur la vie des per-sonnalités qui ont fait entendre leur voix et 
                          donné par là une voix à l’Afrique, comme Miriam Makeba, sur l’apport pas assez souvent mis en valeur du continent noir au monde. Des écri-tures aussi en guise de messages, citations, symboles, flèches, spirales,  visages  et  silhouettes,  sur  des  toiles  où  il  laisse  les  couleurs  guider  ses  émotions.  La  vie  est  à  sens  unique  pour  Ulrich  Gbaguidi,  il  compose  avec  le  présent  pour  participer  d’un renouveau qui fait sens. F.B
