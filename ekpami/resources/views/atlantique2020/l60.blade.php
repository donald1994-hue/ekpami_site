            <p>Didier  Kpassassi  is  a  Beninese  pho-tographer  committed  to  promoting  the artistic and photographic heritage of his country, president of the Associa-tion  of  Art  Photographers  of  Benin  (APAB)  and  organizer  of  the  annual  Quinzaine  de  la  Photographie  since  2013.  This  desire  to  preserve  the  memory  of  Beninese  culture and identity was already reflected in one of his pre-vious  photographic  series:  empathetic  portraits  in  which  the  people  photographed  reveal  the  beauty  of  traditional  hairs-tyles.  With  the  series  Children  on  Lake  Ganvie,  Didier  invites  the viewer to take a luminous walk on this magnificent lake, ‘‘Venice of Africa’’, in contrast to the harsh reality of child labour before and after school. The strength of his photographs stems from  his  ability  to  avoid  self-pity  and  misery  by  portraying  this sublime landscape. Didier chooses to highlight and stage the dignity, strength and courage of these children with their confident, determined looks. The light emanating from these photographs magnifies them. In this series Didier Kpassasi im-poses  himself  as  a  sensitive  photoreporter  deploying  around  his  theme  a  broader  vision  of  the  environment  of  Ganvié,  its  landscapes, its boats, a certain daily life of women.S.C.</p>
