            <p >As a photographer and multimedia artist, Ishola Akpo explores the possibilities of digital technology, while mixing
            modernity and tradition in his work, playing with different
            levels of reading so as to create plural metaphors. The frontier between reality and fiction, fixed and multiple identities, remains at the heart of his approach. In 2013, he was
            awarded the ’’Visa pour la création’’ (Institut Français, Paris)
            and presented the series Pas de flash s’il vous plaît !, a reflection on the interaction of light on the photographed subjects,
            presented in the form of a performance and exhibition at
            the Institut Français of Cotonou. In 2014, he publishes the
            series L’essentiel est invisible pour les yeux (Africa Is No Island, MACAAL, 1:54 fair in Morocco, 2018), based on a family
            experience, which illustrates his grandmother’s dowry, while
            insisting on the burden of her memory. This reflection will
            lead him to explore contemporary marriage. In 2015, with the
            series “Les mariés de notre époque”, Ishola Akpo, Photoquai
            Laureate, enters the collection of the Musée du Quai Branly
            in Paris. Since then, he has undertaken numerous artistic residencies: the Montresso Foundation in Morocco with the Daïbi
            series and more recently the Zinsou Foundation, Benin with
            Agbara Women presented at the Ouidah Museum as an ode
            to the power of women, embodied with portraits of famous
            or forgotten queens of African history. His work has been
            selected in several major international events including the
            Weltkulturenmuseum in Frankfurt, Germany, Fotonoviembre
            in Tenerife, Spain, Nuit blanche de Port-au-Prince, Haiti, Lagosphotos Festival, Nigeria, Afreaka Festival in Brazil.
            Ishola Akpo lives and works in Benin. </p>
