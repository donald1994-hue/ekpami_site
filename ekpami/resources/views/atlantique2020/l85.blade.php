            <p>Gratien Zossou was born in Porto Novo. As a painter, actor and writer, he goes throught life with the ardour and abandon of a condemned man, as if each act xere to be his last  .  ‘‘I  throw  myself  into  life,  sometimes  with  words,  some-times with paintings, and sometimes with film’’. In his make-shift  studio,  he  paints  with  his  hands,  urgently,  needfuly,  for  his children’s sake, he says. After a long journey, through arti-ficial mirages, Les Fenêtres, The Windows, series, marks a new departure  for  him.  This  is  a  collection  of  windows  recovered  from houses in ruins to which he gives a new lease, existing by themselves independently. A rebirth. A second chance, like for everrything  here  in  Benin.  He  calls  these  windows  Prisoners.  Prisoners of they memories, Prisoners of conscience, One out of two prisoners. But is his painting there is allways a hurricane lamp, a sun, a torch, keys, sources of a light of hope, forcing the hand of fate, so that will no longer be prisoner of his memories and of his errors. The ambivalence of two lives, where past and future mingle and clash. And the present ? Gratien smiles with a dreamy gaze. After some difficult times and years of wande-ring, Gratien Zossou would like to add:  ‘‘Today I’m painting. And then there’s my play. I have to put it on. I have to’’. N.F. ‘‘But today, I paint to bear witness to God for having given me the grace of my spiritual and professional resurrection. ‘‘
