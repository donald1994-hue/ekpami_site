    <div class="col-md-6 artiste-content-traduction">
            <p>Ponce  Zannou’s  canvases,  palimpsests  that  integrate  the  weight  of  matter,  the  intense  vibration  of  colours  (pictorial  notes  of  a  symphony  of  balance),  the  precision  of  the  line.  The  telluric  force  that  emerges  is  certainly  the  source  of  its powerful visual impact. He structures his canvases with multiple overlapping layers in a merciless succession for the eye. Beyond the doubt that he inflicts, what did we see ? did we see it all ? have we seen  enough  ?  a  sensual  materiality  aspires  the  gaze  that  buries itself captured by the pictorial field in order to collect impressions, to recognize characters here and there, to make its way through the density of the work. It is not easy to penetrate the intimacy of a work which is also that of its creator - each gesture, each touch, each line, each  relief  and  the  different  materials  that  he  ages  and  metamor-phoses to prepare the canvas, as much as the exposures - the work requires, says the artist, a calm and serene state of mind that allows us  to  apprehend  it.  Pontius  Zannou  breaks  his  universe  down  into  strata, deflagration of images that he renders abstract, as many pos-sible  reading  promises.  Some  paintings,  Rorschach’s  tests,  decipher  the personality of the world, others, impressionist reconstructions of places or of the moment. His gaze, which lingers on vodun altars, on aged objects, on nature, reflects his reading of the world, his desire to participate in its evolution. The strength of his art is not to ascend to heaven  but  to  recognize  the  fertile  seed  in  the  depths  of  the  earth  and make it grow.F.B (adapted)
</p>
