            <p>Les  toiles  de  Ponce  Zannou,  palimpsestes  qui  intègrent  la  pesanteur  de  la  matière,  l’intense  vibration  des  couleurs  (notes  picturales  d’une  symphonie  de  l’équilibre),  la  précision  du  trait.  La force tellurique qui se dégage est certainement à l’origine de son puissant impact visuel. Il structure ses toiles par des couches multiples  qui  se  chevauchent  dans  une  succession  impitoyable  pour le regard. Au delà du doute qu’il inflige, qu’à-t-on vu ? a-t-on tout vu ? a-t-on assez vu ? une matérialité sensuelle aspire le regard qui s’enfouit capturé par le champ pictural pour recueillir des impressions, reconnaître ci et là des personnages, se frayer un passage dans la densité de l’œuvre. On ne pénètre pas aisément dans l’intimité d’une œuvre qui est aussi celle de son créateur - chaque geste, chaque touche, chaque trait, chaque relief et les dif-férentes matières qu’il vieillit et métamorphose pour préparer la toile, autant des mises à nu - l’œuvre requiert, dit l’artiste, un état d’esprit  apaisé,  serein  qui  seul  permet  de  l’appréhender.  Ponce  Zannou décompose son univers en strates, déflagration d’images qu’il  restitue  abstraites,  autant  des  promesses  de  lecture.  Cer-tains tableaux, tests de Rorschach, décryptent la personnalité du monde,  d’autres,  des  reconstitutions  impressionnistes  des  lieux  ou de l’instant. Son regard qui s’attarde sur des autels vodun, sur des objets vieillis, sur la nature, traduit sa lecture du monde, son désir de participer à son évolution. La force de son art n’est pas de se hisser vers les cieux mais de reconnaître la semence fertile au tréfonds de la terre et la faire germer.F.B (adapté)
            </p>


