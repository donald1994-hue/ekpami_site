      <p>Georges Adéagbo is a conceptual artist who has been developing his own style of using found objects since the early
1970s in Cotonou, Benin. When out on walks he gathers items
that have been lost or thrown away, and incorporates them
into his installations. Episodes from his personal past interface with unusual interpretations of so-called objective historical scholarship, mainstream pop culture is juxtaposed with
canonized high culture, and the banal confronts the profound
in his works. Adéagbo avoids overly obvious interpretations of
his work: ambiguity and entrapment are integral to his strategy of provocation.The artist also deliberately avoids those
elements and facets expected of ‘‘African’’ artists – in some
cases even presenting these expectations in a satirical light.
Adéagbo’s works key into the process of coming to terms with
the colonial era, explore the imperialistic claims of Western
powers in mainland Africa, and pose questions about traces of
colonialism still visible in European cities. As an African ethnologist, Adéagbo studies the customs of each place at which
he exhibits, and casts these as glaring clichés– a parallel to
the way his culture is often misrepresented.
Adéagbo has been present at many major events : 2002 Documenta 11, Kassel, 2008 Palazzo Vecchio Florenz, 53rd Venice
Biennial, 2012 Paris Triennial, 2014 Gallery Wien, Berlin, 2016
Israel Museum, Jerusalem, UNO Palace of Nations, Maison
Tavel – Geneva, 2019 Aby Warburg Haus, Hamburg. His works
can be found in important collections, for example: Centre
Pompidou-Paris, Museum Ludwig Cologne, Philadelphia
Museum of Art, Toyota City Museum, the Contemporary Art
Collection of the Federal Republic of Germany</p>
