            <p>‘‘Each  of  my  paintings  is  an  expe-riment,  a  self-projection  to  which  I  add  the  energy  I  introduce.  To  trans-pose  my  metaphysical  thought  and  the  inspiration sometimes coming from dreams, as I felt constrai-ned  by  realistic  painting,  I  chose  symbolism  to  contextualize  them. Based on symbols and other elements carrying meaning and  energy  that  are  a  matter  of  intention  and  pure  potentia-lity,  it  straddles  geometry  and  sometimes  dabbles  in  realism  to translate an idea, a philosophical thought. For me, the com-bination  of  mathematics  and  painting  gives  something  won-derful to the colour and composition’s language. The geome-tric forms merge into symbols, are harmoniously familiar and communicate and appeal to the imagination. In this adventure, I explore the language of shapes and colours in its pure poten-tiality in the service of the being’s balance’’. Wilfried Akotangni, lawyer, graduate in private law, self-taught painter, worked his technique with the painter Parfait Gbogbé. Influenced by the paintings  of  Philippe  Abayi  and  Dieudonné  Otenia,  an  assi-duous  reader  of  art  books,  he  seeks  his  way  through  various  disciplines but with only one point of reference, artistic expres-sion  :  theatre  and  cinema  decorator,  dramaturgy,  storytelling,  comedy,  graphic  design,  painting.  His  research  is  currently  focused on sewing, which he would like to link to mathematics and his art through the creation of a fashion house. An ‘‘Iden-titude’’ show in Belgium will serve as a trigger for Willy, who is committed to the promotion of citizen engagement and the defence of literary and artistic property.F.B
