            <p>Pour Sagbo Missihoun l’environnement et la nature sont la source d’inspiration permanente d’une sorte d’arte povera.La sculpture « un art de la soustraction, la peinture d’addition ». Le bois est sa matière privilégiée, allé jusqu’à construire de ses mains  une  cabane  artisanale,  écologique,  on  sent  son  expres-sion se libérer lorsqu’il cherche à faire sortir des formes anthro-pomorphes  composant  avec  les  nœuds,  érigeant  des  formes  totémiques, en utilisant les racines, les gardant brutes, au plus proche de la nature. Dans la peinture, il expérimente le collage des plantes médicinales pour réaliser des « toiles magiques » et des matériaux de récupération tout en ne choisissant que ce qui vient  à  lui  «  naturellement  ».  C’est  jeune  enfant,  en  observant  le travail de son père qu’il a pris goût aux arts qu’il a dévelop-pés par la suite à travers des formations et ateliers. Son travail est  pluridisciplinaire  et  les  matières  qu’il  utilise,  argile,  plâtre,  papier,  tissu,  de  récupération.  Décorateur  de  places  publiques,  églises, hôtels, il est aussi formateur à son tour au centre pour jeunes Banyuwange de Akassato et co-fondateur du centre d’art plastique Sam-Ba City. La recherche de Saman est orientée vers la restauration d’une harmonie où l’homme participe d‘un ordre qui doit nécessairement garder son équilibre. F.B
