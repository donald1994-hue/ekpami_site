            <p>For Sagbo Missihoun the envi-ronment and nature are the per-manent source of inspiration for a kind of arte povera.Sculpture‘‘an  art  of  subtraction,  painting  of  addition’’.  Wood  is  his  privileged  material, going so far as to build with his hands an artisanal, ecological  hut,  one  feels  his  expression  freeing  itself  when  he tries to bring out anthropomorphic forms composing with knots, erecting totemic forms, using the roots, keeping them raw, as close as possible to nature. In painting, he experiments with  the  collage  of  medicinal  plants  to  create  ‘‘magic  can-vases’’  and  recycled  materials,  choosing  only  what  comes  to  him ‘‘naturally’’. As a young child, while observing his father’s work, he developed a taste for the arts which he later deve-loped  through  training  and  workshops.  His  work  is  multidis-ciplinary and the materials he uses, clay, plaster, paper, fabric, recovery.  Decorator  of  public  squares,  churches,  hotels,  he  is  also a trainer in his turn at the Banyuwange youth centre in Akassato and co-founder of the Sam-Ba City plastic art centre. Saman’s research is oriented towards the restoration of a har-mony where man participates in an order that must necessa-rily keep its balance. F.B
