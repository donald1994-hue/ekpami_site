      <p>Art of textile, history, history of
art, drawing, photography, video, as
a base, installation and fimic research to
waewe links between the techniques, an intense journey that
brought Eliane Aisso for two years to the Fresnoy, the prestigious Studio of Contemporary Arts in Turcoing, France.
The young artist took the risk of getting caught up with another culture, found the audacity to confront new techniques, to
bet on encounters and to ‘‘make the artist in me evolve’’. If in
photography it she does not see ‘‘the possibility of documentation’’ but a pictorial space to bring back or at least, call what
is no longer, installations and cinema are bridges between
mediums to explore other bridges, those fragile, mysterious,
deep, present in all cultures, between the here that we see
and the invisible elsewhere, between the disappeared and
what remains in us of their memory, these links that are invented to retain them. Ghost places that Eliane Aisso invents
and interrogates, architectures in narratives, in fashioned
objects, triggers of memories, in portraits - photos of ancient
times superimposed on current landscapes -, missing links
between our ‘‘revenants’’ and forgetfulness, are reincarnated
in what of the imaginary is the root of the future.</p>
