            <p>Four years at the School of Arts and Crafts, ESMA, a professio-nal  degree  in  art  at  INMAAC,  a  fashion  illustrator,  training  in  textile  art,  Hélène  Mehinto  likes  to  explore  materials  as  she  likes to explore the palette of colors, the nature, the feminine condition.  To  gather  the  mediums  she  loves,  she  works  on  the background of her canvases ‘‘in order to bring out textile motifs’’.  Landscapes,  portraits,  some  paintings  on  the  edge  of  abstraction, all marked by threads of colour that flow over the canvas and function as a screen between the subject and the viewer  adding  a  distance  that  intrigues,  that  destabilizes  the  figurative and makes it uncertain. Hélène Mehinto seems to have the intuition of a reality sensitive to inner movements. F.B</p>
