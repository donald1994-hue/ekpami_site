            <p>Engineer, painter, a professional
degree in plastic arts at INMAAC,
Ghislain Bidossessi Fadohan is in
continuous training, fully committed to
deepening his artistic writing. In his recent
work he is interested in the cauri, the shell found on the sea
bed. Used in the past as an exchange currency, its symbolic
value has made its presence felt in West Africa ever since. Offe-
ring rituals, tool of divinatory practices, symbol of fertility, good
luck charm, ornament, decoration of sacred masks, ceremonial
costumes, jewellery, it is also a symbol of a long resistance to
colonial conquest, to another currency, foreign. The iron sculp-
tures of Fadohan, anthropomorphic forms dressed with fabrics
on which he glues the cowries, painted them, assembling them
on iroko wood scraps - a cultural and cultic element (tree for
the veneration of the god of wealth and abundance, Dan, cere-
monies), the masks, which integrate the assin, Potable altars in
the fons, the atikpavi, commemorative statuettes representing
twins, in a frame sculpted according to the divinatory trays
design, are the fruit of his research on materials, of learning
new techniques, of meticulous work, of his attachment to his
culture. Fadohan’s sculptures, expressions of the secret world
of anonymous beings steeped in history, tell the stories that he
extracts with the cauri from the depths of his culture ‘‘to carry
away messages about resilience’’. F.B</p>
