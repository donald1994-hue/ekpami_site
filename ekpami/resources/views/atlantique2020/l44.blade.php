de son écriture artistique. Dans son travail récent il s’intéresse
au cauri, ce coquillage qu’on trouve dans les fonds marins.
Utilisé antan comme monnaie d’échange, dès par sa valeur
symbolique, sa présence dans l’Afrique de l’Ouest perdure.
Offrande des rituels, outil des pratiques divinatoires, symbole
de fertilité, porte-bonheur, ornement, décoration des masques
sacrés, des costumes de cérémonie, des bijoux, il est aussi
symbole d’une longue résistance à la conquête coloniale, à
une autre monnaie, étrangère. Les sculptures en fer de Fado-
han, formes anthropomorphes habillées de tissus sur lesquels
il colle les cauris, les peint, les assemblages sur des chutes de
bois d’iroko - élément cultuel et culturel (arbre pour les céré-
monies de vénération du dieu de la richesse et de l’abondance
Dan), les masques, qui intègrent les assin, autels portatifs chez
les fons, les atikpavi, statuettes commémoratives réprésen-
tant des jumeaux, dans un cadre sculpté selon le dessin des
plateaux divinatoires, sont le fruit de ses recherches sur les
matières, d’apprentissage des nouvelles techniques, d’un tra-
vail minutieux, de son attachement à sa culture. Les sculptures
de Fadohan, expressions du monde secret des êtres anonymes
chargés d’histoire, racontent les histoires qu’il extrait avec le
cauri du tréfonds de sa culture « pour porter loin des mes-
