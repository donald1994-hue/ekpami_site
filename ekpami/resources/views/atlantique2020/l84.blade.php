            <p>L’œuvre  peint  de  Zinkpé  met  en  scène  de  manière  récurrente  des  personnages  d’apparences  un  peu  folle  conçus  au  gré  de  ses fameux dessins secrets. Des scènes de genre, des batailles, et  même  des  scènes  «  galantes  »  prennent  forme  peu  à  peu  et  donnent  à  ces  dieux  de  mondes  étranges  un  coté  presque  humain. Il se touchent, se parlent, se chamaillent, s’accouplent, et voilà qu’enfin ils nous ressemblent. La peinture devient alors un  miroir  du  monde,  mais  un  miroir  déformant,  grimaçant  à  souhait,  fumant  comme  un  miroir  aztèque.  Point  de  hasard  à  la  surface  de  la  toile,  on  se  frotte,  on  s’aime,  ou  se  dévore  dans  l’espace  d’une  magie  vivante  et  chaude  comme  le  corps  d’une femme. Les visages sont grimaçants et vivants, il y a de l’Ensor  dans  l’œuvre.    Bien  plus  cérébrales,  ses  installations  sont  à  la  mesure  d’une  véritable  rigueur  africaine.  Quand  les  ibejis pullulent à foison, ils cessent étrangement d’être le sujet de l’œuvre. Celle-ci devient inéluctablement une sorte d’autel anthropomorphe,  issu  de  l’homme  pour  revenir  à  l’homme  en  faisant  un  petit  crochet  par  les  territoires  les  plus  sacrés,  comme  un  inévitable  détour  dans  l’ordre  du  divin.  Il  n’est  pas  impossible qu’en d’autres temps, sous d’autres cieux, Zinkpé eut été brûlé vif sans autre forme de procès. Mais l’artiste démiurge d’aujourd’hui n’a d’autre choix que d’être son propre bourreau. Christ autoproclamé et Christ auto-crucifié car il faut bien ga-gner sa résurrection et sa vie éternelle ! B.D. (extraits)
            </p>


    </div>
