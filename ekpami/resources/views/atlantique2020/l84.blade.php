    <div class="col-md-6 artiste-content-traduction">
            <p>Zinkpé’s  painted  work  recurrently  fea-tures characters who appearances seems a little  crazy,  conceived  according  to  his  famous  secret  drawings.  Genre  scenes,  battles,  and  even  ‘‘gallant’’  scenes take shape little by little and give these gods of strange worlds  an  almost  human  side.  They  touch  each  other,  talk  to  each  other,  squabble,  mate,  and  at  last  they  look  like  us.  The  painting then becomes a mirror of the world, but a deforming mirror, grimacing at will, smoking like an Aztec mirror. There is no chance on the surface of the canvas, we rub, love or devour each other in the space of a living magic, as warm as a woman’s body.The  faces  are  grimacing  and  alive,  there  is  some  Ensor  in  the  work.    Much  more  cerebral,  his  installations  are  the  measure of a true African rigour. When the ibejis abound, they strangely cease to be the subject of the work. The work inevi-tably  becomes  a  kind  of  anthropomorphic  altar,  coming  from  Man  to  return  to  Man  by  making  a  small  detour  through  the  most sacred territories, like an inevitable detour into the order of  the  divine.  It  is  not  impossible  that  in  other  times,  under  other skies, Zinkpé would have been burned alive without any other  form  of  trial.  But  today’s  demiurge  artist  has  no  choice  but to be his own executioner. Self-proclaimed Christ and self-crucified Christ for we must earn his resurrection and eternal life! B.D. (excerpts)
            </p>
