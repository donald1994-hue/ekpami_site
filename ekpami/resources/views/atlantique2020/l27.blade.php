            <p>Camille Boko made a stint in the
fashion world and trained as a stage
and film set designer with the artist Laudamus Segbo, where
he acquired a taste for choreography and an interest in the
body as a medium. Hunter, archer without arrow, in his hands
a piece of dry wood, reincarnated spirit floating on the river,
Styx between here and elsewhere, bearer of offerings to the
missing twins, ibedji, never dead, always gone to the forest. His
photos are always taken early in the morning, always in nature,
naked body, a loincloth covers his hips, a few painted signs,
white on the bust, on the face, bracelets, necklaces of pearls,
seeds but also plastic braided threads, brush in the hair, amu-
lets of yesterday and today. He takes pictures of himself and
revisits religious beliefs and practices, becoming an interpreter
or smuggler.</p>
