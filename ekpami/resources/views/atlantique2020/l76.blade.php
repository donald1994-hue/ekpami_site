            <p>He  admits  that  he  does  not  know  himself,  but  ‘‘discovers  himself  in  his  creations  because  the  traits  of  his  persona-lity  are  perceptible  in  his  concept  of  identity’’.  The  people  he paints each have ‘‘different habits, but are bound to live together and collaborate’’. The string that used to run like a leitmotif through his old paintings and allowed him to trace the  outline  of  the  figures  is  now  conceptual. A  necessary mental thread connects Lionel Yamadjako’s faceless charac-ters, portraits whose anonymity replaces the various identi-ties that complement each other with the ‘‘richness of their hopes, passions, values’’ and, most beautifully, ‘‘their inade-quacies’ ’. The series of portraits he offers reveal a confident technique,  acquired  during  workshops  and  residencies  in  Benin  and  the  neighbouring  country  of  Nigeria,  collages  and drawings. His research is based on his landscape, men-tal, emotional or environmental, and gives us his daily life, his  history,  his  awareness.  Yamadjako  prepares  the  canvas  before any clearly defined idea, waiting for a kind of connec-tion which, once established, the colours are arranged as if they were in a trance and the ideas invest the pictorial space and  organize  it.  The  same  mystical  approach  to  the  act  of  creation makes him say that the painting is finished ‘‘when he feels his mind connect with that of the canvas’’. F.B.
