Painting is his favorite mode of expression but he tries other
mediums, sculpture, installations, performance. There is also a
fiurative work in which he also uses the technique of fiigree
mozaic. After studying in the natural sciences, Aihoulihoua
obtained a professional license in the cultural administration
of plastic arts at the ESMAC. His work integrates them, his research is oriented towards ‘‘calculating the amount of energy
