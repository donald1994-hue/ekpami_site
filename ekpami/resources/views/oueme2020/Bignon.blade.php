            <p>«  J’aime  sentir  mes  œuvres  comme  le  prolongement  de  ma  perception du monde, ma compréhension des faits sociaux et parfois  politiques.  Sculpter  accentue  chez  moi  cette  impres-sion  et  cette  connexion  avec  la  matière.  Éviter  de  polluer,  utiliser des matériaux récupérés, donner vie à quelque chose d’inattendu  avec  ses  doigts,  faire  vivre  une  idée,  une  vision  par des matériaux qui, à priori, n’ont rien en commun le tout dans  la  perspective  de  transformer  une  matière  improbable  en  une  œuvre  significative  ».  Des  ateliers  pour  enfants  et une école de métiers d’art au Bénin, quelques rencontres, des occidentaux,  Basquiat,  Polock,  des  compatriotes,  Hazoumé,  Zinkpé, un BTS en design graphique à Paris, un master en arts plastiques à la Sorbonne, ont échelonné le parcours artistique de José Bignon ADOUN. Guides de sa création, ce qu’il appelle les MIT (MATIERE - IDEE et THÈME) où la matière semble être « mythe » des origines, « trame, fil conducteur » de ses travaux. Ses  masques  écologiques,  cordes,  plastique,  tuyaux,  révèlent  un  monde  troublant,  abscons,  inquiétant,  un  monde  comme  étouffé attend une métamorphose. Bignon vit et travaille à Paris et à Porto-Novo au Bénin. F.B
            </p>


    </div>
