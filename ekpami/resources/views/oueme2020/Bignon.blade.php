    <div class="col-md-6 artiste-content-traduction">
            <p>‘‘I enjoy feeling my works as an extension of my perception of the world, my understanding of social and sometimes political facts.  Sculpting  accentuates  this  impression  and  connection  with  the  material.  Avoiding  pollution,  using  recycled  mate-rials, giving life to something unexpected with your fingers, bringing an idea, a vision to life through materials that, at first sight, have nothing in common, all with the aim of transfor-ming an improbable material into a meaningful work’’ Works-hops for children and an arts and crafts school in Benin, a few encounters,  occidentals  Basquiat,  Polock,  and  compatriotes,  Hazoumé, Zinkpé, a HND in graphic design at André Malraux in Paris, a master’s degree in plastic arts at the Sorbonne, have staggered José Bignon ADOUN’s artistic journey. Guides in his creation, what he calls the MIT (MATERIAL - IDEA and THEME) where  the  material  seems  to  be  ‘‘myth’’  of  the  origins,  the  ‘‘weft, thread’’ of his work. His ecological masks, ropes, plastic, pipes, reveal a disturbing, abstruse, worrying world, a world as if it was suffocated, who is waiting a metamorphosis. Bignon lives and works in Paris and also Porto-Novo in Benin. F.B
            </p>
