    <div class="col-md-6 ">
            <p>Préoccupé  par  la  sauvegarde  de  l’environnement,  Simplice  Ahouansou  l’interroge  et  découpe  des  parcelles  du  réel  pour  le  comprendre.  La  vache,  proche  de  l’homme,  au  rôle  bienveillant,  symbole  de  l’abondance  et  parfois  vénérée,  est  devenue  totémique  dans  son  travail.  Son  élevage  abusif  qui  déséquilibre  l’écosystème,  la  rend  emblématique  d’une  éco-nomie de marché orientée vers le profit, où l’homme même devient bête de somme. Découper pour détruire, recoller pour construire,  l’art  de  Simplice  Ahouansou  est  rédempteur.  Ces  morceaux  d’une  vie  et  d’une  géographie  éclatée  qui  mêlent  son histoire personnelle à l’histoire de son continent, donc du monde,  sont  liants  d’une  géométrie.  Nourrie  de  la  diversité,  elle brise comme la poésie sait le faire, le cadre convenu du réel. Pasolini qui voyait dans les années ’60 dans l’Afrique une forme  de  résistance  possible  à  un  modèle  de  société  que  la  consommation de masse avait uniformisé et réduit à des sté-réotypes, en ’75, dans sa dernière interview avertissait « Nous sommes tous en danger ». L’Afrique a depuis emboîté le pas de l’Occident et le danger reste permanent. Simplice Ahouansou a  toujours  eu  l’audace  de  ses  convictions,  avec  sa  toile  Azé,  l’Arbre à feuilles d’oiseaux symbolisant le vodun, il brisait un tabou de l’époque, à contre courant, à défaut de l’assimiler à la  sorcellerie,  il  y  défendait  une  vision  du  monde,  un  mode  de vie respectueux de la nature, en harmonie avec les lois du Cosmos, qui empêche de la vandaliser car, initié ou pas, dans le vodun on doit mériter ce qu’on désire. Azé, qui l’a consacré artiste, a marqué en même temps le début d’un retour à des pratiques endogènes. Si un chevalier sert et protège, Simplice Ahouansou, chevalier, protège la nature souveraine, choisit de servir selon le mérite. F.B
            </p>


    </div>
