    <div class="col-md-6 artiste-content-traduction">
            <p>Concerned  by  the  protection  of  the  environment,  Simplice  Ahouansou questions it and cuts out plots of real life in order to  understand  it.  The  cow,  close  to  man,  with  its  benevolent  role,  symbol  of  abundance  and  sometimes  venerated,  has  become totemic in his work. Its abusive breeding, which unba-lances the ecosystem, makes it emblematic of a profit oriented market economy oriented, where man himself becomes a beast of burden. Simplice Ahouansou’s art is redemptive : cutting to destroy,  gluing  to  rebuild.  These  fragmented  life  pieces  and  geography that mix his personal history with the history of his continent,  and  therefore  of  the  world,  are  bound  together  by  a geometry. Nourished by diversity, it breaks, as poetry knows how  to,  the  agreed  framework  of  reality.  Pasolini,  who  saw  Africa in the 1960s as a possible form of resistance to a society model  that  mass  consumption  had  standardized  and  reduced  to  stereotypes,  warned  in  his  last  interview  in  1975  that  ‘‘We  are all in danger’’. Africa has since followed the West’s lead and the  danger  remains  permanent.  Simplice  Ahouansou  always  had the audacity of his convictions, with his Azé canvas, L’arbre à  feuilles  d’oiseaux,  The  tree  with  bird  leaves  symbolizing  vodun, he broke a taboo of the era, against the current, failing to assimilate it to witchcraft, he defended a vision of the world, a  nature  respectful  way  of  life,  in  harmony  with  the  laws  of  the Cosmos, which prevents it from being vandalized because, initiated or not, in vodun one must deserve what one desires. Azé, who consecrated him as an artist, marked at the same time the beginning of a return to endogenous practices. If a knight serves  and  protects,  Simplice  Ahouansou  knight,  protects  the  sovereign nature, chooses to serve according to merit. F.B
            </p>
