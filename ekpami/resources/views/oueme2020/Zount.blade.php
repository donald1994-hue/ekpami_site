            <p>Philippe    Zountégni    Houédanou    comes  from  a  line  of  blacksmiths.  His  work  is  dependent  on  this  inti-mate  filiation  linked  to  metallurgy. However, metallurgy cannot be appreciated in most African societies if one does not associate it with the important role that belief in the existence of spirits plays in it. In his case, this is the Vodun culture. His sculptures bear wit-ness both to his interest in its traditions and to his aesthetic research.  The  characters  that  come  out  of  the  artist’s  studio  are strange beings evolving in a god, heroes and fascinating animals  populated  universe.  Beyond  the  choice  of  materials,  colours and varnish, one must pay attention to the movement he gives to his characters. The search for the centre of gravity to achieve balance is a permanent challenge for him. Philippe Zount  combines  metal  with  materials  such  as  burlap,  ropes  or  other  heterogeneous  objects  from  recycling.  This  eclectic  mix  is  undoubtedly  for  the  artist  a  way  to  humanize  beings  living on the fringes of our dimension. Evoking the sacred, the search for truth, motherhood, dialogue and understanding, it conveys a message of wisdom, openness and tolerance. Zount puts  forward  the  idea  that  one  must  assimilate  one’s  own  culture before opening oneself to that of others. D.H.
            </p>
