            <p>Philippe  Zountégni  Houédanou  est  issu  d’une  lignée  de  for-gerons. Son travail est tributaire de cette filiation intimement liée  à  la  métallurgie.  La  métallurgie  ne  peut  cependant  pas  s’apprécier  dans  la  plupart  des  sociétés  africaines  si  on  n’y  associe  pas  le  rôle  important  que  la  croyance  en  l’existence  des esprits y joue. Dans son cas, il s’agit de la culture vodoun. Ses sculptures témoignent aussi bien de son intérêt pour ses traditions  que  de  sa  recherche  esthétique.  Les  personnages  sortis  de  l’atelier  du  plasticien  sont  des  êtres  étranges  évo-luant  dans  un  univers  peuplé  de  dieux,  de  héros,  d’animaux  fascinants.  Au-delà  du  choix  des  matériaux  des  couleurs  et  du vernis, il faut prêter attention au mouvement qu’il donne à  ses  personnages.  La  recherche  du  centre  de  gravité  pour  parvenir à l’équilibre constitue pour lui un enjeu permanent. Philippe Zount allie au métal des matières telles que la toile de jute, des cordes ou d’autres objets hétéroclites issus de la récupération. Ce mélange éclectique est sans doute pour l’ar-tiste un moyen d’humaniser des êtres qui vivent en marge de notre dimension. Évoquant le sacré, la recherche de la vérité, la maternité, le dialogue et la compréhension, il véhicule un message  de  sagesse,  d’ouverture  et  de  tolérance.  Zount  met  en avant l’idée qu’il faut assimiler sa propre culture avant de s’ouvrir à celle des autres. D.H. 
