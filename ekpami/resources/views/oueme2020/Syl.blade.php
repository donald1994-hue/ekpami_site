    <div class="col-md-6 ">
            <p>Des  murs  de  Hôgbonu,  d’Adjatchè,  de  Porto-Novo,  Syl.Pâris.Kouton récupère le temps. Les murs séparent ce que l’on voit de ce qu’ils renferment, initié, la mémoire retrace et réinvente l’oubli.  Ses  toiles  inspirées  par  le  Fâ  et  les  pratiques  liées  au  vodun,  recueillent  mœurs,  croyances,  cultures  de  sa  ville  aux  trois noms, ville Yoruba, Gun, Aguda. Empreintes de la lumière, de  la  terre,  des  héritages  des  dieux  et  des  hommes,  les  tons  sont  purs,  l’ocre  est  ocre,  le  rouge  latérite,  le  bleu  est  royal,  pur  l’équilibre  des  collages,  cercles,  inscriptions  de  symboles  igbo,  avec  l’écriture  Syl.Pâris.Kouton,  son  nom  devenu  motif,  qui intègre l’artiste à son œuvre, signe son appartenance à un territoire qui le nourrit, qu’il réinvente. S’il reconnaît la pré-sence et l’influence des valeurs et croyances « importées », son regard est lucide, dans ses dessins il critique l’exploitation et la corruption dont l’Afrique, endormie, est la cible. Son identité coule dans ses veines, il la trouve dans l’héritage spirituel de son peuple où il puise « avec grande précision car rien n’est à compléter ni à diminuer dans l’ancestralité ». Un concept, Cité Baata. Baata, chaussure en langue yoruba, associé aux pas de danse  sacrée  Bâta,  l’un  des  trois  rythmes  qui  accompagnent  la  sortie  des  Egunguns  (revenants).  A  travers  les  chaussures  usagées,  mémoire  de  lieux  parcourus,  objets  de  pouvoir  qui  participaient  de  l’intronisation  du  roi,  signes  d’humilité  et  égalité, interdits de certains lieux de culte, objets de désir de la  consommation,  invite  à  une  subtile  interrogation  sur  les  symboles « du culte de la marque et du sacré de la chaussure transcendée dans le masque ». Le travail de Syl.Pâris.Kouton a le rythme précis des pas des danseurs Egunguns, il le « sort » pour rétablir l’harmonie dans la cité, ici et ailleurs. F.B
            </p>


