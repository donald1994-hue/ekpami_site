    <div class="col-md-6 artiste-content-traduction">
            <p>From  the  walls  of  Hôgbonu,  Adjatchè,  Porto-Novo,  Syl.Pâris.Kouton  recovers  time.  The  walls  separate  what  we  see  from  what they contain, insider, the memory retraces and reinvents oblivion.  His  canvases  inspired  by  the  Fâ  and  the  practices  related to vodun, collect customs, beliefs, cultures of his town with  three  names,  Yoruba,  Gun,  Aguda  city.  Imprints  of  light,  earth, legacies of gods and men, the tones are pure, the ochre is ochre, red the laterite, the blue is royal, pure the balance of collages, circles, inscriptions of igbo symbols, with the writing Syl.Pâris.Kouton,  his  name  became  a  motif  which  integrates  the  artist  in  his  work,  signs  his  belonging  to  a  territory  that  feeds  him,  which  he  reinvents.  If  he  recognizes  the  presence  and influence of ‘‘imported’’ values and beliefs, his gaze is lucid, in his drawings he criticizes the exploitation and corruption of which Africa, asleep, is the target. His identity flows in his veins, he finds it in the spiritual heritage of his people, from which he draws ‘‘with great precision because nothing is to be added to or diminished in ancestrality’’. A concept, Baata City. Baata, shoe in  Yoruba  language,  associated  with  the  sacred  dance  steps  Bâta, one of the three rhythms that accompany the release of the Egunguns (ancesters reincarnations). Through used shoes, memory of travelled places, objects of power that participated in the intronization of the king, signs of humility and equality, banned  from  certain  places  of  worship,  objects  of  desire  for  consumption,  invites  a  subtle  questioning  of  the  symbols  ‘‘of  the  brand  worship  and  the  shoe  sacrality  transcended  in  the  mask’’. The work of Syl.Pâris.Kouton has the precise rhythm of the  Egungun  dancers’  steps,  he  ‘‘takes  it  out’’  to  restore  har-mony in the city, here and elsewhere. F.B
            </p>
