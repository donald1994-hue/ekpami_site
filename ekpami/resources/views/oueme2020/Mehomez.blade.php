    <div class="col-md-6 artiste-content-traduction">

            <p>Ezéchiel Janvier Mehomè, son of a photographer, founded his research  on  materials  based  onobserving  lifestyles  and  cus-toms  of  his  fellow  citizens,  has  oriented  his  work  on  trans-mission  and  memory  and,  ethnographer’s  eye  for  heritage,  he  continues  to  count.  After  a  laborious  operation  of  safe-guarding  and  archiving  his  father’s  work,  he  continues  it  by  integrating these old studio photographs into his own. Family, lover  or  Sunday  portraits,  black  and  white  identities  of  the  inhabitants  of  Porto  Novo,  he  presents  them  in  the  form  of  nests. Nests, because transmission protects memory, a sort of tangible DNA, shelters the experience of fathers in the sons’ flight. These portraits of a city at the crossroads of languages, ethnic  groups,  traditions,  witnesses  of  a  world  with  diverse,  unfolded, open identities, Mehomez, confronts them to an un-just, unequal, discriminatory world. His reflections lead him to another work where from his setting of cities, portraits, lives, which cross materials, fabrics, plastic, sculptures, the links he grasps,  invents  and  reinvents  with  each  territory,  brings  out  aerial  installations,  rainbow  architectures.  Rainbow,  not  as  tolerance  (word  of  a  resigned  acceptance)  but  coloured  to  acquiescence without withdrawal, to consent without respite, to  the  other.  Mehomez  lives  and  works  since  2017  between  Dresden, Berlin in Germany and Porto-Novo, Benin. F.B
            </p>
