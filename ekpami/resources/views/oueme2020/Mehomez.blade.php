            <p>De  ses  recherches  sur  les  matières,  en  partant  de  ses  obser-vations des modes de vie et des coutumes de ses concitoyens, Ezéchiel Janvier Mehomè, ce fils de photographe, a orienté son travail sur la transmission et la mémoire et, œil d’ethnographe en  héritage,  continue  de  recenser.  Après  une  laborieuse  opé-ration  de  sauvegarde  et  d’archivage  du  travail  de  son  père  il  le prolonge en intégrant ces photographies anciennes, de stu-dio, au sien. Portraits de famille, d’amoureux ou de dimanche, identités  en  noir  et  blanc  des  habitants  de  Porto-Novo,  il  les  présente  sous  forme  de  nids.  Nids,  car  la  transmission  pro-tège la mémoire, sorte d’ADN tangible, abrite l’expérience des pères (des paires) dans l’envol des fils. Ces portraits d’une ville carrefour  de  langues,  d’ethnies,  des  traditions,  témoins  d’un  monde  aux  identités  diverses,  dépliées,  ouvertes,  Mehomez,  les confronte à un monde injuste, fait d’inégalités, discrimina-toire. Ses réflexions le conduisent à un autre travail où de son écrin de villes, portraits, vies, qui croisent matières, tissus, plas-tique, sculptures, les liens qu’il saisit, invente et réinvente avec chaque territoire, fait surgir des installations aériennes, archi-tectures arcs en ciel. Arc en ciel, non pas comme tolérance (mot d’une  acceptation  résignée)  mais  colorié  à  l’acquiescement  sans repli, au consentement sans répit, à l’autre. Mehomez vit et travaille depuis 2017 entre Dresde et Berlin en Allemagne et Porto-Novo, Bénin. F.B
            </p>


