    <div class="col-md-6 artiste-content-traduction">
            <p>Pascal   Sétondji’s   iron   sculptures   reproduce in the tradition of ironwork deities, royal symbols or various persona-lities, respond to orders from institutions or his inspiration for a  particular  event  and  continue  to  bring  to  life  this  ancient  court  art  which  requires  a  good  mastery  of  techniques  and  a  certain  sobriety.  In  his  other  works,  one  can  recognize  his  admiration for the Dakpogan brothers, heterogeneous assem-blages, diverse forms that integrate and embrace the contours of the elements, that display a profusion of materials. Wood, animal bones, fabrics, plate scraps, bark, anthropomorphic sta-tuettes  that  he  dresses,  paints  and  glues  are  used  to  create  works  that  become  the  actual  imprint  of  the  territory  from  which they were born. He turns them into witnesses, storytel-lers  who  tell  legends  and  traditions  that  the  artist  recycles  and translates while caring for the environment. F.B
            </p>
