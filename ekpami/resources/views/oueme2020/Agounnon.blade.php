            <p>Les sculptures en fer de Pascal Sétondji reproduisent dans la tradition de la ferronnerie des divinités, des symboles royaux ou diverses personnalités, répondent à des commandes d’ins-titutions ou à son inspiration pour un événement particulier et continuent de faire vivre cet art de cour ancien qui demande une bonne maîtrise des techniques et une certaine sobriété. Dans ses autres travaux, on peut reconnaître l’admiration qu’il porte aux frères Dakpogan, assemblages hétéroclites, formes diverses  qui  intègrent  et  embrassent  les  contours  des  élé-ments,  qui  étalent  une  profusion  de  matières.  Bois,  os  d’ani-maux,  tissus,  débris  d’assiettes,  écorces,  statuettes  anthropo-morphiques  qu’il  habille,  peint,  colle,  servent  à  la  création  d’ouvrages qui deviennent ainsi empreintes actuelles du terri-toire d’où ils sont nés. Il en fait des témoins, des conteurs, qui racontent des légendes et des traditions que l’artiste recycle et traduit tout en se souciant de l’environnement. F.B
            </p>


