            <p>n  a  revealing  slip  of  the  tongue,  he  presents himself as an visual artist re-sisting Porto-Novo. Raphaël Zossou works with  recycled  plastic  objects  to  ‘‘protect  the  environment  and fight against the aggressions that have programmed the loss  of  Mother  Nature,  because  our  towns  and  villages  are  crumbling  under  the  weight  of  our  various  wastes,  massive  residues  of  our  consumer  societies’’.  Recycling,  sewing  with  fishing thread, sewing the environment, its eco-plastic design is a form of resistance. Recycled, painted, cut out metal boards, the  ‘‘collages’’,  seams  with  wire,  colored  assemblies,  list  our  missed  acts.  Our  surpluses  therefore  our  shortages.  RaphaëlZossou has the touch of creative madness that forces him to search for materials in his polluted environment, to find pipes, cans, arms and heads of abandoned dolls, to turn them into a kind of contemporary Gélédé, who found their own rhythm.Raising awareness is part of his approach, Rahael Zossou was present at the 3rd edition of Festisol (Festival of Solidarities) in Châteaudun in France, on the theme of sustainable deve-lopment. F.B
