            <p>Dans  un  lapsus  révélateur,  il  se  présente  artiste  plasticien  résistant à Porto-Novo. Raphaël Zossou travaille des objets en plastiques recyclés pour « protéger l’environnement et lutter contre les agressions qui ont programmé la perte de la dame nature, car nos villes et villages croulent sous le poids de nos déchets divers, résidus massifs de nos sociétés de consomma-tion ». Recycler, coudre avec du fil de pêche, recoudre l’environ-nement, son éco-plastique design est une forme de résistance. Tableaux en métal récupéré, peint, découpé, les « collages », des  coutures  au  fil  de  fer,  assemblages  colorés,  recensent nos actes manqués. Nos surplus donc nos manques.  Raphael Zossou a le brin de folie créatrice qui l’oblige à chercher ses matières  dans  son  environnement  pollué  pour  dénicher  des  tuyaux,  des  bidons,  des  bras  et  des  têtes  de  poupées  aban-données, renoue avec des gestes anciens et crée des masques en  plastique  récupéré,  sorte  de  Gélédé  contemporains  qui  ont  trouvé  leur  propre  rythme.  Sensibiliser  fait  partie  de  sa  démarche,  Raphaël  Zossou  était  présent  à  la  3e  édition  de  Festisol,  (Festival  des  solidarités)  de  Châteaudun  en  France,  placée sous le signe du développement durable. F.B
            </p>


