    <div class="col-md-6 artiste-content-traduction">
            <p>Coming  from  a  great  blacksmiths  family,  the  Dakpogan  of  Porto  Novo,  he  has  preserved  the  legacy  of  a  tradition  marked by the presence of the vodun cult. Metal is the raw material of his work as a sculptor. Thus bolts, screws, springs, metal  rods  and  other  scrap  metal  are  assembled  under  his  hand,  which  gives  them  a  symbolic  function.  Simonet  Bio-kou is a magician of the world. He brings life back to where it  stopped.  Like  his  ancestors.  He  forges,  welds  and  models  iron by giving his works absolute balance. God, men, women, animals,  objects,  scenes  of  life:  humanity  and  its  temporal  production  are  divinely  resourced,  as  if  the  magic  of  the  Beninese  vodun,  the  original  vodun,  penetrated  each  work.  Through  this  redemptive  recycling,  Biokou  offers  a  second  birth to metal. He has forged an impressive series of life-size characters,  representing  small  trades,  sportsmen  or  musi-cians of all kinds. These current ‘‘fetishes’’ are resolutely mo-dern, not only in terms of their material but also in terms of their subjects: motorcyclist, golf player, cameraman, etc. But there is no break: his masterpiece, Gunuko, is a gripping evo-cation of an elementary divine force, always in close contact with the world of the living. Between heaven and earth, man and nature are reconciled.  (adapted).
            </p>
