    <div class="col-md-6 ">
            <p>Originaire d’une grande famille de forgerons, les Dakpogan de Porto-Novo, il a conservé l’héritage d’une tradition empreinte de  la  présence  du  culte  vodun.  Le  métal  est  la  matière  pre-mière de son travail de sculpteur. Ainsi boulons, vis, ressorts, tiges  métalliques  et  autres  ferrailles  s’assemblent  sous  sa  main qui leur confère une fonction symbolique. Simonet Bio-kou est un magicien du monde. Il refait la vie là où elle s’est arrêtée. Comme ses ancêtres. Il forge soude et modèle le fer un  donnant  à  ses  œuvres  l’équilibre  absolu.  Dieu,  hommes  femmes, animaux, objets, scènes de vie : l’humanité et sa pro-duction temporelle sont divinement ressourcées, comme si la magie  du  vodun  béninois,  vodun  originel,  pénétrait  chaque  œuvre. Par ce recyclage rédempteur, Biokou offre une seconde naissance  au  métal.  Il  a  forgé  une  impressionnante  série  de  personnages,  grandeur  nature,  représentant  petits  métiers,  sportifs ou musiciens en tout genre. Ces « fétiches » actuels sont  résolument  modernes,  non  seulement  par  leur  matière  mais  aussi  par  les  sujets  :  motocycliste,  joueur  de  golf,  ca-méraman, etc. Mais il n’y pas rupture : son œuvre maîtresse, Gunuko c’est l’évocation prenante d’une force divine élémen-taire,  toujours  en  contact  étroit  avec  le  monde  des  vivants.  Entre ciel et terre, homme et nature sont réconciliés. (adapté). 
            </p>


    </div>
