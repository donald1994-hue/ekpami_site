            <p>Désiré  Dédéhouanou  a  appris  à  accorder  beaucoup  d’impor-tance  à  ses  outils,  il  colle  des  pinceaux  sur  ses  toiles  en  faisant  des  sujets  à  part  entière  qui  donnent  à  ses  tableaux  des allures de sculptures. Outils en bois, aux poils d’animaux, outils  de  peintre  ou  d’artisan,  outils  de  travail  proche  de  la  nature de par les matériaux qui les font naître comme par leur destination qui est la création. Arbre au contour de femme ou corps  de  femme  dont  la  couleur  et  la  texture  reproduisent  les nervures, les lignes, le dessin intérieur des arbres, montre la  nature  des  choses,  la  vie  et  ses  origines,  les  instincts  qui  viennent  des  profondeurs.  Désiré  Dédéhouanou,  calligraphie  son  paysage  mental,  la  nature,  ses  valeurs,  les  livre  pinceau  sur toile ou à la main. F.B
