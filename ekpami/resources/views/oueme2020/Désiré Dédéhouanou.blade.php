    <div class="col-md-6 artiste-content-traduction">
            <p>Désiré Dédéhouanou has learned to give a lot of importance to  his  tools,  he  glues  brushes  on  his  canvases,  making  full-fledged  subjects  that  give  his  paintings  the  appearance  of sculptures. Wooden tools, animal hair, painter’s tools or crafts-man’s  tools,  working  tools  close  to  nature  by  the  materials  that gives birth to them as well as by their destination which is  creation.  Trees  with  the  outline  of  a  woman  or  a  woman’s  body  whose  colour  and  texture  reproduce  the  veins,  lines  and  inner  design  of  trees,  showing  the  nature  of  things,  life  and  its  origins,  instincts  that  come  from  the  depths.  Désiré  Dédéhouanou, calligraphies his mental landscape, nature, its values, delivers them brush on canvas or by hand. F.B
            </p>
