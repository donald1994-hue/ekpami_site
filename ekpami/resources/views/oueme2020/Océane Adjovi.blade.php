            <p>‘‘I didn’t choose painting, it’s a gift I share’’ It is she who creates it, it is he who shapes it. For Océane Maria K. Adjovi, art is first of  all  a  palette  of  emotions,  then  a  gateway  to  herself.  This  being that she draws, paints, photographs, multiple of herself, herself and another, is the quintessence of her inner lives, of her past and future lives that she imagines and invents, ver-sions of a young woman who, when she leaves the brush, also takes up the pen to tell herself. She represents a part of a new generation of artists, a part that does not pretend to change the world with messages but rather that questions. First her-self, through readings, then her art, looking for a comfortable or uncomfortable place for her in the currents of history, then the poetry of little things like, shivering in a gust of wind or reading Eluard, or painting a portrait with a notch that erases it. By also accepting to fail and start again, by photographing oneself to become or to be and the world does not change but cracks and becomes less impenetrable.  ‘‘Where  could  we  put  all  this  (our  emotions,  our  interrupted  sentences)  if  we  were  not  given  this  grace  of  unrestrained  expression?’’F.B
