    <div class="col-md-6 artiste-content-traduction">
            <p>Kiffouly  Youchaou  is  driven  by  an  obsession  :  the  respect  of  established  conventions,  especially  when  they  contribute  to  shaping  a  being  more  respectful  of  the  environment,  of  the  social  environment,  a  patriot.  Recycled  materials  are  part  of  the artist’s tools to shape his forms. They are generally used capsules that are meant to cover an object previously carved in wood. Woodcarving indeed holds an important place in Kif-fouly’s work, who readily claims its place in the great Yoruba sculptural tradition. The artist’s works denounce civic impro-priety, focus on educating the Beninese population about civic acts and are in search of social justice. A committed artist, he is an excellent performer who shows us by example that our actions are too often far removed from the fine speeches with which we think we can build a solid nation. D.H.
            </p>
