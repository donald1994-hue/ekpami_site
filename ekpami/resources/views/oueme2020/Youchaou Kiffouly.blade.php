            <p>Kiffouly Youchaou est taraudé par une obsession : le respect des  conventions  établies,  surtout  lorsqu’elles  concourent  à  façonner  un  être  plus  respectueux  de  l’environnement,  de  l’environnement social, un patriote. Les matériaux de récupé-ration font partie des outils dont se sert l’artiste pour modeler ses  formes.  Il  s’agit  généralement  de  capsules  usagées  qui  lui  servent  à  recouvrir  un  objet  préalablement  sculpté  dans  du bois. La sculpture sur bois tient en effet une place impor-tante dans le travail de Kiffouly qui revendique volontiers son inscription  dans  la  grande  tradition  sculpturale  yoruba.  Les  œuvres  du  plasticien  dénoncent  les  inconvenances  civiques,  portent sur l’éducation des populations béninoises aux actes citoyens, sont en quête de justice sociale. Artiste engagé, c’est un  excellent  performeur  qui  nous  montre  par  l’exemple  que  nos actes sont trop souvent éloignés des beaux discours avec lesquels nous pensons pouvoir bâtir une nation solide. D.H.
