            <p>Théodore Dakpogan forge depuis l’enfance à Gukomé au sein de sa famille : « Le métal a un vrai sens pour moi qui suis né forgeron, parce que c’est ça en un premier temps notre dieu, en tant que fils du dieu Ògún ». Il travaille le métal récupéré, fondu  et  refondu,  martelé  et  agencé  avec  d’autres  pièces  et  restes de la société industrielle. Ses œuvres de faïences mé-talliques  expriment  un  continuum  entre  son  regard  d’artiste  contemporain  et  son  métier  d’artisan  attaché  à  Ògún,  à  la fabrication  des  assen,  autels  portatifs  dédiés  aux  morts.  Ses  créations  diffèrent  selon  leurs  destinations,  sacrées  ou  pro-fanes,  initiées  au  culte  vodun  ou  à  l’art  contemporain.  Dans  les années 1990, il crée ainsi un Gù de fer rouillé patiné qui rentrera  dans  la  collection  de  Jean  Pigozzi.  En  mai  2015,  en  résidence  au  Centre,  à  Lobozounkpa,  il  choisit  de  produire  un  Gù  Manchot  en  référence  au  Gou  exposé  au  pavillon  des  Sessions du Louvre, longtemps exposé au musée de l’Homme. La  machette  et  la  cloche,  ses  attributs,  furent  égarées  dans  les  limbes  des  réserves  du  musée  de  l’Homme  et  ne  seront  retrouvées  que  dans  les  années  2000,  au  moment  de  son  transfert  au  Louvre.  Se  saisissant  de  cette  image  d’un  Gou  doublement  spolié,  volé  et  sans  armes,  mais    désormais  ex-posé au Louvre, Théodore Dakpogan réalise un Ògún amputé, porté par un placide manchot, modeste, statique et silencieux. Oreilles déployées, yeux grands ouverts, son pouvoir vient du secret,  du  silence,  ce  qui  n’empêche  pas  la  connaissance,  la  bienveillance, l’enjouement. S.C
