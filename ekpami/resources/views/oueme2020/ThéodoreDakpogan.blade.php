            <p>Theodore  Dakpogan  has  been  forging  since  childhood  at  Gukomé in his family: ‘‘Metal has a real meaning for me who was born a blacksmith, because it is first of all our god, as the son of the god Ògún’’.He works the metal recovered, melted and remelted, hammered and arranged with other pieces and remains  of  the  industrial  society.  His  works  of  metallic  ear-thenware express a continuum between his gaze as a contem-porary artist and his trade as a craftsman attached to Ògún, to the making of assen, portable altars dedicated to the dead. His  creations  differ  according  to  their  destination,  sacred  or  profane, initiated to the vodun cult or contemporary art. In the 1990’s, he created a Gù of rusty iron with a patina that will be part of Jean Pigozzi’s collection. In May 2015, Théodore Dak-pogan  is  invited  in  residence  at  the  Centre  in  Lobozounkpa.  He then chose to produce a Gù Manchot in reference to the Gou  exhibited  in  the  Pavillon  des  Sessions  of  the  Louvre,  long  exhibited  in  the  Musée  de  l’Homme.  The  machete  and  the  bell,  his  attributes,  were  lost  in  limbo  in  the  reserves  of  the Musée de l’Homme and will only be found in the 2000s, when it was transferred to the Louvre. Seizing this image of a Gou doubly looted, stolen and unarmed, but now exhibited in the Louvre, Théodore Dakpogan creates an amputated Ògún, carried by a placid, modest, static, silent one-armed man. Ears outstretched, eyes wide open, his power comes from secrecy, silence,  which  does  not  prevent  knowledge,  benevolence,  playfulness. S.C.
            </p>
