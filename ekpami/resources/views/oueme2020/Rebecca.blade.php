            <p>Some  of  Rebecca  Odjo  Favi’s  paintings  surprise  egun-guns  (ghosts, reincarnations of ancestors) at a ceremony, the move-ments  of  a  dancer  at  a  party  or  capture  the  sensuality  of  a  nude.  But  as  a  woman,  a  city-dweller,  a  painter  for  the  past  fifteen years, she is naturally inclined to incriminate anything that touches on freedom of expression. She then looks at ob-solete morals, fixed traditions, the use of vodun in malicious practices, forced marriage, in paintings with symbolic accents. In this new setting she tells a story that delivers her thoughts and  emotions  in  a  symbolic  way  where  the  colour  red  natu-rally replaces the ochre shades. F.B
