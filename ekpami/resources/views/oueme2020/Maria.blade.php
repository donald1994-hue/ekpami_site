            <p>Martine Dédéhouanou’s palette reveals a fine sensibility, her quest for serenity unfolds in dreamlike landscapes that reveal a  love  for  nature  that  perhaps  comes  from  the  north  of  the  country where she was born. The man is more wisely inscribed there  by  interventions  that  better  respect  the  cycles  and  let  it  breathe.  Her  daydreams,  constructions  where  reality  slips  into imaginary projections, are bathed in a beautiful light and, sometimes melancholy, they acquire a beautiful energy when she  spreads  them  out  in  layers  with  a  knife.  Self-taught,  her  practice has been refined by her deep desire to surprise and fix her states of mind and spirit, which she refines with spiritual readings. She also questions it ‘‘what should be the role of an artist in a society ? Will he be able to contribute to the emer-gence of a nation while making a living out of his art ?’’. F.B
