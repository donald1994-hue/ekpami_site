            <p>At  thirty  years  old,  ten  years  after  a  trai-ning   in   photography   and   video   editing,   Louis Oke Agbo takes the risk to do the photography he loves. In  2013,  he  initiates  a  workshop  for  social  reintegration  and  artistic expression at the Jacquot psychiatric centre in Cotonou and  directs  his  research  towards  therapeutic  art.  And  in  2017,  he creates an art therapy centre in Porto-Novo, an association that  offers  artistic  activities  to  people  with  cognitive  or  intel-lectual disabilities. The ‘‘crazy’’, often rejected and marginalized, are accompanied here in order to express themselves and (re)connect.  The  link  is  the  very  principle  of  Louis  Oké  Agbo’s  art.  Health as balance, man’s unique alliance with the earth, vodun, feeds his questioning. The mind is disturbed by the loss of the link, a sick man is disconnected. The religious system of vodun translates  by  a  linguistic  support  and  a  precise  symbolism  its  philosophical  concepts  of  Mahu,  god,  Wéké,  the  universe,  Gbé,  life, Gbéto, man. The four fundamental elements of nature that the  vodun  sacralizes,  Sakpata,  earth,  Xéviosso,  fire,  Toxosou, water and Dan, air, partly constitute man and provide his energy. Increasing the power of life is paramount, man himself can re-turn, divinity, Toxwio, embody the spirit of an ancestor. Louis Oké Agbo writes a book, Couches vulnérables Layers, and invents a ritual. His photographs, filled with earth and light, carry the fra-gility of bodies in front of walls where our world is used to hide it. He cannot heal, neither the sick nor the world that confines them, he protects them under layers of mother earth, writes on their bodies the links that connect them with the light. F.B
            </p>
