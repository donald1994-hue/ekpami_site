            <p>À  trente  ans,  dix  ans  après  une  formation  en  photographie  et  montage  vidéo,  Louis  Oke  Agbo  prend  le  risque  de  faire  la  photographie qu’il aime. En 2013, il initie un atelier de réinser-tion  sociale  et  d’expression  artistique  au  centre  psychiatrique  Jacquot  à  Cotonou  et  oriente  ses  recherches  vers  un  art  thé-rapeutique.  Et  en  2017,  il  crée  à  Porto-Novo  un  centre  d’art-thérapie, association qui propose des activités artistiques à des personnes  avec  des  déficiences  cognitives  ou  intellectuelles. Les « fous », souvent rejetés et marginalisés, sont ici accompa-gnés afin de s’exprimer et se (re)connecter. Le lien est le principe même  de  l’art  de  Louis  Oké  Agbo.  La  santé  comme  équilibre,  l’alliance unique de l’homme avec la terre, le vodun, nourrissent ses questionnements. L’esprit est perturbé par la perte du lien, un  homme  malade  est  déconnecté.  Le  système  religieux  du  vodun  traduit  par  un  support  linguistique  et  un  symbolisme  précis ses concepts philosophiques de Mahu, dieu, Wéké, l’uni-vers,  Gbé,  la  vie,  Gbéto,  l’homme.  Les  quatre  éléments  fonda-mentaux de la nature que le vodun sacralise, Sakpata, la terre, Xéviosso, le feu, Toxosou, l’eau et Dan, l’air, constituent en partie l’homme,  fournissent  son  énergie.  Augmenter  la  puissance  de  vie est primordial, l’homme même peut revenir, divinité, Toxwio, incarner  l’esprit  d’un  ancêtre.  Louis  Oké  Agbo  écrit  un  livre,  Couches  vulnérables  et  invente  un  rituel.  Ses  photographies,  pétries  de  terre  et  de  lumière,  portent  la  fragilité  des  corps  devant  les  murs  où  notre  monde  à  l’habitude  de  la  cacher.  Il  ne peut guérir, ni les malades, ni le monde qui les confine, il les protège sous des couches de la terre-mère, écrit sur leurs corps des liens qui les connectent avec la lumière. F.B
            </p>


