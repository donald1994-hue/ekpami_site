            <p>Pour son travail basé sur une technique mixte de récupération et de peinture, Didier Donatien Alihonou utilise des plaques aluminium  d’imprimerie.  Il  coud  ses  supports  avec  du  fer  rouge et blanc et du fil en cuivre, utilise du papier journal, fait des collages. Ses créations, portraits de l’enfance, expres-sions d’un bonheur tangible, superposés sur ses observations de l’actualité, recensent les injustices d’une société qui coupe les ailes et l’entrave. Son art est engagement. Les liens entre les  matières,  autant  des  liens  qu’il  imagine  possibles  dans  sa communauté qui s’inscrit dans celle plus vaste du monde. Donatien Alihonou réagit aux souffrances des plus fragiles, le trafic des enfants, la condition des enfants de la rue, les droits des femmes et des enfants. Son engagement se prolonge par des interventions dans les écoles, à éveiller et démontrer l’in-térêt d’une éducation artistique. Dans un même souci pédago-gique, il propose des ateliers destinés à des publics de jeunes avec des déficiences, révélant là encore, que les déficiences sont ailleurs. Les tableaux de Donatien Alihonou, supportent leurs poids de sens, portent l’idée d’un bonheur possible. F.B
            </p>


