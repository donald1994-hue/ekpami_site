            <p>For  his  work  based  on  a  mixed  technique  of  recovery  and  painting.  Didier  Donatien  Alihonou  uses  aluminium  printing  plates.  He  sews  his  supports  with  red  and  white  iron  and  copper  wire,  uses  newspaper,  makes  collages.  His  creations,  portraits  of  childhood,  expressions  of  a  tangible  happiness,  superimposed on his observations of current events, identify the  injustices  of  a  society  that  cuts  off  the  wings  and  hin-ders it. His art is commitment. The links between materials, as many links as he imagines possible in his community, which is  part  of  the  larger  world  community.  Donatien  Alihonou  reacts  to  the  most  fragile’s  sufferings,  child  trafficking,  the condition of street children, women and children’s rights. His commitment  extends  to  interventions  in  schools,  to  awaken  and  demonstrate  the  interest  of  an  artistic  education.  With  the same pedagogical concern, he offers workshops for young people  with  disabilities,  revealing  once  again  that  disabili-ties  are  elsewhere.  Donatien  Alihonou’s  paintings,  bear  their  weight of meaning, carry the idea of a possible happiness. F.B
