            <p>Tous les matins il monte les marches d’une terrasse suspendue dans sa cour, dans son cœur, pour nourrir ses frères du ciel, les oiseaux de Porto-Novo. Comme un oiseau, il picore des sensa-tions, des pensées, leur attache des mots et les niche dans la féerie du monde. Libre penseur, poète, il s’aménage un espace dans  la  langue  par  des  mots  qui  la  dé-composent,  outroeil,  évolassistance, écrimage, révelépuration... pour donner le sens précis  de  sa  quête  picturale  qui  n’est  que  la  quête  intime  de  son être. À pas d’oiseau, il cherche, il cherche dans, il cherche dedans, il cherche dehors où il voit un dedans plus vaste, il ef-fleure l’intimité du plus vaste, il devine, divine, heurte des pen-sées, écoute les profondeurs, s’agrippe à son bic (à son bec !) et le pointe, saisit sa variation la plus infime, la plus intime, et la pose, la pause aussi, l’écoute respirer. Bicquillages, bicquillures. De mère potière et père acrobate, François Zansou Kougblénou n’a pas d’âge. Enfant ou vieux sage lorsqu’il regarde le monde, adolescent lorsqu’il danse, les trois à la fois lorsqu’il pond ses réflexions, qu’il pose ses questions. Vannier, ses premières créa-tions artistiques étaient des fées, « la peinture est venue avec les angoisses ».  Depuis, il niche dans une calligraphie subtile à la qualité de l’art brut. À coté de sa signature, il inscrit 5cinq, cinq  comme  5doigts  de  la  main,  comme  5sens,  5continents.  Zansou  est  à  lui  seul  une  connexion  avec  le  monde,  vu,  pas  vu, vitale, inévitable. Surgis tout droit de l’inconscient collectif sur fond marron, couleur terre, couleur lourde, couleur ciel, des yeux  immobiles  d’idoles  nous  observent,  ils  nous  inquiètent.  C’est nous qui les regardons, ce sont eux qui nous voient. Êtres de pierre, oiseaux, poissons, portraits... Métamorphose des pro-fondeurs. F.B
            </p>


