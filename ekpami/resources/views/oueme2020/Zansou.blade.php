            <p>Every  morning  he  climbs  the  steps  of  a  terrace  suspended  in  his  courtyard,  in  his  heart,  to  feed  his  brothers  in  the  sky,  the  birds  of  Porto  Novo.  Like  a  bird,  he  pecks  sensations  and  thoughts,  attaches  them  and  nests  them  in the world enchantment. Free thinker, poet, he creates his own space    in  the  language  through  words  that  decompose  it,  biz-zardadaisme,  outreoeil,  (outofsight),  pondrahouse,  to  give  the  precise meaning of his pictorial quest which is just the intimate quest of his being. At bird’s pace, he seeks, he searches inside, he searches  outside  where  he  sees  a  larger  inside,  he  touches  the  intimacy  of  the  larger  inside,  he  guesses,  divine,  hits  thoughts,  listens to the depths, clings to his bic (to his beak !) and points it  out.  He  lets  it  guide  him,  grasps  it’s  most  intimate  variation,  and poses it, imposes it, pauses it too, listens it breathe. Bicquil-lages, bicquillures. Son of a potter mother and an acrobat father, François  Zansou  Kougblénou  is  ageless.  Child  or  wise  old  man  when he looks at the world, teenager when he dances, all three at  the  same  time  when  he  ponders  his  thoughts,  when  he  asks  his questions. Basket-maker, his first artistic creations were fai-ries,  ‘‘painting  came  with  the  anguish’’.  Since  then,  he  nests  in  a  subtle calligraphy with the quality of art brut. Next to his signa-ture, he inscribes 5cinq, five as 5fingers of the hand, as 5senses, 5continents. Zansou alone is a connection with the world, seen, unseen,  vital,  inevitable.  Emerging  straight  from  the  collective  unconscious  on  a  brown,  earth-coloured,  heavy  or  sky-coloured  background, the motionless eyes of idols observe us, they worry us. We’re the ones who look at them they’re the ones who see us... Beings, stone beings, birds, fish, portraits... Metamorphosis of the depths. F.B
            </p>
