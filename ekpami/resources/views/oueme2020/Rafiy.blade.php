            <p>Rafiy Okefolahan a été formé dans les ateliers de ses aînés au Bénin et à l’Ecole Nationale des Arts de Dakar. Artiste pluridisci-plinaire et quasiment autodidacte, il réalise principalement des peintures,  des  dessins,  des  photographies  et  des  installations.  Mais  quel  que  soit  son  médium,  sa  recherche  artistique  reste  celle d’un coloriste. La couleur est pour lui un moyen d’expres-sion essentiel, qui permet d’adoucir le propos traité tout en atti-rant  le  regard  du  spectateur,  et  en  l’amenant  à  questionner  la  société qu’il contemple. Ses œuvres traitent en effet de sujets sociétaux  importants.  Engagé,  il  aborde  à  travers  son  art  des  problématiques  socio-économiques  et  identitaires  comme  la  question  des  déplacements  de  populations,  des  inégalités,  du  changement climatique et de la corruption. Ancrées dans la réa-lité ses oeuvres sont souvent en lien avec l’actualité. Accordant une importance toute particulière au dialogue des disciplines, dans son travail, il peut associer la danse, le théâtre, le film. Rafiy Okefolahan participe régulièrement à des résidences et des  workshops  nationaux  et  internationaux.  Depuis  2009  son  travail est proposé par la Galerie Lazarew à Paris, Out of Africa à Sitges, en Espagne, Gafra à Londres, Arcadem à Genève et aussi en Afrique au Bénin, au Sénégal, en Ouganda et en Asie à Hong-Kong...  Créée en 2010, son association Elowa est aujourd’hui responsable du fonctionnement global de  « La Grande Place », un lieu culturel que Rafiy Okefolahan a inauguré en décembre 2019,  ouvert  à  des  résidences,  expositions,  actions  de  forma-tion, projets d’éducation et débats, au cœur de la ville de Porto-Novo.  Afin de développer toutes ses activités, Rafiy Okefolahan vit et travaille entre la France et le Bénin.
            </p>


    </div>
