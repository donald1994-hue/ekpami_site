    <div class="col-md-6 artiste-content-traduction">

            <p>Rafiy  Okefolahan  was  trained  in  his elders’  workshops  in  Benin  and  at  the  National School of Arts in Dakar. A multidisci-plinary and almost self-taught artist, he mainly produces pain-tings,  drawings,  photographs  and  installations.  But  whatever  the medium, his artistic research remains that of a colourist. For him,  colour  is  an  essential  means  of  expression,  which  allows  him  to  soften  the  subject  matter  while  attracting  the  viewer’s  attention and leading him to question the society he is contem-plating. Indeed, his works deal with important societal subjects. Through his art, he addresses socio-economic and identity-rela-ted issues such as the displacement of populations, inequalities, climate change and corruption. Anchored in reality, his works are often linked to current events. Giving particular importance to the dialogue of disciplines, in his work he can associate dance, theatre and film. Rafiy Okefolahan regularly participates in na-tional and international residencies and workshops. Since 2009 his work has been proposed by Galerie Lazarew in Paris, Out of Africa in Sitges, Spain, Gafra in London, Arcadem in Geneva and also in Africa in Benin, Senegal, Uganda and Asia in Hong-Kong. Created  in  2010,  His  association  Elowa  is  today  responsible  for the overall operation of ‘‘La Grande Place’’, a cultural space that Rafiy Okefolahan inaugurated in December 2019, open to residencies, exhibitions, training activities, educational projects and debates in the heart of Porto-Novo. In order to develop all his activities, Rafiy Okefolahan lives and works between France and Benin. 
            </p>
