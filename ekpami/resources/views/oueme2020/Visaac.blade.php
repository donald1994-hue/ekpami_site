            <p>Une  formation  en  peinture  de  décoration  et  calligraphie  a  peaufiné le goût d’Isaac Vissou pour les formes pures et les lignes. Peintre à ses débuts, ce goût, ses observations sur les habitudes  de  vie,  sur  les  intérieurs  des  habitants  de  sa  ville,  son habileté et ses recherches ont orienté son travail vers la création de design mobilier. Dans un environnement où l’uti-lité immédiate cède le plus souvent le pas sur l’esthétique et ne s’enquière pas tellement du confort, ses meubles modernes en branches de goyavier ont de quoi surprendre. Il intervient et  transforme  les  branches  de  cet  arbre  solide,  dont  il  suit  les formes et les lignes, pour imaginer des meubles, canapés, tables, tabourets, lampes, qui, ingénieux et épurés, sont aussi pratiques et confortables. Visaac qui continue de peindre les faits  de  société  qui  l’interpellent,  calligraphie  surtout  les  sèves  de  son  terroir,  les  enduit  de  son  expérience,  introduit  la  nature  dans  les  maisons  d’un  œil  attentif  à  leurs  besoins,  les orientant aussi avec l’assurance sereine de sa vision. F.B
