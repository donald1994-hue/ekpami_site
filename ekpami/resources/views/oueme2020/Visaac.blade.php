            <p>A  training  in  decorative  painting  and  calligraphy  refined  Isaac  Vis-sou’s  taste  for  pure  forms  and  lines.  As a painter in his early days, this taste, his observations on the habits of life, on the interiors of the inha-bitants of his city, his skill and his research have oriented his work towards the creation of furniture design. In an environ-ment  where  immediate  utility  often  gives  way  to  aesthetics  and  does  not  care  so  much  about  comfort,  his  modern  fur-niture  made  of  guava  branches  is  surprising.  He  intervenes  and transforms the branches of this solid tree, whose shapes and lines he follows, to imagine furniture, sofas, tables, stools, lamps,  which,  ingenious  and  refined,  are  also  practical  and comfortable.  Visaac,  who  continues  to  paint  the  facts  of  so-ciety that challenge him, calligraphy especially the saplings of his land, coating them with his experience, brings nature into homes with an eye attentive to their needs, guiding them also with the serene assurance of his vision. F.B
