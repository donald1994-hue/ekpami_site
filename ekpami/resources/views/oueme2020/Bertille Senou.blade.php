            <p>Her sensitivity  apprehends  life  with  childhood’s  eyes  and  transposes  on  canvas  her  wonder  for  the  world’s  play  of  co-lours.  This  innocence,  fruit  of  a  deep  things  of  life  intuition,  is her gateway to the buried beauty of fleeting moments, the magic  of  ordinary  gestures.  The  surface  of  certain  canvases  takes  up  the  cracks,  the  warmth  of  the  ochre  earth  that  her  footsteps  graze,  a  land  of  welcome  for  the  characters  inscri-bed among the signs and symbols that serve as their compass. As  a  painter,  and  therefore  a  rebel,  Bertille  Sénou  is  no  less  intimately  linked  to  her  culture.  Proud  of  her  origins,  of  the  values that preceded her and served as her foundation, she is no less convinced of the art’s imperious necessity in the Man’s blossoming. Painting is a space of freedom. F.B.
            </p>
