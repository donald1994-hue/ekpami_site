            <p>François Ablefonlin’s  painting  is  straddling  between  figura-tive and abstract. The stylized characters are either chimerical forms  reminiscent  of  the  ancient  civilizations’  gods  that  are  now extinct, or ghostly-looking beings moving in an ethereal universe,  in  which  diffuse  light  itself  seems  to  be  endowed  with  consciousness.  The  characters  represented  are  gene-rally asexual beings who seem to be emanations of the light source,  the  materialization  (material  manifestation)  of  this  light to which they give the impression of wanting to return, in which they seem to want to blend in. The presence of war-mer hues does not succeed in attenuating this ghostly light, which, if we refer to the testimonies of people who have un-dergone a near-death experience, could well be similar to the famous  light  tunnel,  passage  to  the  afterlife.  A  passage  that  the artist’s painting would like to abolish through ritual. D.H.
            </p>
