            <p>Une licence en administration de territoire, un parcours d’illus-tratrice de livres, une résidence photographique au Studio des arts contemporains de Fresnoy en France, la dévoilent en par-tie.  Le  regard  attentif  qu’elle  porte  sur  l’être  humain,  sur  son  histoire  et  sa  destinée,  sur  ses  rapports  avec  les  autres  qui  «  l’activent », ce regard qui voit en l’homme une constellation de rencontres et des interactions avec les autres, révèle l’essence de son travail artistique. D’une démarche assurée, scientifique, elle  suit  des  traces,  relève  des  empreintes,  interroge  la  place  et  le  rôle  de  la  femme  dans  la  société,  questionne  l’identité.  Fascinée par la lumière, elle la consacre médium, la développe grâce  à  plusieurs  techniques  pour  écrire  des  métaphores  qui  donnent à réfléchir. Matériaux recyclés, papier, tissus, peinture, lampes,  photogrammes  -  photos  sans  utilisation  d’un  appareil  -,  pour  créer  des  installations  interactives  :  c’est  au  visiteur  d’activer  l’œuvre,  ateliers  pour  enfants  pour  faire  ressusciter  des  jeux  oubliés,  le  awale  ou  le  cerf-volant  et  leur  donner  le  goût de la communauté, d’une vie sociale, hors des écrans, dans une  démarche  toujours  incitative.  Mais  aussi  imaginer  une  récade,  entrée  dans  la  collection  du  Petit  Musée  de  la  récade  à  Lobozounkpa,  pour  Tassi  Hangbe,  seule  reine  parmi  les  rois  de Danxomé, effacée de l’histoire du royaume, car femme. « Le plus grand talent que Dieu m’ait donné, et cela même avant le dessin, c’est peut être de voir derrière un simple tas de sable les centaines de millions de grains qui pourraient le composer. » Et de voir avec une science si poétique, un homme comme une « bibliothèque de cellules ». F.B
            </p>


