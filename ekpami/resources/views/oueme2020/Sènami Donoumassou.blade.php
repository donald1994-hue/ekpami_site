    <div class="col-md-6 artiste-content-traduction">
            <p>A degree in territorial administration, a  career  as  a  book  illustrator,  a  photo-graphic  residency  at  the  Studio  des  arts  contemporains de Fresnoy in France, reveal her in part.The  attentive  look  she  takes  on  the  human  being,  on  his  history and his destiny, on his relationships with others that ‘‘activate’’  him,  this  look  that  sees  man  as  a  constellation  of  encounters and interactions with others, reveals the essence of her artistic work. With a confident, scientific approach, she follows  traces,  takes  up  footprints,  questions  the  place  and  role  of  women  in  society,  questions  identity.  Fascinated  by  light,  she  consecrates  it  as  a  medium,  developing  it  through  several  techniques  to  write  metaphors  that  give  food  for  thought. Recycled materials, paper, fabrics, paint, lamps, pho-tograms -photographs without the use of a camera -, to create interactive  installations  :  is  up  to  the  visitor  to  activate  the  work;  workshops  with  children,  to  revive  forgotten  games, the  awale  or  the  kite  and  give  them  a  taste  of  community,  of  a  social  life,  outside  the  screens,  allways  in  an  incentive  approach.  But  also  to  imagine  a  recade,  entered  in  the  col-lection  of  the  Petit  musée  de  la  récade  in  Lobozounkpa,  for  Tassi  Hangbe,  the  only  queen  among  the  kings  of  Danxomé,  erased from the history of the kingdom, because woman ‘‘The greatest talent that God gave me, even before the drawing, is perhaps to see behind a simple pile of sand the hundreds of millions of grains that could make it up’’. And to see with such poetic science, a man as a ‘‘library of cells’’. F.B
            </p>
