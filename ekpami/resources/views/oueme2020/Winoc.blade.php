    <div class="col-md-6 artiste-content-traduction">

            <p>Winoc Boton is involved in recovery in a movement committed to drawing the attention of the authorities and the population to  the  urgency  and  the  need  to  safeguard  the  environment.  Through his work, the objects he transforms are reintegrated into society and convey his message. Attentive to social facts, he  denounces  manifest  inequalities  such  as  the  poverty  of  many of his compatriots, but also acts of incivility, as he cas-tigates in the same spirit the neo-colonialist practices of cer-tain foreign powers. With imagination and a sense of derision, he  paints  shop  signs,  hairdressing  and  sewing  workshops,  sentences on the walls of houses : advertisements for house sales, various warnings... The decoration thus arranged draws the urban landscape and reproduces a map of the city where he delivers his observations on man in his environment. F.B
            </p>
