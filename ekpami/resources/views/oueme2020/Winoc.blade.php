            <p>Winoc  Boton  fait  de  la  récupération  dans  un  mouvement  engagé d’attirer l’attention des autorités et de la population sur l’urgence et la nécessité de sauvegarder l’environnement. Par  son  travail,  ses  objets  qu’il  transforme  sont  réinsérés  dans la société et véhiculent son message. Attentif aux faits sociaux, il dénonce les inégalités manifestes comme la pau-vreté d’un grand nombre de ses compatriotes, mais aussi les actes  d’incivisme,  comme  il  fustige  dans  un  même  élan  les  pratiques à caractère néocolonialiste de certaines puissances étrangères.  Avec  imagination  et  sens  de  la  dérision  il  peint  des  enseignes  d’échoppes,  d’ateliers  de  coiffure,  de  couture,  des phrases relevées sur les murs des maisons : annonces de vente  de  maisons,  diverses  mises  en  garde...  Le  décor  ainsi  agencé  dessine  le  paysage  urbain  et  reproduit  une  carte  de  la  ville  où  il  délivre  ses  observations  sur  l’homme  dans  son  environnement. F.B
            </p>


