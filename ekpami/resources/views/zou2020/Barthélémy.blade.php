            <p>Ses  masques  Gélédé  ont  la  fraîcheur  des  trouvailles  heu-reuses, des jeux d’enfants qu’on regarde sourire aux yeux. Son « kamikaze », trafiquant miséreux d’essence de contrebande, doudoune orange, bonnet tiré sur les oreilles contre le froid du  matin,  lunettes  protège-poussière,  pantalon  en  tissu  et  «  tapettes », attaché par des fils à ses bidons, autre bidon entre ses  bras,  conduit  sa  Suzuki.  On  est  saisis.  Saisis  par  la  scène  au point qu’on oublie. On oublie la folie de l’entreprise pour admirer le personnage avant de réaliser que relié à sa moto, à ses bidons comme un malade à ses perfusions, le conducteur est relié à la survie. Pousse-pousse, vespas, bidons d’essence, Barthélémy Hountchonou sculpte le bois, détourne les objets de  son  environnement,  observe.  Cherche  l’essence  du  quoti-dien, de la vie. F.B
