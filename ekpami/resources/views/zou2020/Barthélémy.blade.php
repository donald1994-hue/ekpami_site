            <p>His  Gélédé  masks  have  the  freshness  of  happy  discoveries,  children’s games that you look at with a smile on your face. His ‘‘kamikaze’’, a miserable contraband gas smuggler, an orange comforter, a hat pulled over his ears against the morning cold, dust goggles, cloth pants and ‘‘sandals’’, tied by threads to his cans, another can between his arms, drives his Suzuki. We are seized. Seized by the scene to the point that we forget. We for-get the madness of the undertaking to admire the character before realizing that connected to his motorcycle, to his cans like a sick person to his infusions, the driver is linked to sur-vival. Rickshaw, vespas, petrol cans, Barthélémy Hountchonou carves wood, diverts objects from his environment, observes. Seeks the essence of daily life. F.B
            </p>
