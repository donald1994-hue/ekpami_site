    <div class="col-md-6 artiste-content-traduction">
            <p>The   paintings   and   sculptures   that   Adjamalé  creates  are  rooted  in  the  fon  and  vodun  tradition,  through  his  observation  of  daily  life,  through his anger. The vodun is the focal point around which and  from  which  his  work  draws  its  sap.  The  artist  manages  to distance himself from certain traditional practices that he finds static and notes a certain immobility of tradition that reduces his fellow citizens to a wait-and-see attitude that he deplores. Some canvases, supports for his outrage in front of a  continent  that  continues  to  bend  its  back  and  in  front  of  the  continuous  humiliations  that  this  continent  undergoes,  castigate  an  Africa  that  consumes  only  goods  produced  out-side.  Adjamalé  mainly  uses  cement  packaging  to  make  his  sculptures. From this paper he makes up his own material, he adds  laterite,  pigments  and  a  lot  of  glue  to  make  it  all  stick  together and to facilitate the modelling of certain elements for a strikingly realistic result. D.H. (excerpts)
            </p>
