            <p>Les tableaux et les sculptures que Adjamalé réalise prennent leur  sources  dans  la  tradition  fon  et  vodun,  dans  son  obser-vation  du  quotidien,  dans  ses  colères.  Le  vodun  constitue  le  point focal autour duquel et à partir duquel son travail puise sa sève. L’artiste arrive à prendre sa distance face à certaines pratiques traditionnelles qu’il trouve statiques et constate un certain immobilisme de la tradition qui réduit ses concitoyens à un attentisme qu’il déplore. Certaines toiles, supports pour ses coups de gueule face à un continent qui continue de cour-ber l’échine et face aux humiliations continues que ce conti-nent subit, fustigent une Afrique consommatrice uniquement des  biens  qu’on  produit  à  l’extérieur.  Pour  réaliser  ses  sculp-tures  Adjamalé  utilise  surtout  les  emballages  de  ciment.  A  partir de ce papier il constitue sa propre matière, il y ajoute la latérite, des pigments et énormément de colle pour faire tenir le tout et pour faciliter le modelage de certains éléments pour un résultat saisissant de réalisme. D.H. (extraits)
