    <div class="col-md-6 artiste-content-traduction">
            <p>Following  the  tradition  of  the  court art of Abomey, Edouard Vo-doumbo makes bas-reliefs and de-corated temples. Drawing on the same imagery, he painted divinities, animals, symbols of the kings of Dahomey, recades (signs of royalty), assen (cult objects used to pay homage to the dead) and carries the vodun pantheon and his disciples on canvases of a clear sobriety, which is not in the drawing or in the use of colours but in the composition. The  strokes  are  clean,  the  surface  of  the  canvas  is  entirely  covered with white, a whiteness that highlights and exalts the single figure even more. His work, following in the footsteps of  Cyprien  Tokoudagba,  one  of  the  ‘‘magicians  of  the  earth’’,  a  renowned  Beninese  artist,  with  whom  a  plethora  of  local  artists were trained, continues to bring to life the spirit of the place while participating in its history. F.B
            </p>
