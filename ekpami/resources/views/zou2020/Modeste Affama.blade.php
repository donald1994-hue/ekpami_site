            <p>La terre est l’élément qui le définit le mieux. La terre est un matériau,  une  matière  première  omniprésente  et  récurrente  dans la composition de ses tableaux. La relation qu’il entre-tient avec l’élément terre est quasi obsessionnelle, une obses-sion telle que la terre pourrait parfois se substituer à la signa-ture de l’artiste. D’où peut bien provenir un tel attachement de Modeste pour sa terre natale ? Car il s’agit à n’en point douter d’un lien affectif qui transparaît dans l’utilisation répétitive de la terre de la région d’Abomey. Il signifie par la sorte son ap-partenance au milieu dans lequel ses œuvres prennent nais-sance ? Il y a probablement dans cette attitude un question-nement identitaire. Ses études l’ont conduit hors de la région mais il revient toujours inexorablement. Il se pourrait que les seuls repères dont l’artiste soit conscient, aient un lien étroit avec son terroir. Dans ses toiles il y a comme une hantise de la  disparition  de  ce  terroir-repère,  une  certaine  désolation  engendrée  par  la  sécheresse  apparente,  la  famine  possible  comme  entrave  dans  la  vie  symbiotique  des  êtres  avec  leur  milieu.  Modeste  Afama  réalise  aussi  des  installations  issues  de  la  récupération  et  utilise  souvent  les  cauris,  symbole  de  l’argent, qui l’inscrivent encore une fois dans un rapport étroit avec la matérialité. D.H. (extraits)
            </p>


