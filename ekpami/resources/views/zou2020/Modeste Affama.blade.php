            <p>The  earth  is  the  element  that  best defines it. Earth is a material, a raw material that is omnipresent and  recurrent  in  the  composition  of  his  paintings.  His  relationship  with  the  earth element is almost obsessive, an obsession such as earth could  sometimes  substitute  for  the  artist’s  signature.  Where  does Modeste’s attachment to his native land come from? Be-cause it is undoubtedly an emotional bond that is reflected in the repetitive use of the land in the region of Abomey. In this way, does he mean his belonging to the environment in which his works originate? There is probably in this attitude a ques-tioning of identity. His studies took him out of the region but he always returns inexorably. It could be that the only land-marks of which the artist is aware, have a close link with his terroir. In his paintings, there is like a haunting of this terroir’s disappearance,  a  certain  desolation  caused  by  the  apparent  drought, the possible famine as a hindrance in the symbiotic life  of  beings  with  their  environment.  Modeste  Afama  also  creates installations that come from recovery and often uses cowries, the symbol of money, which once again inscribe it in a close relationship with materiality. D.H. (excerpts)
