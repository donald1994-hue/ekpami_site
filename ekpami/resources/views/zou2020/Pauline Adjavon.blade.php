            <p>Freed  from  the  weight  of  tra-dition,   freed   from   the   clichéd   colours  of  the  continent,  freed  from  the duty of running out of answers, Marius Adjagan seems happy to find the right questions. Free or at least lighter. In these new series, two dominant colours, black and white, tree branches, twigs, bird feathers, cowries, masks, wooden statuettes and paint as materials to deliver a refined work, symbolic, reduced to the essential. The essential for him is nature, man and his environment, roots and wings. The trees are characters, the characters are trees. They remain standing or  fall  down  roots  towards  the  sky,  coloured  feathers  cover  a character with closed eyes, here and there arrows indicate all directions, others, the possible entrances and exits of the frame. The world is side up or upside down, the life is source of death, death is source of life. The tree-man feeds on what was before him and from dreams in order to fly away. His plas-tic compositions, cases where beauty hides face to face. ‘‘I am willing to link earth to heaven’’ . F.B
            </p>
