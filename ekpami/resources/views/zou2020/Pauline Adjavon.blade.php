            <p>Affranchi  du  poids  des  traditions,  affranchi  des  couleurs  cli-chées du continent, affranchi du devoir de courir les réponses, Marius Adjagan semble heureux de trouver les bonnes ques-tions.  Affranchi  ou  tout  au  moins  allégé.  Dans  ces  nouvelles  séries,  deux  couleurs  dominantes,  le  noir  et  le  blanc,  des  branches  d’arbres,  des  brindilles,  des  plumes  d’oiseaux,  des  cauris, des masques, des statuettes en bois et de la peinture comme matériaux pour délivrer un travail épuré, symbolique, réduit à l’essentiel. L’essentiel pour lui est la nature, l’homme et son environnement, les racines et les ailes. Les arbres sont des personnages, les personnages sont des arbres. Ils restent debout  ou  se  renversent  racines  vers  le  ciel,  les  plumes  co-lorées  coiffent  un  personnage  aux  yeux  fermés,  ci  et  là  des  flèches  indiquent  toutes  directions,  d’autres,  les  entrées  et les  sorties  possibles  du  cadre.  Le  monde  est  à  l’endroit  ou  à  l’envers,  la  vie  est  source  de  mort,  la  mort  source  de  vie.  L’homme-arbre se nourrit de ce qui était avant lui et du rêve pour s’envoler. Ses compositions plastiques, écrins où la beau-té se cache à visage découvert. « Je veux bien relier la terre au ciel ». F.B
