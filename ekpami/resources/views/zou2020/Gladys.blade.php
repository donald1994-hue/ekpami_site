    <div class="col-md-6 artiste-content-traduction">
            <p>The  delirious  inventiveness,  skill  and  originality  with  which  he approached the materials and his subjects, took out of the ordinary  a  stall  of  salvaged  objects  and  pieces  of  dry  wood  that became masks and sculptures. As if from a crucible, from this  bubbling  magma  that  he  seems  to  have  abandoned,  emerged a painting with fresh colours, with delicately drawn pictures, where the gaze crosses a delirious world, duck-rab-bits, a sort of optical illusion on blindly reconstituted bodies, turtle heads, a snake, plants and roots, human characters, an animated world, animated as in cartoons. Vehicles, huts, uni-dentifiable objects, all that is not alive, are miniaturized...The world seems bewildered and it seems that human beings and nature cannot cohabit. When they are present, she is absent. A kind of play is set up where pieces of hybrid beings and lives cross,  meet  and  separate  in  a  joyful  chaos  in  a  nature  that  makes one think that if man can be welcome he is not master. Gladys Monlémé has retained the creativity of his practice and has, as best she can, given it a framework to flourish without dispersing. F.B
            </p>
