            <p>L’inventivité délirante, l’habileté et l’originalité avec laquelle il abordait les matériaux et ses sujets, sortaient de l’ordinaire un étal d’objets de récupération et des morceaux de bois sec qui  devenaient  des  masques  et  des  sculptures.  Comme  d’un  creuset,  de  ce  magma  bouillonnant  qu’il  semble  avoir  aban-donné, est ressortie une peinture aux couleurs fraîches, aux dessins  délurés,  où  le  regard  croise  un  monde  délirant,  des  canard-lapins, sorte d’illusions optiques sur des corps recons-titués à l’aveugle, des têtes de tortues, un serpent, des plantes et  des  racines,  des  personnages  humains,  un  monde  animé,  animé  comme  dans  dessins  animés.  Les  véhicules,  les  cases,  les objets inidentifiables, ce qui n’est pas vivant, sont miniatu-risés...Le monde semble dérouté et il semblerait que les êtres humains  et  la  nature  ne  peuvent  pas  cohabiter.  Lorsqu’ils  sont  présents,  elle  s’absente.  Se  joue  une  sorte  de  pièce  où  des morceaux d’êtres et de vies hybrides se croisent, se ren-contrent et se séparent dans un chaos joyeux dans une nature qui fait penser que si l’homme peut être bienvenu il n’est pas maître. Gladys Monlémé a conservé la créativité de sa pra-tique  et  l’a  comme  mieux  cernée,  lui  a  donné  un  cadre  pour  s’épanouir sans se disperser. F.B
