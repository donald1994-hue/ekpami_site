<h3 style="color:red;">STATE OF EMERGENCY</h3>

<div class="" style="color: #53a5cb;">
  <small><strong>We were captivated by an image. And we
couldn’t get out of it because it resided in
our language and it seemed to repeat it only
inexorably. Ludwig Wittgenstein</strong> </small>
    <p>In 2012, during the first national census that allowed
the publication in 2013 of the Catalog of the visual
artists and profesionnels of the visual arts in Benin,
when we watched the creation blown away go out into
the street, hiding in the courtyards, One could feel this
ebullition that Arttistik Afrika, l’Institut culturel français, l’Espace Tchif, la Fondation Zinsou, Kulturforum
Nord-Sud, la Médiathèque des diasporas, Abap, Elowa,
Fagplag, Ouadada and other cultural associations,
accompanied. That two biennials Regard Bénin, MAVA
(Museum of Activ life), the first Open Studio’s, Waba, le
Centre Unik, the exchanges with ESA Clermont Ferrand,
the Chinese culturel Center, the artists’ collaboration
with la dilettante, Cotonou creative, have maintained.
It continued through collaboration with the curatorial
project of Galerie Vallois and the creation of Le Centre
in Lobozounkpa in 2015, of Cotonou Creative in the
residencies at ENSA Dijon, which the new INMAAC Arts
Department, the prefiguration in 2016 in Porto Novo
of an alternative art school, l’école de Hogbonu, in
the opening towards African creation of Fresnoy, the
contemporary art studio of Tourcoing, in the partnership between the University of Abomey Calavi and the
University of Cergy Pontoise and the creation of new
cultural centres.</p>
<p>This repertory, frut of the census of 2020, lists two hundred fifty-nine artists. Thirty three female artists. </p>
<p>Self-taught, integrating the art training institute of
the country or schools, some prestigious abroad, courageous, these female artists, explore new forms, raise
their place in society, through painting, photography,
performance, video, they initiate and direct structures,
question identity, show, guess the places and myths
from which they extract their substance. The society,
the conditioning, the prejudices, the idea of a mission
invested in women, maternity, domestic life makes this
path complex and thorny, the opening isn’t not yer there... but it augurs well.</p>
<p>Female, men, artists are continuously trained, with
their peers in studios, schools, exchanges and residencies, have odd jobs that and fortunately they also
continue to be self-taught. The past of the continent,
of the country, always clashes but they focus on the
future, redraw their environment. They seek to define
their work, to name it, to refine their intentions, their
approach and even if it means breaking with the comfort of habits, they try to understand it, renew it, change
their mediums, mix them too without hesitation.</p>
<p>The artists read Franz Fanon, Joseph Beuys, Jean Dubuffet, Cheikh Anta Diop, Paul Eluard, Bigara Diop, admire
Picasso, Basquiat, Keith Harring, Andy Warhol, the first
sculptors of Benin, Queen Tassi Hangbé, the Mino, (the
Amazons), Béhanzin, Sankara, Mandela, Wole Soyinka,
other young African artists inspire them, they trained
with the Beninese masters, Joseph Kpobly, Fadairo,
Magou, with Zinkpé, Charly d’Almeida... Some of them
write, slam, are musicians. </p>
<p>The vodun, immutable, framework for the constructions
of the imaginary, so anchored in reality, often defines
the rela-tionship to the sensitive universe, gives the
tools of understanding, runs like a vein through the
social body of which the artist takes the imprint including himself because mirror, soothsayer or oracle, it is
a necessary part of this same body.</p>
<p>The masks are still presents, available in wood, papiermâché, recycled plastic, rubber, the Egungun, Zangbeto,
Gélédé, are transposed into paint. Their function to
protect man, to soothe tensions in the community and
keep its cohesion is also through the protection of the
environment, masks become eco-friendly</p>
<p>Sculptures in wood but also in iron, welded, twisted,
wire, braided, bent, in plastic, in cans, materials of recuperation, which are burned, dressed, disguised, nailed,
scarified, painted, veiled and unveiled, where the materials intersect, say the links, the addictions, transmit an
aesthetic and a meaning of a world still present to the
world still in shaping.</p>
<p>The painting? Earth, pigments, colours, collages, a
figurative that seeks itself, recomposes itself through
materials, motifs, symbols, sometimes also abstract,
only partitions of colours.</p>
<p>The drawing present on the canvases, also in charcoal,
pencil portraits, completes school notebooks, recreates
urban utopias, and leads out serial metamorphoses.</p>
<p>The representation of the body, the nude most often
hidden in timid sketches, hardly present in painting,
arises in body painting, extends in photography. The
female body is over-represented but some male bodies
make a not so discreet appearance. </p>
<p>The intimate is also, seldom, exposed, but when it reveals itself, it does so courageously, openly.</p>
<p>Photography is matter, is one with the walls, with the
earth, takes cults as a bulwark for vulnerability. Staging, documentary, reportage, it is reworked, one add
colors, one color to sublimate, to protect, one use black
and white also to disturb. Sto-rytelling, it retains the
ancient rituals, fixes the contemporaries, is a tool for
comparison, transmision. Retains the memory, the lost
gestures, the daily, is active, involves the public, is
material for collages, witness of an era or a city on old
prints, install a legacy.</p>
<p>The installations draw from archetypes, point out flaws,
point out new idols, give tools to rethink society, kinds
of instances that analyze, question rituals, politics, leaving the verdict to the public. Artists become ethnographers, ethnologists, anthro-pologists, sociologists,
they question language, draw inspiration from tales,
reinterpret history. Questions colonialism, post-colonialism, otherness, traces and imprints, power relations,
clichés from here and elsewhere, traces of domination,
here and elsewhere.</p>
<p>The performance denounces violence against women,
violence that degrades the environment, violence
of politics, corrup-tion, erases the names attached
to power, it is also a tool for revaluing ancestral
knowledge replaced by industries, valori-zing old traditions, reconstructing customs, costumes, the quest for
identity The crafts of old Dahomean court traditions, hanging,
bas-reliefs, recades inspire, they are revisited by pop
art, by digital, reinvented... </p>
<p>Committed, artists intervene in prisons, organize painting workshops, storytelling workshops, create activity
centres for children, and for the vulnerable. Art as therapy.</p>
<p>They are their own agents, gallery owners, event creators. They are teachers, pioneers, talent scouts.</p>
<p>It is essential to arouse the interest of the public. Artists circulate, their creations travel and contemporary
art in Benin is gaining more and more recognition. It
is present at the biennials, Johannesburg, Havana, Sao
Paolo, Shanghai, Venice, in fairs, London, 1-54, Paris
Art Fair, FIAC, AKAA in Paris, Montresso in Marrakech,
in museums in Brazil, United States, Germa-ny, Finland,
Sweden, Japan ... Galleries on 4 continents, too many
to mention.</p>
<p>Admittedly, the techniques and especially the intentions of some are still uncertain and then, as everywhere, being an artist remains an alternative economy. Artists studios are rare, as are trips outside the
continent, visas are not always granted, cultural funding, as everywhere, is meagre. Materials are expensive, the disparities with the countries of the North,
especial-ly in regard to new technologies, of digital
arts, remain significant, the condition of the artist is not
a sinecure... It remains to reflect on the art market and
its stakes, on the possibilities of alternative economies
that do not subject the artist to the market demand, on
a responsible way of creating, without adding objects
to the world but why not remove in a gesture that can
be derisory but symbolic, to privilege direct intervention, actions, involve the population without making it
a public... </p>
<p>About the writing of an African art history that sets its
own codes. On art media libraries, on an alternative
pedagogy</p>
<p>In the multitude of works that are obviously not all
of them <br>
- it is an imprint of the current global visual
27
landscape that we wanted to highlight - we note some
surprising appearances, innovative, accomplished, sensitive work, qualities that are not always accompanied
by a clearly formulated approach, personalities who assert themselves through elaborate approaches whose
work is struggling to keep pace. Their fusion raises a
thrill.</p>
<p>This urgency to be, to grasp, which like an aura marks
the presence of the artist, I have called it state of emergency</p>
<p>Thrill also in front of a few clumsy sentences that
express in a few words, simple, the years of learning,
working, becoming oneself, of an artist-artisan who
lives far from the cities, in a certainly green and beautiful countryside where life is certainly not really easy
but who lives his time by doing what he does so well
and participates by a necessary work of the spiritual
life of his community. State of emergency when the
child forbidden to create who perceived the sky as a
mysterious montage of colours devotes himself forty
years later to build a universe from scratch.</p>
<p>The graphic design belongs, like the previous one,
to Sarah Dugrip, who, playing with the constraints of
space, has skillfully taken up and transposed the artists’
work with the invaluable, unavoidable collaboration of
Bernadine Eclou, who has very provided a long and
patient work, in this book which, dense and representative, remains simple to leaf through. </p>
<p>The translation in English by Eugen Sergiu Mîrza is
the fruit of a fine and meticulous work that has relied on the cheerful contribution of Ilinca Lou Badoi.The attentive, necessary proofreading is the work
of Jacques Malgorn. Culture at work Africa, European
Union, the Benin Ministry of Culture, made this edtion
possible.  </p>
<p>We would like to thank them.
A hundred days with the artists of Benin to rediscover
and discover their work, to search for their intentions,
to guess their voices, tom imagine their constraints,
to accompany them in the places where they grew up
or dreamed, to learn sometimes the number of their
children, the original myth of their first drawings... </p>
<p>A compatriote, dear to my heart, Constantin Brâncusi,
used to say: What really makes sense in art is joy. You
don’t need to understand. What you see makes you
happy? It’s all there.
I didn’t always understand, I kept joy...</p>
