  <h3 style="color:red;">L’ÉTAT D’URGENCE</h3>
<p><img src="../resources/img/repertoire2013/plein.png" class="img-fluid ml-4"  alt="Responsive image" style="float:right;" >
  <small><strong>Une image nous captivait. Et nous ne
pouvions en sortir car elle résidait dans notre
langage et il semblait ne la répéter que de
façon inexorable .Ludwig Wittgenstein</strong> </small>
<p>En 2012, lors du premier recensement national qui
a permis l’édition en 2013 du répertoire des artistes
plasticiens et des acteurs des arts visuels du Bénin,
lorsqu’on regardait la création yeux pleins sortir
dans la rue, se cacher dans les cours mitoyennes,
on ressentait cette ébullition qu’Arttistik Afrika,
la Fondation Zinsou, l’Institut culturel français,
Kulturforum Nord-Sud, MAVA (le Musée de la Vie
Active), la Médiathèque des diasporas, Abap, Elowa,
Fagplag, Ouadada et d’autres associations culturelles
ont accompagnée. Que deux biennales Regard Bénin,
les premières Portes ouvertes des ateliers d’artistes,
Waba, le Centre Unik, l’Espace Tchif, les échanges
avec l’ESA Clermont Ferrand, ainsi que le nouveau
département des arts de l’INMAAC, le Centre culturel
chinois, la collaboration des artistes avec la dilettante,
ont entretenue. Elle s’est prolongée dans le projet
curatorial de la Galerie Vallois et la création du Centre
à Lobozounkpa en 2015, de Cotonou Creative, dans les
résidences à ENSA Dijon et la préfiguration en 2016
à Porto Novo d’une école d’art alternative, l’école de
Hogbonu, dans l’ouverture vers l’Afrique de Fresnoy,
le studio d’art contemporain de Tourcoing, dans le
partenariat entre l’Université d’Abomey Calavi et
l’Université de Cergy Pontoise.</p>
<p>Ce répertoire, fruit du recensement de 2020, intègre
259 artistes. Trente trois femmes. </p>
<p>Autodidactes, ou intégrant l’institut de formation en
art du pays ou des écoles, certaines prestigieuses
à l’étranger, coura-geuses, ces femmes artistes
explorent des formes nouvelles, relèvent leur place
dans la société, par la peinture, la photo-graphie,
la performance, la vidéo, elles initient et dirigent
des structures, s’interrogent, interrogent, cernent,
montrent, devinent les lieux et les mythes d’où elles
extraient leur substance. </p>
<p>La société, les conditionnements, les préjugés,
l’idée d’une mission dont serait investie la
femme, la maternité, la vie domestique rend ce chemin
complexe et épineux, l’ouverture n’est toujours
pas là... mais elle se laisse présager.
</p>
<p>Femmes, hommes, les artistes se forment en continu,
avec leurs pairs en ateliers, dans des écoles, échanges
et résidences, ont des métiers qui les ressourcent, ils
continuent heureusement aussi d’être autodidactes.
Le passé du continent, du pays, heurte toujours
mais ils se concentrent sur l’avenir, redessinent leur
environnement. Ils cherchent à définir leur travail, le
nommer, à peaufiner leurs intentions, leur démarche
et quitte à rompre avec le confort des habitudes, ils
cherchent à le comprendre, le renouvellent, changent
de médiums, les mixent aussi sans hésiter. </p>
<p>Les artistes lisent Franz Fanon, Joseph Beuys, Jean
Dubuffet, Cheikh Anta Diop, Paul Eluard, Bigara
Diop, admirent Picasso, Basquiat, Keith Harring, Andy
Warhol, les premiers sculpteurs du Bénin, la reine Tassi
Hangbé, les Mino (les Amazones), Sankara, Mandela,
Wole Soyinka, ils se sont formés auprès des maîtres
béninois, Joseph Kpobly, Fadairo, Magou, avec Zinkpé,
Charly d’Almeida... Certains parmi eux écrivent, font du
slam, sont musiciens.</p>
<p>Le vodun, immuable, armature pour les constructions
de l’imaginaire, si ancré dans la réalité, définit souvent
le rapport à l’univers sensible, donne les outils de
compréhension, parcourt comme une veine le corps
social dont l’artiste prend l’empreinte lui y compris car
miroir, devin ou oracle, il est partie nécessaire de ce
corps.</p>
<p>Les masques, toujours présents, sont déclinés en
bois, papier mâché, en plastique ou caoutchouc,
de récupération, Egungun, Zangbeto, Gélédé, sont
transposés parfois en peinture. Leur fonction de
protéger l’homme, d’apaiser les tensions dans la
communauté et garder sa cohésion passe aussi
par la protection de l’environnement, des masques deviennent écologiques.</p>
<p>La sculpture du bois mais aussi du fer, soudé, tordu,
en fil de fer, plié, tressé, en plastique, cannettes,
matériaux de récupéra-tion, qu’on brûle, habille, cloute,
scarifie, tatoue, voile et dévoile, où les matières se
croisent, disent les liens, les addictions, transmettent
une esthétique et un sens, celui d’un monde encore
présent au monde encore en devenir.</p>
<p>La peinture ? Terre, pigments, couleurs, collages,
un figuratif qui se cherche, se recompose au fil des
matières, des motifs, des symboles, quelques fois aussi
abstraites, uniquement partitions de couleurs. </p>
<p>Le dessin présent sur les toiles revient aussi en
portraits au fusain, avec les doodles, en illustrations,
en graphisme, au stylo, au bic, il complète des cahiers
d’écolier, recrée des utopies urbaines, enchaîne des
métamorphoses en série.</p>
<p>La représentation du corps, le nu caché le plus
souvent dans des esquisses timides, peu présent dans
la peinture, surgit dans le body painting, se prolonge
dans la photographie. Le corps féminin est surréprésenté mais quelques corps d’hommes font une
apparition pas si discrète. </p>
<p>L’intime se met à nu aussi, rarement, mais lorsqu’il se
livre il le fait courageusement, sans fausse pudeur.</p>
<p>La photo est matière, fait corps avec les murs, avec
la terre, prend les cultes comme rempart pour la
vulnérabilité. Mise en scène, documentaire, reportage,
elle est retravaillée, on rajoute des couleurs, on colorie
pour sublimer, pour protéger, on utilise le noir et blanc
pour troubler. Récit, elle retient les rituels anciens,
fixe les contemporains, est outil de comparaison,
de transmission. Retient la mémoire, les gestes
perdus, les quotidiens, s’active, implique le public,
est matériau pour des collages, témoin d’une époque,
d’une ville sur des tirages anciens, installe un héritage. </p>
<p>Les installations puisent dans les archétypes, relèvent
les tares, désignent les nouveaux idoles, donnent des
outils pour repenser la société, sortes d’instances qui
analysent, qui questionnent les rituels, la politique,
laissant le soin du verdict au public. Les artistes se
font ethnographes, ethnologues, anthropologues,sociologues, ils interrogent le langage, s’inspirent
des contes, réinterprètent l’histoire. Interrogent
colonialisme, post-colonialisme, l’altérité, les traces et
les empreintes, les rapports de force, les clichés d’ici
et d’ailleurs, les traces de domination, ici et ailleurs.
</p>
<p>La performance dénonce la violence, envers les
femmes, celle qui dégrade l’environnement, la
politique, la corruption, efface les noms attachés
au pouvoir, elle est aussi outil de revalorisation des
savoirs ancestraux remplacés par les industries,
valorisation d’anciennes traditions, reconstitution de
coutumes, de costumes, quête d’identité.</p>
<p>Les métiers d’art des vieilles traditions de cour
dahoméens, tenture, bas-reliefs, récades inspirent,
ils sont revisités par la pop art, par le numérique,
réinventés.... </p>
<p>Engagés, des artistes interviennent dans les prisons,
organisent des ateliers de peinture, des atelierscontes, créent des centres d’activités pour les enfants
et pour les vulnérables. L’art comme thérapie. </p>
<p>Ils sont leurs propres agents, galeristes, créateurs
d’événements. Enseignants, défricheurs, dénicheurs de
talents.</p>
<p>L’intérêt du public est essentiel. Des artistes circulent,
leurs créations voyagent, l’art contemporain du Bénin
acquiert de plus en plus une reconnaissance. Il est
présent aux biennales, Johannesburg, La Havane,
Sao Paolo, Shanghai, Venise, dans les foires, Londres,
1-54, Paris Art Fair, la FIAC, AKAA à Paris, Montresso à
Marrakech, dans des musées au Brésil, aux Etats Unis,
en Allemagne, au Japon et encore... Des galeries sur 4
continents, trop nombreuses à citer.</p>
<p>Certes, les techniques et les intentions de certains
sont encore incertaines et puis, être artiste, comme
partout, reste une économie alternative. Les ateliers
de travail sont rares comme les voyages à l’extérieur
du continent, les visas ne sont pas toujours accordés,
les financements culturels, comme partout, sont
maigres. Les matériaux sont chers, les disparités
avec les pays du Nord surtout en ce qui concerne
les nouvelles technologies, les arts numériques,
restent importantes, la condition d’artiste n’est pas
une sinécure... Il reste à réfléchir sur le marché de </p>
<p>l’art sur les économies alternatives de l’artiste qui
ne l’assujettissent pas à la demande du marché, sur
une manière responsable de créer, sans rajouter
des objets au monde mais pourquoi pas lui enlever,
dans un geste peut être dérisoire mais symbolique, à
privilégier l’intervention directe, les actions, impliquer
la population sans en faire un public... Sur l’écriture
d’une histoire de l’art de l’Afrique qui fixe ses propres
codes. Sur des médiathèques d’art, sur une pédagogie
alternative.</p>
<p>Dans la multitude d’œuvres qui n’en sont évidemment
pas toutes <br>
 – c’est une empreinte du paysage visuel
global actuel qu’on a voulu relever <br>
 - on note quelques apparitions étonnantes, un travail novateur,
abouti, sensible, qualités qui ne sont pas toujours
accompagnées d’une démarche clairement formulée,
des personnalités qui s’affirment par des démarches
élaborées, auxquelles le travail a du mal à emboîter
les pas. Leur fusion lève un frémissement.</p>
<p>Cette urgence d’être, de saisir, qui comme une aura
marque la présence de l’artiste je l’ai appelée l’état
d’urgence.
</p>
<p>Frémissement devant quelques phrases maladroites
qui expriment en peu de mots les années
d’apprentissage, de travail, de devenir soi, d’un
artiste-artisan qui vit loin des villes, dans une
campagne certainement verte et belle où la vie n’est
certainement pas vraiment facile, qui vit son temps
imparti en faisant ce qu’il sait si bien faire et participe
par un travail nécessaire de la vie spirituelle de sa
communauté. </p>
<p>Etat d’urgence quand, interdit de créer, l’enfant qui
percevait le ciel comme un mystérieux montage de
couleurs se consacre quarante années après à monter
un univers de toutes pièces. </p>
<p>La conception graphique appartient tout comme
la précédente à Sarah Dugrip, qui se jouant des
contraintes d’espace, a relevé et transposé avec
habileté le travail des artistes avec la collaboration
précieuse, incontournable, de Bernadine Eclou qui a
fourni un travail long et patient, dans cet ouvrage qui
dense et représentatif, reste simple à feuilleter. </p>
<p>La traduction en anglais d’Eugen Sergiu Mîrza, fruit
d’un travail fin et minutieux, s’est appuyée sur la
contribution allègre d’Ilinca Lou Badoi. La relecture
attentive, nécessaire revient à Jacques Malgorn.
Nous les remercions.</p>
<p>Une centaine de jours avec les artistes du Bénin, à
redécouvrir et découvrir leur travail, au fil de leur
intentions, à deviner leurs voix, à imaginer leurs
contraintes, à les accompagner dans les lieux où ils
ont grandi ou rêvé, à apprendre parfois le nombre de
leurs enfants, le mythe d’origine de leurs premiers
dessins... </p>
<p>Un compatriote cher à mon cœur, Constantin
Brâncusi disait : Ce qui a vraiment un sens dans
l’art, c’est la joie. Vous n’avez pas besoin de
comprendre. Ce que vous voyez vous rend
heureux ? Tout est là. </p>
<p>Je n’ai pas toujours compris, j’ai eu la joie.</p>
