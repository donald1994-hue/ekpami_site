  <h3 style="color:red;">RETOUR EN FORCE DE L’ARTISTE PLASTICIEN </h3>
<p><img src="../resources/img/HAUT/mozard.png" class="img-fluid ml-4" width="100px"  alt="Responsive image" style="float:right;" >
  Le projet ENAKPAMI 2, entendu 2ème Expérience Nouvelle
  d’Assistance Kyrielle aux Plasticiens pour leur Ascension
  via le Multimédia et l’Internet, est initié par l’Association
  pour le Développement des Arts Visuels dans le Nord Bénin
  (ADAV-NORD BENIN) et l’Association Béninoise des Arts
  Plastiques FOUR P (ABAP-FOURP), du Sud Bénin. Ce projet
  repose sur diverses formes de partenariat déjà existant dans
  une approche participative avec ces deux associations très
  engagées renforçant ainsi la coopération Nord-Sud.
</p>
<p>L’Association Béninoise des Arts Plastiques FOUR P (ABAPFOURP) a eu à réaliser en 2013 le 1er recensement national
ainsi que le répertoire des artistes plasticiens grâce à son
projet d’appui à la filière arts visuels au Bénin (PAFAVBENIN) sur financement de l’Union Européenne (10ème FED)
à travers le Programme Société Civile et Culture (PSCC). Le
projet ENAKPAMI 2 consolide et étend les résultats du projet
PAFAV-BENIN en réalisant cette fois-ci le 2ème recensement
national des artistes plasticiens puis des acteurs puis en
actualisant le présent répertoire.</p>
<p>Ce 2ème recensement national a été réalisé dans les douze
départements du Bénin et a permis d’enregistrer à ce jour
370 artistes plasticiens dont 35 femmes, 50 associations, 43
lieux ou espaces de promotion, 05 historiens, 20 journalistes
et 07 collectionneurs. Ce 2ème répertoire élaboré au terme de
l’inventaire national, propose une présentation identique à
tous les artistes :
</p>
<p>_ La présentation en couleur de quelques œuvres de
chaque artiste ; le choix a été effectué, dans la mesure du
possible, directement en contact avec l’artiste lui-même pour
approcher au plus près ses préoccupation et pour fournir une
documentation originale et représentative.
_ Une photo personnelle de l’artiste</p>
<p>_ Une courte présentation biographique en français avec une
traduction en anglais et comportant les points de repère
nécessaires à la connaissance  : lieu ou l’artiste s’séjourne
et travaille, date de naissance, formation, date de première
exposition personnelle, appartenance, quand elle existe, à
un groupe, un mouvement, une école de pensée, relation au
contexte historique (guerres, événement politiques) quand
celui-ci influe sur le parcours de l’artiste. Ces textes sur le
travail des artistes, ont été rédigées pour la majorité par
un seul auteur, afin d’offrir au lecteur une unité de style, de
ton et de traitement des informations. Chaque fois que cela
était possible, l’auteur a pris contact directement avec les
artistes pour bénéficier de la documentation la plus fiable et
la plus actuelle possible, ainsi que de l’iconographie la plus intéressante.</p>
<p>_ Des adresses fonctionnelles
(téléphones, numéros
whatsaps, mails) </p>
<p>_ Lieux de résidence ou de travail</p>
<p><strong>Le site internet www.beninartsvisuels.org, actualisé,
comporte cette base de données récente, fiable et complète
sur ces acteurs de la filière arts visuels au Bénin.</strong> Ce site
fonctionne comme une plateforme application sur laquelle,
les artistes peuvent désormais s’inscrire ou actualiser leurs
données à travers leurs Smartphones tout en restant chez
eux</p>
<p>Je rends ici un grand hommage à Yves PEDE, Koffi
GAOU, Grégoire NOUDEHOU, Osée Konon Issa, Damien
TOKOUDAGBA, Eunock HOUNKPÉVI, Mica HOUNPÉVI etc.
qui ont commencé l’aventure avec nous et ne sont plus restés
jusqu’à cet instant.</p>
<p>Je remercie tous ceux qui ont contribué de diverses
manières à la réalisation de ce travail en particulier
l’Union Européenne, Culture At Wor Africa (C@WA) avec ses
partenaires, le Ministère du Tourisme, de la Culture et des
Arts, et surtout, tous les artistes plasticiens de notre pays
qui nous ont confirmé une fois encore par leur implication,
par leur collaboration, qu’ils sont toujours prêts à relever les
défis, prêts à participer au développement des arts visuels
du Bénin.</p>

