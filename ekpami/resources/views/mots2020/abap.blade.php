  partners, the Ministry of Tourism, Culture and Arts,
  and above all, the visual artists of our country who
  confirmed us once again by their involvement, by their
  collaboration, that they are always ready to take up
  the challenges, ready to participate in the development of visual arts in Benin.
  </p>
