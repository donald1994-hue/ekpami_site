<h3 style="color:red;">VISUAL ARTIST’S POWERFUL COMEBACK</h3>

<div class="" style="color: #53a5cb;">
  <p>The ENAKPAMI 2 project, understood as 2nd New
  Experience of Kyrielle Assistance to Plastic Artists
  for their Ascension via Multimedia and Internet, is
  initiated by the Association for the Development of
  Visual Arts in Northern Benin (ADAV-NORTH BENIN)
  and the Beninese Association of Plastic Arts FOUR P
  (ABAP-FOURP), from Southern Benin.</p>
  <p>The Association Béninoise des Arts Plastiques FOUR
  P (ABAP-FOURP) carried out in 2013 the 1st national
  census as well as the 1st directory of visual artists
  thanks to its project of support to the visual arts
  sector in Benin (PAFAV-BENIN) funded by the European Union (10th EDF) through the Civil Society and
  Culture Programme (PSCC).</p>
  <p>The ENAKPAMI 2 project consolidates and extends
  the results of the PAFAV-BENIN project by carrying out
  this time the 2nd national census of visual artists and
  actors and then by updating the repertoire.</p>
   <p>This 2nd national census was carried out in the
  twelve departments of Benin and has made it possible
  to register to date 370 visual artists including 35
  women, 08 federations, 47 associations, 50 places
  or spaces of promotion, 05 historians, 21 journalists
  and 07 collectors. This second repertory, drawn up at
  the end of the national inventory, offers an identical
  presentation to the 284 participating artists:</p>
  <p>_ A color presentation of a few works by each artist:
  the choice was made, as far as possible, in direct
  contact with the artist himself to get a closer look at
  his concerns and to provide original and representative documentation. <br>
  _ A personal photo of the artist <br>
  _ A presentation in French with its translation into
  English including the place where the artist stays and
  works, the year of his birth, and whenever possible
  the reference points necessary to the knowledge of
  his work: biographical dates, training, the various
  influences on the artist’s career. These texts on the
  work of artists have been written for the most part
  by a single author, in order to offer the reader a unity
  of style, tone and treatment of information. Wherever
  possible, the author has contacted the artists directly
  in order to obtain the most reliable and up-to-date
  documentation and the most interesting iconography.
  Functional addresses: phone numbers, WhatsApp,
  email addresses, websites...)</p>
  <p>The website www.beninartsvisuels.org, updated,
  contains this recent, reliable and complete database
  on these actors of the visual arts sector in Benin. This
  site includes an application platform on which new
  artists can now register from their smartphones while
  staying at home.</p>
  <p>I pay here a great tribute to Yves Pede, Koffi Gaou,
