  <h5 style="color:red;">…LES ARTISTES CONTEMPORAINS AU BÉNIN
D’UNE DÉCENNIE À L’AUTRE… </h5>
<p>Ce texte, plutôt bref, Il propose, grâce à une comparaison
simple et rapide des principales tendances que l’on peut
déceler dans les œuvres des artistes contemporains
béninois, une meilleure compréhension de ce qui
se produit sur la scène artistique contemporaine de
ce petit pays de l’Afrique de l’Ouest où les artistes
bourgeonnent comme de la mauvaise herbe sans
que l’on ne puisse évoquer pour leur présence la
multiplicité des écoles de beaux-arts ou encore des
facilités particulières offertes aux personnes de cette
catégorie par l’état, bien au contraire. Le texte permet
de constater un approfondissement des tendances
qui pourraient désormais être considérées comme
les soubassements d’un art contemporain qui devrait
aller grandissant et étonnant toujours les observateurs
et critiques. Il est vrai que l’esprit souffle où il veut et
nos artistes semblent bénéficier de sa faveur encore et
pour longtemps. </p>
<p>Il aurait fallu plus de temps pour approfondir la
méditation nécessaire à la mise en exergue des
principales qualités de ces œuvres de l’esprit, qui,
tel un vent, souffle où il veut, inspirant à sa guise
jeunes et moins jeunes pour la joie des amateurs, des
collectionneurs et de tous les amis de l’art. L’analyse
porte ici sur des œuvres déjà connues grâce au
premier répertoire daté de 2013 et sur les nouvelles à
découvrir dans le second répertoire de 2020. Puissiezvous prendre du plaisir en compagnie de ces talents
béninois dont le seul souci est de vous retenir auprès
d’eux .</p>
<p>
  <strong>1- 2013</strong>
  <p>Le premier répertoire des
artistes et plasticiens du Bénin et qui
se veut aussi un répertoire des artistes visuels de ce
même pays a assouvi sa noble ambition de prouver
que désormais, dans le plus petit « bled », que l’on soit
au sud, au nord ou au centre, dans les villes surpeuplées
où la fébrilité est évidente ou dans les faubourgs plutôt
calmes et paisibles, un acteur du monde des arts, un
plasticien est à l’œuvre qui n’attend que notre visite.
Mais mieux, ce que ce premier répertoire affirme, c’est
que la communauté des plasticiens ici est d’abord une
population de peintres, c’est-à-dire d’hommes qui ont
voulu apprendre des leçons d’hier : ce pays n’est pas
un pays de la sculpure, du moins pas de celle qui a
séduit le monde au XIXème siècle, que l’on a appelé la
« grande sculpture » que l’on retrouve dans les zones
de grandes forêts que sont la Côte-d’Ivoire, le Gabon,
l’actuelle République Démocratiquue du Congo : point
de grands masques blancs, pas de sculptures géantes
avant le 19ème siècle. Ne pouvant s’appuyer sur ce qui
d’habitude est la référence, il fallait trouver autre
chose, les artistes et plasticiens du Bénin se sont alors
tournés vers la peinture, et le patrimoine de leurs
ancêtres. Ils n’ont pourtant pas oublié entièrement la
sculpture et si le bois leur fait de plus en plus défaut,
c’est vers le métal qu’ils tourneront leurs amours tant
il est vrai que le dieu Gu fut créé au dix-neuvième
siècle par leur ancêtre dans le métal au lieu du bois.
Mais à la guerre où il devait aller, au front, le feu des
canons aurait vite eu raison de son corps végétal. Ainsivoyez-vous, si Rémy samuse, c’est avec le métal qu’il le
fait ; les coiffures de Marius DANSOU disent dans les
lamelles et tiges de métal, la beauté des chevelures
noires autrefois souples comme des cordes, référence
essentielle dans cette culture où c’est au bout de
l’ancienne corde qu’on tresse la nouvelle. Les frères
Dakpogan n’ont pas fini, à partir de la récupération
des récipients émaillés et autres de nous séduire par
nombre de créations qui intègrent tous les règnes.
D’autres jeunes se sont engagés à leur suite et la scène
internationale les a consacrés dans leur métier</p>
<p>L’autre élément que les plasticiens béninois n’ont
pas oublié et qui affleure partout dans la plupart
des œuvres du répertoire 2013, c’est la spiritualité
que leur a léguée leurs ancêtres : presque toutes les
œuvres sont estampillées du signe du vodun, discret
ou clairement représenté comme si l’homme ici ne
peut se dire que par lui. faut-il rendre responsable
de cette prégnance le fait que tous nos artistes sont
autodidactes et qu’ils nous livrent sans fard ni masque
le fond de leur âme ou regarder le vodun comme un
refuge identitaire qui permet aux jeunes créateurs de
s’assurer un label et une différence par rapport aux
créateurs des autres pays du continent  ? la mise en
place d’une école des beaux-arts nous permettra très
vite sans doute de trouver les véritables raisons de ce
qui malgré tout demeure une forte marque identitaire
des arts plastiques béninois.</p>
<h5> <strong>Quand la récupération est reine absolue</strong> </h5>
<p>L’artiste contemporain d’ici est roi de la récupération ;
certes en recourant à un matériel qui a déjà vécu,
en choisissant de lui faire vivre une autre vie bien
meilleure, puisqu’il a vocation à côtoyer les grands de ce monde, il s’investit dans la lutte pour un
environnement plus sain. Mais plus fondamentalement,
la recherche du « déjà utilisé » l’amène à chercher le
second souffle pour lui-même et à vouloir aller au fond
de l’âme de la matière qu’il utilise et qui raconte en
son corps plusieurs histoires qui se superposent dans
le silence qu’il doit briser.</p>
<p>Dans ce répertoire, il n’est pas rare que le plasticien
recoure encore à ce que nous nommons des lieux
communs, ceux que l’on retrouve au commencement
de tous les arts plastiques se mondialisant  : il peint
des paysages, des monuments rendus célèbres par les
touristes en quête de souvenirs mais c’est en attendant
que sa personnalité profonde émerge et qu’il oublie
qu’il est passé par là... Qui de nous n’a pas eu ses
heures noires ou balbutiantes ? Aussi, murît-il vite au
contact de la scène internationale et de ses multiples
exigences multiples de dépassement. </p>
<p>Nous parlons d’art contemporain, et où que l’on soit, les
moyens de la monstration ne sont pas innumérables.
L’artiste contemporain doit exposer, des peintures et
tableaux ou des sculptures ; il passe dans ce cas de la
surface au volume ; il peut aussi choisir de faire une
installation, pour se donner une plus grande liberté
avec les genres classiquement attendus. Le choix est
donc restreint et le plasticien béninois n’a pas plus de
choix que les autres dans le monde. La nature réelle
de sa pratique apparaît donc mieux dans ces genres.
Oui, il peint, et le contact avec le monde extérieur, la
facilité désormais offerte de se procurer à prix d’or ou
contre trois fois rien les mêmes matières premières
et outils que ses collègues d’Europe et d’ailleurs par
exemple rend compte de ses envies. La couleur ici
est abondante ; elle nous vient surtout de l’acrylique, voyante, violente, fort différente des teintes douces
et nuancées obtenues autrefois par divers procédés
de teinture dont le secret se perd rapidement face
à la modernité, comme si on ne gagnerait pas à
sauvegarder les traditions et techniques picturales
d’hier. Certes, il y a quelques artistes qui s’essaient,
à partir des pigments locaux, mais l’effort que ceci
représente et les risques à prendre avec l’image
contraignent sans doute à préférer la facilité du tout
prêt dans les villes qui sont presque toutes pourvues
d’usines de production de peinture acrylique. Comme
on peut s’y attendre, toutes les techniques que l’on
rencontre dans le monde des arts se retrouvent bien ici
où elles sont chez elles ; les peintres béninois peignent
au couteau, mais ils n’hésitent pas à se servir de leurs
mains aussi comme pour transmettre directement
l’énergie et le magnétisme de leur corps à la matière
inanimée sur laquelle ils doivent œuvrer. N’oubliez pas
que nous sommes au pays des vodun et ici, tout est
dans le subtil, dans une vibration que vous pouvez ne
pas percevoir mais qui ne manquera certainement pas
de vous affecter. </p>
<h5> <strong>Une esthétique de l’accumulation</strong> </h5>
<p>L’art tout d’abord s’entend comme la volonté de
créer une pièce unique  ; copier, répéter, dupliquer
a toujours fait sortir du champ de l’art. Pour l’artiste
béninois, depuis toujours et plus particulièrement
pour l’artiste contemporain, sous l’influence peutêtre des esthétiques vodun si fortement présentes,
l’accumulation a été et reste un trait fort de la
créativité  ; un élément tout Seul est insuffisant
pour créer l’émotion attendue du spectateur en art
plastique  ; il en est de même des sons par exemple
où la répétition est obligatoire même si elle est jugée ennuyeuse dans la culture occidentale ; ajouter une
forme à une autre, une figure à une autre permet de
renforcer son effet ; c’était vrai dans le passé, cela le
reste encore aujourd’hui où on a fini par reconnaître
l’importance des fractales Amener le spectateur
à se confronter à des œuvres qui peuvent au besoin
lui servir de référence pour remplacer progressivement
des images issues d’un passé révolu pour d’autres
appelées à se renouveler plus rapidement au fil du
temps qui court désormais tout en restant toujours un
grand maître. </p>
</p>
<h5> <strong>2. 2020</strong> </h5>
<h5> <strong>Encore un pas …</strong> </h5>
<p>Aimé Césaire que je cite de mémoire nous a dit qu’il
fallait un pas, un autre pas, encore un autre pas et
tenir pour gagné chaque pas… Tel est le principe
auquel se conforme l’ABAP en se lançant dans la mise
au point d’un second répertoire des artistes du Bénin.
Cette tentative pour faire connaître l’art contemporain
béninois et ses acteurs est indispensable pour ne pas
toujours rester dans l’ombre. La pleine lumière projetée
ainsi de façon continue et persistante sur les œuvres de
l’esprit de nos contemporains, nous permet de mieux
nous connaître comme elle donne une chance à chacun
d’eux de se faire découvrir ailleurs que dans leur propre
pays. Mais il ne faut pas croire que tout fut aisé. </p>
<p>En effet, le deuxième répertoire des artistes et
plasticiens béninois ne semble pas avoir suscité le
maysême enthousiasme que le premier, si l’on en croit
le nombre d’inscrits. Il confirme pourtant, à l’examen,
les grandes tendances et orientations décelées depuis
le premier qui a eu la force de rassembler les artistes
vivant sur le territoire national et les autres, de la </p>
<p>diaspora, mais qui se sentaient par l’âme et l’expression
d’ici  ; ce brassage des idées et des personnalités a
donné comme résultat un premier répertoire où l’on
peut affirmer que l’art « contemporain » au sens où il
est entendu sur la scène internationale a commencé à
prendre sa place, toute sa place dans notre pays. </p>
<p>Il est en effet attendu de l’artiste contemporain
qu’il assume, en totalité sa création et sa créativité ;
ainsi le recours à la tradition, aux us, coutumes et
leur translation pour le visuel dans les œuvres d’art
deviennent moins contraignants ; l’art n’est plus appelé
à être le miroir d’un quelconque patrimoine mais le
signe d’un dialogue de séduction entre l’artiste et ses
publics d’ici et d’ailleurs.…</p>
<p>Il est loisible de se demander dans quelles conditions
une telle crase se réalise-t-elle ;faut-il une catégorie
particulière d’artistes, car en fait dans la réalité, la
condition des artistes n’a guère vraiment changé
depuis le premier répertoire  ; ils continuent d’être
pour la plupart des autodidactes, formés le plus
souvent sur le tas, obéissant principalement à leur
propre inspiration et à leur volonté à vrai dire à leur
passion d’être «  artistes  » et de le demeurer plutôt
qu’aux règles et prescriptions académiques d’une
institution de formation à laquelle se substituent
depuis bien longtemps quelques ateliers de leurs
aînés, pompeusement appelés les « doyens », ateliers
qu’ils listent prestement comme les centres de leur
initiation artistique sans diktat  ; la population est
essentiellement masculine, mais il convient d’observer
que les rares femmes qui osent troubler la scène de
leurs ébats et de la fougue de leur jeunesse audacieuse
seront bien capables d’ici quelques années de tenir
la dragée haute à leurs collègues, tant leur mordant est grand. On ne sait leur reprocher leur apparente
insolence, celle-ci a toujours été la porte du succès.</p>
<p>Mais l’art contemporain est aussi le lieu des rencontres
et des influences qu’on subit et assimile dans une
volonté de dépassement ou d’enrichissement de
l’héritage commun aux artistes. Le mimétisme y est
certainement pour quelque chose. </p>
<p>Tenez une de nos artistes contemporains est devenue
experte en «  drapés  », non pas celui des revenants
dont le jeu de toge continuera de séduire pendant
longtemps, mais de monuments publics et de grands
bâtiments nationaux, dans leur propre pays ou
ailleurs, ; inutile de vous demander si le public béninois
y comprend quelque chose, oublieux que les rois du
Danxomè avaient l’habitude de couvrir de grandes
toiles appliquées ornées de motifs, les toits de leurs
palais pour les fêtes des coutumes. Il n’y a pas de doute
que notre artiste n’a pas puisé son inspiration de ce
passé historique, si éloigné de la modernité… </p>
<p>On peut donc penser qu’il y a bien une jeune génération
d ’artistes qui, sans se refuser à aller vers son propre
destin, s’assure une rupture lente et claire avec les
aînés, consciente qu’un public nouveau désormais est
là qui frappe aux portes et attend d’être séduit. Cette
nouvelle génération, surtout lorsqqu’elle affirme
être issue d’une école, en donne les preuves, non pas
nécessairement dans le choix des thèmes, mais surtout
dans le traitement graphique des figures qui sont plus
soignées ; la géométrie des formes apparaît clairement
et la couleur qui est la </p>
<p>raison d’être de la peinture par exemple est utilisée
pour créer, à elle seule le mouvement. La volonté
clairement affichée de prendre ses distances envers unpatrimoine ne la pousse pourtant pas à cracher sur ce
qui toujours restera sacré. Les anciens rois du Danxomè
seront toujours peints, sculptés, mais Béhanzin
chaussera des lunettes, le confondant presque aux
intellectuels qu’il n’a pas connus et auxquels il ne se
serait jamais confondus.</p>
<p>L’artiste contemporain joue aussi avec les mots et
prend part de cette manière aux plus grands débats
contemporains. De Lègba, le « glébeux », mais connu
pour être l’intermédiaire entre les dieux et les
hommes, Aplogan Edwidge fait un être parcouru de
lumières multicolores, tout le contraire de la motte de
terre qui habituellement le représentait couronné de
deux cornes qui font plus de lui un combattant qu’un
messager.</p>
<p>La disponibilité des moyens techniques autorise
aussi les nouvelles formes de notre art contemporain.
Autrefois, sans les masques du Gèlèdè, il n’y avait
pas d’art, du moins pas de sculpture digne de ce
nom  ; aujourd’hui, et le répertoire le montre bien,
les sculpteurs de masques Gèlèdè n’ont plus la
première place et pourtant…Grâce à la technologie
contemporaine, la sculpture passe par les machines ;
plus aucun bois n’est impropre et les plus durs, de
couleur rougeâtre, se prêtent à des représentations
dignes des anciennes sculpteurs où on ne voit plus
seulement la tête, mais tout le personnage du danseur. </p>
<p>Les mutations sont donc évidentes et les changements
de mentalité se lisent dans des œuvres chaque fois
plus audacieuses les unes que les autres dont certaines
défient largement les capacités d’hier. Le sculpteur
contemporain est devenu un joueur, et la sulpture plus
ludique…</p>
<p>Le propre de l’art, c’est de raconter une histoire. Dans
l’art contemporain béninois, l’histoire que l’œuvre
raconte est au-delà de ce qui apparaît au regard,
dans l’immédiateté  ; elle demande aujourd’hui, en
permanence une reconstruction, laissée au soin de
celui qui regarde, observe, se laisse séduire.</p>
<p>On comprend aisément qu’il s’agisse d’histoires
multiples, changeantes en fonction de tout l’imaginaire
et de toute la culture dont l’amoureux d’art est
détenteur, toutes aussi belles les unes que les autres. Il
est tout simplement heureux que nos artistes se soient
si bien insérés dans le temps qui change pour se saisir
en permanence des nouveaux outils pour partager
et faire connaître notre imaginaire qui lui aussi doit
probablement continuer d’étonner et de faire admirer
les dons de nos artistes contemporains.</p>
