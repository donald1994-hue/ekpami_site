<div class="" style="color: #53a5cb;">
  <h3 style="color: red;">ART: A TOOL FOR PERFORMANCE</h3>
  <p>The new Catalogue of Visual Artists of Benin and professionals of visual arts in Benin, which you are currently browsing,
  is a precious tool. It presents two-hundred and fifty-nine
  visual artists, with their techniques made of a specific use
  of material, form and support and carried by the breath of
  their inspiration. It is the new national census and the recognition of their milieu, established over time by professionals
  in the visual arts and also by the community, confirmed by
  people who accompany them in their artistic quest that have
  brought them together in this book.</p>
  <p>Presenting contemporary art in Benin in all its diversity, the
  new edition of this repertoire, unanimously acclaimed at its
  official launch in 2013, brings together essential information
  on the current affairs of visual artists. It is a real guide to
  contemporary art in Benin, essential for professionals and
  people in the field of art, whether they are artists, managers
  of museums, contemporary art centers, professional associations.</p>
  <p>The book will also be of great use to curators, organizers of
  exhibitions and artistic events, students and teachers, professors of universities and art schools, publishers of books
  and specialized magazines. As for the public authorities and
  local authorities, they will have a reliable database on the
  operators in the field in their municipalities or departments</p>
  <p>By supporting the publication of this revised and updated
  version, we encourage the creativity of our artists and support the role of «culture brokers» that they are. We rediscover
  here those who, since 2013, have already been referenced,
  while at the same time discovering the young people of the
  rising generation. The styles have become more refined for
  some, they have redeployed towards other centres of interest
  and themes for others, while for some still, they are beginning to emerge and assert themselves.</p>
  <p>This is an opportunity to salute the bridge that this collection creates between all the professionals of the field and
  the potential partners that it can mobilize, whether private,
  associative or institutional. Beyond its informative content,
  this book offers the reader fairly faithful visuals of the artists’ works.</p>
  <p>But we are obliged to stick to the one impression they suggest to us.The images chosen have succeeded in capturing
  the paintings, sculptures and other works of the visual artists
  in their completed forms. However - and this is their limit
  - they do not manage to offer the spectator the different sensations that only a real close contact can provide in his trade
  and his approach to the work. For this book does not pretend
  to substitute itself for the works. It only gives a glimpse of
  the various talents listed in it, which, incidentally, should
  encourage those who want to know more to go directly to
  meet the artists. </p>
  <p>This new catalogue is neither fixed nor definitive. If it is a
  snapshot of what exists, it is however not an exhaustive
  platform of all the contributors to the expressions of Benin’s
  plastic arts. It is for this reason that it will be subject to permanent updating. Especially since, on the website dedicated
  to it, the same information will be available. It is desirable
  that this site becomes a truly collaborative and interactive
  tool welcoming information and suggestions from Internet
  users.</p>
  <p>We owe this work to the fruitful collaboration between two
  dynamic and very committed associations in Benin: the Association Béninoise des Arts Plastiques and the Association
  pour le Développement des Arts Visuels dans le Nord Bénin
  (ADAV-NORD BENIN). So, we salute once again the Benin-European Union cooperation and thank Culture At Work Africa
  (C@WA) and its partners who have recognized the relevance
  of the ENAKPAMI 2 project through which this repertoire has
  been updated.</p>

  <p>Dear readers, as you read through the pages, you will discover talents who are working to express a sector of extraordinary potential. You will also discover that they constitute,
  along with artists from other fields, the intelligent mediators
  who contribute to the blossoming of culture, and thus to the
  development of Benin.</p>
