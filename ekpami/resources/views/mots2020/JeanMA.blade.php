<div class="text" style="padding: 20px; text-align: justify;">
  <h3 style="color:red;">L’ART : UN OUTIL DE PERFORMANCE</h3>
<p><img src="../resources/img/HAUT/JMA2020.JPG" class="img-fluid ml-4" width="100px;" alt="Responsive image" style="float:right;" >
  Le nouveau Répertoire des artistes plasticiens du Bénin
et des acteurs des arts visuels du Bénin que vous parcourez en ce moment, est un précieux outil. Il présente
deux cents cinquante neuf plasticiens du Bénin, avec
leurs techniques faites d’une utilisation spécifique de la
matière, de la forme et du support et portés par le souffle
de leur inspiration. C’est le nouveau recensement national
et la reconnaissance de leur milieu, établie au fil du temps
par des professionnels des arts visuels et aussi par la
communauté, confirmée par des personnes qui les accompagnent dans leur quête artistique qui les ont réunis dans
cet ouvrage.</p>
<p>Présentant l’art contemporain au Bénin dans sa diversité,
la nouvelle édition de ce répertoire unanimement salué
lors de son lancement officiel en 2013, rassemble des
informations essentielles sur l’actualité des plasticiens.
C’est un véritable guide de l’art contemporain béninois,
indispensable aux professionnels et aux personnes du
domaine de l’art, qu’ils soient artistes, responsables de
musées, de centres d’art contemporain, d’associations professionnelles.</p>
<p>L’ouvrage sera aussi d’une grande utilité aux curateurs,
aux organisateurs d’expositions et évènements artistiques, aux étudiants et enseignants, aux professeurs des
universités et écoles d’art, aux éditeurs d’ouvrages et de
revues spécialisées. Quant aux pouvoirs publics et autorités locales, ils disposeront d’une base de données fiable
sur les acteurs du secteur dans leurs communes ou leurs
départements.  </p>
<p>En soutenant la publication de cette version revue et
actualisée, nous encourageons la créativité de nos artistes et apportons notre caution au rôle de « passeurs
de culture » qu’ils sont. On redécouvre ici ceux qui, depuis
2013, ont été déjà référencés en même temps qu’on découvre les jeunes de la génération montante. Les styles
se sont affinés pour les uns, ils se sont redéployés vers
d’autres centres d’intérêts et thématiques pour d’autres,
tandis que pour certains encore, ils commencent à émerger et à s’affirmer.</p>
<p>C’est l’occasion de saluer la passerelle que ce recueil
permet de créer entre tous les acteurs de la filière et
les potentiels partenaires qu’il peut mobiliser, qu’ils
soient privés, associatifs ou institutionnels. Au-delà de
son contenu informatif, cet ouvrage offre au lecteur des
visuels assez fidèles des
œuvres des artistes. Les
images choisies ont réussi à
saisir les tableaux, les sculptures
et les autres travaux des plasticiens
dans leurs formes achevées. Cependant – et c’est là leur
limite – elles n’arrivent pas à offrir au spectateur les différentes sensations que seul un corps à corps réel peut lui
procurer dans son commerce et son contact avec l’œuvre.
Mais on est obligé de s’en tenir à cette seule impression
qu’elles nous suggèrent. Car ce livre n’a pas la prétention
de se substituer aux œuvres. Il ne donne qu’un aperçu des
différents talents qui y sont recensés, ce qui, au demeurant, doit pousser ceux qui veulent en savoir davantage à
aller directement à la rencontre des artistes.  </p>
<p>Ce nouveau répertoire n’est ni figé, ni définitif. S’il est une
photographie instantanée de l’existant, il n’est cependant
pas une plateforme exhaustive de tous les contributeurs
aux expressions des arts plastiques du Bénin. C’est pour
cette raison qu’il sera objet d’une actualisation permanente. Surtout que, sur le site web qui lui est consacré,
ces mêmes informations seront disponibles. Il est souhaitable que ce site devienne un outil vraiment collaboratif
et interactif accueillant informations et suggestions de la
part des internautes.</p>
<p>Ce travail, nous le devons à la collaboration fructueuse
entre deux associations dynamiques et très engagées au
Bénin : l’Association Béninoise des Arts Plastiques et l’Association pour le Développement des Arts Visuels dans le
Nord Bénin (ADAV NORD BENIN). Aussi, saluons nous une
fois encore la coopération Bénin Union Européenne et
remercions Culture At Work Africa (C@WA) et ses partenaires qui ont reconnu la pertinence du projet ENAKPAMI
2 par le biais duquel ce répertoire a été actualisé. </p>
<p>Chers lecteurs, au fil des pages, vous découvrirez des
talents qui œu-vrent à l’expressivité d’un secteur au
potentiel extraordinaire. Vous dé-couvrirez aussi qu’ils
constituent, avec les artistes des autres domaines, les médiateurs intelligents qui contribuent à l’épanouissement
de la culture, et partant, au développement du Bénin.</p>

