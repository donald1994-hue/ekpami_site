            <p>‘‘When I work, I’m not afraid of dama-ging the canvas, I let the painting take on  its  full  meaning.  I  concentrate  in  the  imagination.  I  go  to  meet  the  spirits  and  I  come back with the writing and the voice of the disappeared’’. Youssouf Atacora comes back and tells his story of Africa, brings back emblematic characters from the continent and the world. Victor  Schoelcher,  Angela  Davis,  Muhammad  Ali,  Nina  Simone,  De Gaulle, Thomas Sankara... Their radiance, their talent, their wanderings and commitments, impart an impetuous movement to his gesture which launches ‘‘the multicoloured tones, vivid as a new life, like a bouquet of flowers to the dead’’. Monuments, sometimes  declined  as  the  last  king  of  Danxome,  Behanzin  I,  pop art, period of glory, Behanzin II, figurative adrift, tarnished colors,  defeat,  exile  and  Kondo  the  Shark,  extract  colors  and  forms,  purify  until  touching  his  spirit,  recreates  a  character,  sublimation of a resistance. ‘‘Monuments’’ or collections of old walls on cardboard, painted wood assemblages, games with the frame  of  the  painting  and  with  the  void.  The  rainbow  colours  mutate,  less  decorative,  lose  their  figurative  rage  and  hold back, the compositions become interior, more daring when he releases  his  drawing.  Youss  Atacora  has  the  extravagance  of  the verb, the extravagance of the colour, has given a colourful, striking name to his style. ‘‘stupid realism or, follies of reality’’. Follies of reality, canvases in the shape of a scream borrow the fullness of a sound, a kind of squealing, familiar ghosts pushing a door. Memory opens. F.B
            </p>
