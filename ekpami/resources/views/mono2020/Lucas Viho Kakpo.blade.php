            <p>Parfait Gbogbé works to make the imaginary « more concrete ». French  teacher,  he  is  concerned  about  the  future  of  the  new  generation  and  participates  in  workshops  to  introduce  child-ren  to  the  decryption  of  paintings  through  games,  to  streng-then their knowledge of the country, history and traditions in a playful way. His canvases, cracked surfaces, porous surfaces where  he  inserts  sand  and  puts  colors  or  paint,  multicolored  abstractions,  look  like  maps  seen  from  the  sky,  other  com-positions  incorporate  collages  of  cardboards,  fabric,  burlap  that  almost  exclusively  organize  the  subject.  Natural  mate-rials,  beans,  cauris,  fabrics,  simple  elements,  geometric  forms,  mathematical  signs,  of  the  plus  and  minus,  of  the  equalities,  express differences, inequalities of chance, of talent, the mul-titude  of  interests,  of  vocation,  which  germinate  a  being,  the  diversity which makes the beauty of the world. F.B
            </p>
