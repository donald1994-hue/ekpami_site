    <div class="col-md-6 artiste-content-traduction">
            <p>Self-taught  plastician,  Monel  Coovi  Domingo  has  known  to  affine  his  pictural  practice  according  to  his  readings,  mee-tings, various workshops. His painting is meant to be an ode to the transcendence of ideas, from intelligible, to the sensitive world,  a  purge  of  lyrical  abstraction  towards  realism.  In  his  paintings,  the  poetic  accents  of  his  slamic  thought  become  plastic, pictorial, to speak of his existentialist peregrinations, of his questions. The colors are bright to highlight the curative strength of the material. For he thinks : « to create is to live, to  bring  one  more  component  to  the  universe,  both  for  it’s  beauty,  than  to  educate  and  move  but  also  to  heal  through  colors  ».  Realism  and  lyrical  abstraction  are  thus  the  main  prisms  of  reflection  in  his  pictorial  production  which  thus finds its leaven in the transgression of principles inherent in contemporary art. S.A. (adapted)
            </p>
