            <p>Plasticien autodidacte, Monel Coovi Domingo a su affi-ner sa pratique picturale au gré de ses lectures, de ses rencontres,  des  divers  workshops.  Sa  peinture,  se  veut  une ode à la transcendance des idées, de l’intelligible, vers le monde sensible, une épuration de l’abstraction lyrique vers le réalisme. Dans ses toiles, les accents poé-tiques  de  sa  pensée  slamique  deviennent  plastiques,  picturaux, pour parler de ses pérégrinations existentia-listes, de ses questionnements. Les couleurs sont vives pour mettre en évidence la force curatrice de la matière car  pense-t-il,  ‘‘créer  c’est  vivre,  apporter  une  compo-sante  de  plus  à  l’univers,  tant  pour  le  beau,  que  pour  éduquer  et  émouvoir,  guérir  aussi  par  le  truchement  des couleurs’’. Le réalisme et l’abstraction lyrique consti-tuent les principaux prismes de réflexion de sa peinture qui trouve ainsi son ferment dans la transgression des principes, inhérente à l’art contemporain. S.A. (adapté)
