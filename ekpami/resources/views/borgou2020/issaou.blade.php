            <p>His  painting  desire  came  from  a  trip  to  Saudi  Arabia  where  the  discovery  of  calligraphy  made  him  think  of  the  shape  of  tree roots. This is where the sense of movement comes from, to which he has added the sense of forms and lines specific to his own culture, his Peuhl ethnic group, in his paintings de-voted to scenes of Beninese life. The verticality around which the canvas is composed is almost omnipresent: the verticality of the tree that takes root, rises like an axis, from the earth to the sky and spreads its branches; the colour, playing degrades or  sometimes  brutal  oppositions,  gives  the  same  feeling  of  an  upward  movement.  I  don’t  know  if  the  purpose  of  art  is  to deliver a message and deep down I don’t believe it. Touré offers  us  pure  beauty.  What  it  can  inspire  us  is  to  be  or  to  become vertical beings, strongly anchored, rooted in the soil of  their  particular  existence,  but  unfolding  and  opening  out  like trees of all the colours, fragrances and music of the world.A.Q (adapted)
