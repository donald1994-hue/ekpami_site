            <p>Son  désir  de  peinture  est  jailli  d’un  voyage  en  Arabie  Saou-dite où la découverte de la calligraphie lui a fait penser à la forme des racines des arbres. De là vient le sens du mouve-ment auquel il a rajouté celui des formes et des lignes spéci-fiques à sa propre culture, son ethnie peuhl dans ses tableaux consacrés à des scènes de vie béninoise. La verticalité autour de  laquelle  se  compose  la  toile  est  presque  omniprésente  :  verticalité  de  l’arbre  qui  prend  racines,  se  dresse  comme  un  axe, de la terre au ciel et répand ses rameaux ; la couleur, jouant  des  Dégrades  ou  des  oppositions  parfois  brutales,  donne ce même sentiment d’un mouvement ascendant. Je ne sais  si  le  but  de  l’art  est  de  délivrer  un  message  et  au  fond  je  ne  le  crois  pas.  Touré  lui  nous  offre  de  la  beauté  pure.  Ce  que celle-ci peut nous inspirer, c’est d’être ou de devenir des êtres  verticaux,  fortement  ancrés,  enracinés  dans  le  terreau  de leur existence particulière mais se déployant et s’ouvrant comme des arbres à toutes les couleurs, les fragrances et les musiques du monde. A.Q (adapté) 
